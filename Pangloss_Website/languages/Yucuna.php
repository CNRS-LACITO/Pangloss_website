      <P ALIGN="JUSTIFY">4. Quand un morph&egrave;me se termine par deux voyelles (diphtongue), la derni&egrave;re est nasalis&eacute;e. </P>
      <table width="80%" align="center">
        <tr>
          <td width="100pt">Exemple</td>
          <td width="100pt">[he'i&#x030B;]</td>
          <td width="100pt"><B>je&iacute;</B></td>
          <td width="100pt">&laquo;&nbsp;serpent&raquo;&nbsp;</td>
        </tr>
      </table>
      <P ALIGN="JUSTIFY">De m&ecirc;me, les voyelles pr&eacute;c&eacute;d&eacute;es d'un <B>j</B> sont g&eacute;n&eacute;ralement nasalis&eacute;es, quand elles sont au milieu ou &agrave; la fin d'un mot, ou lorsqu'elles pr&eacute;c&egrave;dent<B> &ndash; ka</B> ou &ndash; <B>ch</B>.</P>
      <table width="80%" align="center">
