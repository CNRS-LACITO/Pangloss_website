      <h3><a name="intro"></a>Introduction &agrave; la langue et &agrave; l'&eacute;criture yucuna</h3>
      <P ALIGN="JUSTIFY">Les Indiens &laquo;&nbsp;yucuna&nbsp;&raquo; renvoient &agrave; l'ensemble des indig&egrave;nes dont la caract&eacute;ristique commune est de parler habituellement au sein de leur famille d'origine, le yucuna, une langue appartenant &agrave; la famille linguistique Arawak (Schauer, 1975). Ces Indiens partagent de nombreux traits culturels avec les Tanimuca et Letuama, leurs principaux alli&eacute;s exogamiques, en raison de la similitude de leurs institutions (parent&eacute;, maloca, chamanisme). Mais leur culture contraste avec celles des autres populations environnantes (mira&ntilde;a, huitoto, tucano) sur une vaste zone g&eacute;ographique qui comprend le Miriti-Parana, le Popeyaca, et le Guacaya (Jacopin, 1970, 1977, 1981). </P>
      <P ALIGN="JUSTIFY">Actuellement, les Indiens de langue yucuna vivent principalement sur les rives du fleuve Miriti-Parana et dans la r&eacute;gion de La Pedrera sur le Bas Caqueta de la Colombie (voir carte). Selon diff&eacute;rentes estimations, cette population compte entre 500 et 1000 personnes. </P>
      <P ALIGN="CENTER"> <B>Carte. Localisation des Indiens de langue yucuna</B> <br>
          <IMG SRC="yucuna.jpg" alt="yucuna" WIDTH=80%> </P>
      <P ALIGN="JUSTIFY">&nbsp;</P>
      <P ALIGN="JUSTIFY">La langue yucuna a &eacute;t&eacute; &eacute;tudi&eacute;e pour la premi&egrave;re fois en 1963 par un couple de linguistes am&eacute;ricains du Summer Institute of Linguistics (SIL) qui a notamment publi&eacute; une phonologie (Schauer et Schauer, 1967), un texte traduit du mythe de <I>Carip&uacute; Laquena</I> (1975) et une grammaire (1978). Durant les ann&eacute;es quatre-vingt et quatre-vingt-dix, ils ont &eacute;galement &eacute;labor&eacute; et encadr&eacute; des travaux en langue destin&eacute;s &agrave; apprendre &agrave; lire et &agrave; &eacute;crire le yucuna dans les &eacute;coles bilingues (Matapi, 1984&nbsp;; Yucuna, 1994). Plus r&eacute;cemment, en dehors du Nouveau et de l&rsquo;Ancien Testament traduit en yucuna, ils ont &eacute;labor&eacute; une nouvelle &eacute;tude phonologique et syntaxique de la langue (Schauer et Schauer, 2000).</P>
      <P ALIGN="JUSTIFY">En ce qui concerne la notation graphique, ceux-ci ont choisi de se baser sur l'alphabet espagnol afin de faciliter son enseignement et son emploi chez les Yucuna et d'&eacute;viter certaines confusions orthographiques entre espagnol et yucuna. </P>
      <P ALIGN="JUSTIFY">Mais cette notation a suscit&eacute; de nombreuses discussions entre linguistes et indig&egrave;nes, sans que les uns et les autres parviennent dans la pratique &agrave; fixer une seule norme d&rsquo;&eacute;criture. </P>
      <P ALIGN="JUSTIFY">Actuellement, &agrave; en juger par les derniers travaux que nous avons pu consulter, et qui sont encore actuellement en phase pr&eacute;paratoire, il semblerait qu&rsquo;un certain consensus ait finalement &eacute;t&eacute; &eacute;tabli autour de certaines normes. Mais celles-ci restent pour ainsi dire &laquo;&nbsp;flottantes&nbsp;&raquo;, car elles ne sont encore ni compl&egrave;tement d&eacute;finies, ni utilis&eacute;es par tous les r&eacute;dacteurs de cette langue. Pour noter graphiquement notre corpus, nous avons donc repris les principales normes qui tendent &agrave; s&rsquo;imposer aujourd&rsquo;hui, et fait certains choix qu&rsquo;il convient d&rsquo;argumenter.</P>
