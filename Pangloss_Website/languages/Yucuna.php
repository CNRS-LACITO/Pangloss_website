      <P ALIGN="JUSTIFY">2. Pour le reste, la notation graphique des linguistes de la SIL a globalement &eacute;t&eacute; reprise, sauf quelques exceptions. </P>
      <P ALIGN="JUSTIFY">2.1. La premi&egrave;re modification touche leur &eacute;criture du /w/ (not&eacute; <B>hu)</B> et du /k/ (not&eacute;<B> c</B> devant <B>a</B>,<B> o</B> et <B>u</B>, et <B>qu </B>devant <B>e</B> et <B>i</B>), respectivement not&eacute;s <B>w</B> et <B>k</B> aujourd&rsquo;hui.</P>
      <P ALIGN="JUSTIFY">Remarque&nbsp;: cette nouvelle notation pose le probl&egrave;me des emprunts &agrave; l&rsquo;espagnol, car si l&rsquo;on change l&rsquo;orthographe de ces emprunts, cela peut entra&icirc;ner des confusions pour &eacute;crire en espagnol, et si l&rsquo;on garde leur orthographe d&rsquo;origine dans les textes yucuna, d&rsquo;autres confusions sont &agrave; attendre... Sans doute qu&rsquo;il serait pr&eacute;f&eacute;rable de garder les deux possibilit&eacute;s, en laissant le choix aux r&eacute;dacteurs indig&egrave;nes. </P>
      <P ALIGN="JUSTIFY">2.2. L&rsquo;autre modification importante par rapport &agrave; la notation initiale de la SIL&nbsp;concerne la <I>notation des voyelles qui encadrent une glottale.</I></P>
      <P ALIGN="JUSTIFY">Lorsque la glottale se situe entre deux voyelles de m&ecirc;me nature&nbsp;:&nbsp;<I>a&rsquo;a, e&rsquo;e</I>,<I> i&rsquo;i</I>, <I>o&rsquo;o</I>, <I>u&rsquo;u</I>, la deuxi&egrave;me n&rsquo;est pas not&eacute;e si la premi&egrave;re voyelle est plus accentu&eacute;e que la seconde, sauf quand elle se situe &agrave; la fin d'un morph&egrave;me.</P>
      <table width="80%" align="center">
        <tr>
          <td width="100pt">Exemple:</td>
          <td width="100pt">[i&#x0294;ipi't&#x0283;i]</td>
          <td width="100pt"><B>i'pich&iacute;</B></td>
          <td width="100pt">&laquo;&nbsp;ver&nbsp;&raquo;</td>
        </tr>
        <tr>
          <td/>      
          <td width="100pt">['a&#x0294;a]</td>
          <td width="100pt"><B>a&rsquo;a</B></td>
          <td width="100pt">&laquo;&nbsp;oui&nbsp;&raquo;</td>
        </tr>
      </table>
      <P ALIGN="JUSTIFY">Remarque&nbsp;: deux raisons nous ont incit&eacute; &agrave; adopter cette &eacute;conomie d'&eacute;criture. D'une part, parce que la notation de la voyelle non accentu&eacute;e appara&icirc;t redondante une fois que la phonologie a &eacute;t&eacute; d&eacute;finie, et d'autre part, parce qu'elle est souvent &agrave; peine audible, ou non prononc&eacute;e devant une consonne, ce qui n'a aucune incidence sur le sens du morph&egrave;me. La distinction entre 'CV&#x0294;
        VCV et 'CV&#x0294;
        CV n'est donc pas pertinente dans cette langue.</P>
      <P ALIGN="JUSTIFY">En revanche, la voyelle postglottale se note toujours lorsqu'elle est accentu&eacute;e. </P>
