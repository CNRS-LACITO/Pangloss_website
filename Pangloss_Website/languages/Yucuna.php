      <P ALIGN="JUSTIFY">3. Beaucoup de morph&egrave;mes &agrave; deux syllabes ont une voyelle allong&eacute;e dans la premi&egrave;re syllabe, m&ecirc;me s'ils s'&eacute;crivent avec une seule voyelle. </P>
