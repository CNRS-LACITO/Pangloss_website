      <P ALIGN="JUSTIFY">1. La plus importante ambigu&iuml;t&eacute; actuelle concerne sans doute les <I>r&egrave;gles de notation de l'accentuation</I>. A notre connaissance, aucune d&eacute;cision d&eacute;finitive n&rsquo;a encore &eacute;t&eacute; fix&eacute;e de mani&egrave;re consensuelle &agrave; ce sujet. Les linguistes de la SIL avaient choisi de reprendre les m&ecirc;mes r&egrave;gles que pour l&rsquo;espagnol, mais certains linguistes et instituteurs indig&egrave;nes ont opt&eacute;s pour la notation de l&rsquo;accent sur la voyelle la plus accentu&eacute;e quel que soit le morph&egrave;me. L&rsquo;ambigu&iuml;t&eacute; est telle que la plupart les indig&egrave;nes prennent rarement la peine de la noter (en tout cas jamais syst&eacute;matiquement), ce qui entra&icirc;ne de nombreuses confusions entre morph&egrave;mes (le m&ecirc;me probl&egrave;me se pose en tanimuca). Afin de r&eacute;duire les risques de confusions li&eacute;es aux diff&eacute;rences de r&egrave;gles orthographiques entre yucuna et espagnol, nous avons pr&eacute;f&eacute;r&eacute; nous tenir au choix des linguistes de la SIL. Par cons&eacute;quent, nous l&rsquo;explicitons de la mani&egrave;re suivante&nbsp;:</P>
      <P ALIGN="JUSTIFY">Comme tous les morph&egrave;mes de la langue yucuna se terminent par une voyelle, l'accent tonique est not&eacute; pour marquer l'accentuation, sauf lorsqu'il se place sur l'avant derni&egrave;re syllabe. </P>
