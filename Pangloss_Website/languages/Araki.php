        <p>L'araki n'est plus utilisé en public mais seulement dans un contexte privé. Cela explique pourquoi la langue a survécu dans certaines familles et pas dans d'autres. On le voit, par exemple, avec les dix enfants de Lele Moli et même avec certains des petits-enfants qui vivent avec lui : tous parlent très bien l'araki. Mais cette pratique étendue à trois générations est peu fréquente dans les autres clans familiaux.</p>
