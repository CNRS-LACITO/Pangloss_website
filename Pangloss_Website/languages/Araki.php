        <p class="intertitre"><a name="Araki"></a><a href="../corpus/show_text.php?id=crdo-AKR_HISTOIRE_ARAKI_SOUND&idref=crdo-AKR_HISTOIRE_ARAKI" target="_blank"><img src="../../images/icones/Txt_Inter_parchemin1.jpg" alt="a" width="20" height="20" border="0">Mythe d'origine : l'&icirc;le d'Araki</a> <a href="#texts"><img src="../../images/icones/fleche-haut.gif" alt="to texts" width="10" height="9" border="0"></a><br>
        Vanuatu, Espiritu Santo, Luganville, 1997, Moli Lele, <a href="../../membres/francois.htm">Alexandre Fran&ccedil;ois </a></p>
        <p>Ce mythe raconte comment l'île d'Araki, qui se trouvait à côté de Hog Harbour (au nord-est de l'île de Santo), décida un jour de partir le long des côtes de l'île de Santo pour finalement s'installer à son emplacement actuel, au sud de Santo. Toujours selon le mythe, l'île emporta les femmes de Hog Harbour. C'est un indice intéressant qui permet de comprendre ce récit en s'appuyant sur des bases historiques. La population actuelle d'Araki est considérée comme descendant d'un ancien groupe humain ayant vécu sur (ou près de) la côte est de Santo ; après un certain temps de bonnes relations – et spécialement d'échanges de femmes – avec la population de Hog Harbour, ce groupe aurait quitté la grande île et peuplé l'île d'Araki. D'autres interprétations sont néanmoins possibles et la question pourrait être résolue grâce à la linguistique historique ou à l'archéologie.</p>
        <p class="intertitre"><a name="RatHawkOctopus"></a><a href="../corpus/show_text.php?id=crdo-AKR_RAT_OCTOPUS_SOUND&idref=crdo-AKR_RAT_OCTOPUS" target="_blank"><img src="../../images/icones/Txt_Inter_parchemin1.jpg" alt="a" width="20" height="20" border="0">Le Rat, le Faucon et la Pieuvre</a> <a href="#texts"><img src="../../images/icones/fleche-haut.gif" alt="to texts" width="10" height="9" border="0"></a><br>
Vanuatu, Espiritu Santo, Luganville, 1997, Moli Lele, <a href="../../membres/francois.htm">Alexandre Fran&ccedil;ois </a></p>
        <p>Ce conte est un long et agréable récit des mésaventures du Rat en compagnie d'autres animaux : d'abord  des créatures vivant dans l'air et spécialement le Faucon (<em>Circus approximans</em>), ensuite des créatures marines et surtout la Pieuvre. Cette histoire comprend plusieurs parties : [1] Le Rat rame dans sa pirogue mais plusieurs oiseaux le harcèlent pour embarquer avec lui (thème à l'origine du chant) ; le bateau est sur le point de couler. [2] Sur le bateau, le Rat vole un morceau d'igname au Faucon ; le Faucon se venge en faisant couler le bateau. [3] Le Rat nage pour échapper à la mort et demande l'aide d'un requin, de Tortue, du Dauphin, et enfin de la Pieuvre qui accepte de le transporter. Mais quand la Pieuvre se rend compte que le Rat se moque d'elle, elle essaie de le tuer. [4] La dernière partie est une réflexion étiologique sur comment cette histoire considère aujourd'hui le rapport entre les rats et les pieuvres. Nous pensons que cette version mêle probablement plus d'une trame traditionnelle : la première partie, en particulier, a un crescendo qui devrait aboutir obligatoirement au naufrage du bateau, mais l'épisode du faucon apporte un nouveau rebondissement ; en fin de compte, le récit avec la pieuvre pourrait former une histoire à lui seul. Le résultat de cet ensemble est un récit agréable, poétique et très vivant, dans lequel chaque animal est investi par son équivalent humain. La personnification des animaux est évidente dans de nombreux détails de l'histoire (par exemple le rat qui  déjeune avec sa Maman ou les oiseaux qui l'effraient en racontant tout à leur Papa quand ce dernier rentre à la maison), mais elle est aussi perceptible au niveau linguistique, au travers du génitif et du marqueur de l'objet.</p>
        <p class="intertitre"><a name="Sope"></a><a href="../corpus/show_text.php?id=crdo-AKR_PASTOR_SOPE_SOUND&idref=crdo-AKR_PASTOR_SOPE" target="_blank"><img src="../../images/icones/Txt_Inter_parchemin1.jpg" alt="a" width="20" height="20" border="0">Le d&eacute;senvo&ucirc;tement de Sope</a> <a href="#texts"><img src="../../images/icones/fleche-haut.gif" alt="to texts" width="10" height="9" border="0"></a><br>
          Vanuatu, Araki I. South Espiritu Santo, 1998,  Moli  Sohe, <a href="../../membres/francois.htm">Alexandre Fran&ccedil;ois </a></p>
        <p>Ce récit religieux raconte comment un village (actuellement Sope) d'Araki fut délivré d'un maléfice du démon par un pasteur chrétien du nom de Sope. Le diable avait l'habitude de se cacher dans un arbre creux au bord de la route et tuait les gens en leur lançant de l'eau de pluie empoisonnée, conservée dans l'arbre. Ce tout-premier missionnaire qui appartenait au séminaire du T.T.I. (Institut d'enseignement de Tangoa), était l'un des premiers Presbytériens qui vinrent sur Araki pour combattre les &quot;âges sombres&quot; du paganisme, à la fin du XIXe siècle. Il libéra l'endroit des peurs païennes et des tabous, apportant la parole de Dieu. Le village de Sope prit son nom.</p>
        <p class="intertitre"><a name="coconutcrab"></a><a href="../corpus/show_text.php?id=crdo-AKR_CRABE_COCOTIER_SOUND&idref=crdo-AKR_CRABE_COCOTIER" target="_blank"><img src="../../images/icones/Txt_Inter_parchemin1.jpg" alt="a" width="20" height="20" border="0">La chasse au crabe de cocotier</a> <a href="#texts"><img src="../../images/icones/fleche-haut.gif" alt="to texts" width="10" height="9" border="0"></a><br>
        Vanuatu, Araki I. South Espiritu Santo, 1998, Ropo, <a href="../../membres/francois.htm">Alexandre Fran&ccedil;ois </a></p>
        <p>C'est l'histoire d'un esprit malin qui se transforma en crabe de cocotier et joua un méchant tour à deux hommes justement à la recherche de crabes de cocotier. C'est ainsi que le chasseur se transforme en  proie. Selon des croyances largement répandues à cet endroit, le pire avec les esprits diaboliques est leur capacité de se transformer en une personne ou un animal, en vue d'approcher leurs futures victimes ; c'est ainsi qu'ils &quot;mentent&quot; aux gens. La scène a lieu à Rahuna, un endroit particulier dans le &quot;bush&quot; à l'ouest d'Araki où les crabes de cocotier pullulent. Ce type de &quot;chasse&quot; est très apprécié sur l'île, car l'animal est apprécié pour ses qualités gustatives.</p>
