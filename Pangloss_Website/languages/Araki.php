      <h4><a name="vanuatu"></a>Vanuatu</h4>
      <p>L'araki est parlé dans une minuscule île, ronde et rocheuse, officiellement appelée Araki (les habitants l'appellent <strong>Daki</strong>) ; elle est  à seulement trois &quot;miles&quot; des côtes sud de Espiritu Santo, la plus grande île du territoire. Vanuatu, anciennement Nouvelles-Hébrides, est une république indépendante du Pacifique Sud [<em>carte 1</em>], peuplée principalement par des Mélanésiens qui s'y installèrent il y a environ trois mille ans.</p>
      <p>L'archipel du Vanuatu est très connu pour sa  densité linguistique, l'une des plus élévées au monde : plus de cent langues différentes sont parlées sur quatre-vingts îles par moins de 200 000 habitants [<em>carte 2</em>]. Toutes ces langues appartiennent au groupe océanique des langues Austronésiennes et sont souvent appelées langues &quot;mélanésiennes&quot;, bien que cette appellation corresponde à plusieurs groupes génétiques. L'araki appartient plus précisément à l'ensemble de langues que Clark (1985) a dénommé &quot;Vanuatu Nord et Centre&quot;. À côté de leur langue maternelle, tous les habitants du Vanuatu parlent le pidgin bislama, de base anglaise ; mais cette<em> lingua franca</em> n'est majoritairement utilisée que dans deux villes, Port-Vila et Luganville, et rarement en zone rurale.</p>
