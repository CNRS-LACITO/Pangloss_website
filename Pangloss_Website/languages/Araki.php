      <h4><a name="araki"></a>L'araki, une langue en train de disparaître <a href="#up"><img src="../../images/icones/fleche-haut.gif" alt="to up" width="10" height="9" border="0"></a></h4>
        <p> Le nombre de locuteurs de l'araki a été donné par plusieurs auteurs  : 80 (Tryon and Charpentier 1989) et même  105 (SIL 1983), mais ces chiffres ont été manifestement exagérés. Une étude sociolinguistique en profondeur de l'île (Varisipite and Bogiri-Vari 1998) montre que bien que 34 personnes affirment parler l'araki, la majorité d'entre eux ne l'utilisent pas habituellement. En fait, il y a eu une baisse importante du nombre de locuteurs depuis trois ou quatre générations, ce qui fait qu'il n'y a actuellement pas plus de quinze locuteurs qui comprennent et parlent araki couramment. La plupart d'entre eux sont nés entre 1920 et 1960, ce qui signifie que cette langue a cessé d'être utilisée au quotidien, dans la plupart des familles de cette île, il y a une quinzaine d'années.</p>
