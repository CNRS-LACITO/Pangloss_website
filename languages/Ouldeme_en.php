  <p>This story was recorded in the village of Dibon in the Uldeme Mountains, on the 10th of May, 1976, during the evening in dry season, in traditional settings.<br>
    At the birth of a child with an oversized round head, only one arm and one leg, two parents decide to leave their home for good, leaving the monstrous child behind. But the eldest child, who is as yet a young girl, insists on bringing him along. Seeing this, the parents abandon both children on the way. Later on, after having asked his sister to beat him against a tamarind tree, then a &ldquo;hailstone tree&rdquo; (<em>Combretaceae Anogeissus leiocarpa</em>), the young boy gets his members back, then a whole young man&rsquo;s body. He then builds a house with a metal roof, and marries eight wives. Meanwhile, the old parents are wandering around in the bush, attempting to survive on wild roots. So the boy magically sends them food. Then he goes out on a horse and meets them. At first, the parents think he is a Mandara and are afraid of him, but end up recognizing their son, who offers to take them in.</p>
