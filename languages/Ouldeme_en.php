        <h4 class="intertitre"><a name="presentation"></a>Presentation </h4>
        <p>The eastern edge of the Mandara Mountains (North Cameroon), is inhabited by Chadic language speakers, with populations ranging from 2,000 to 15,000 people. Among these the Uldeme population size is situated in the middle. Some of their neighbors are the Podokos, the Moukt&eacute;l&eacute;s, the Moras-massif, the Vam&eacute;-br&eacute;m&eacute;s, the Madas, the Ourzos and the Mouyangs.<br>
          Uldeme territory is a small &ldquo;peninsula mount&rdquo; of three kilometers by six, leaning against the central plateau of the Mandara mountains. Their territory is very distinct due to the large valleys of the Mayo Plata and Uldeme which encircle it. Going through it, the houses are invisible and the chain resembles a fortress. The inhabitants live at the summit and on the higher slopes between 650 and 900 meters. Currently, some villages have begun descending and are grouped together in the foothills. <br>
          The mountain is located in a region of wooded savanna, where the climate is dry and the vegetation scarce most of the year. The rains fall between June and August. The landscape is only green during that period. It is during the rainy season that the inhabitants, who have a long history of farming, cultivate their fields, the main crop being millet. <br>
          The lifestyles of these populations, of animist tradition, contrast with those of the city dwelling Muslims, the Fulbe and the Mandaras, often merchants (our story alludes to this). As in the rest of the Mandara Mountains, the Uldeme economy revolves around obtaining sustenance in harsh surroundings, and has kept its equilibrium in autarchy. This balance is threatened by efforts to evolve towards a cash based economy. The traditional farms, viable in autarchy, have culled everything possible from the natural environment. Therefore the possibilities for improvement are relatively limited. Farming is done with hoes. In these conditions, raising a handicapped child is a heavy burden, as the story suggests. This narrative, which still pertains to their current lifestyle, is part of the stock of preferred stories among the young Uldeme. It is this reality that guided our choice.</p>
        The Uldeme language, like the other Chadic languages, can, through its roots and certain grammatical features, be attached to the Chamito-Semitic group. For more information concerning verbal affixes and its unusual phonemes, such as lateral fricative consonants, one may consult the works listed below.
