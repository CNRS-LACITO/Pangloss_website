      <h1 class="intertitre"><a name="up"></a>N&ecirc;l&ecirc;mwa Corpus </h1>
      <p class="intertitre">&nbsp;</p>
      <p class="intertitre"><a href="../../ALC/Languages/Nelemwa_popup.htm" target="_blank" onClick="window.open(this.href,'popupLink','width=400,height=400,scrollbars=yes,resizable=yes',1);return false"><img src="../../images/icones/info.gif" alt="a" width="15" height="15" border="0">N&ecirc;l&ecirc;mwa</a>: a brief note </p>
      <p class="intertitre">&nbsp;</p>
      <h4 align="left">Resources :</h4>
      <p class="intertitre">
      <img src="../../images/icones/h_parleur.gif" alt="a" width="20" height="20"> <a href="../tools/list_rsc_en.php?lg=Nelemwa&aff=Nêlêmwa" target="_blank"> All available resources here
      </a></p>
      <p class="intertitre">&nbsp;</p>
      <p><em>N&ecirc;l&ecirc;mwa</em> and the dialectal variant <em>Nixumwak</em> are two of the twenty-nine Kanak languages of New Caledonia which belong to the Oceanic subgroup of Austronesian languages. They are spoken by approximately 1500 speakers, in the far north of the Mainland of New Caledonia (around Koumak, Poum, Tiabet and the neighbouring islands).</p>
      <p>&nbsp;</p>
      <ul class="liste-liens">
        <li><a href="#biblio">Existing documents</a></li>
        <li><a href="#transcription">Transcription</a></li>
        <li><a href="#characteristics">Main characteristics</a></li>
        <li><a href="#texts">N&ecirc;l&ecirc;mwa texts</a></li>
      </ul>
      <h4><a name="biblio"></a>Existing documents </h4>
      <ul>
        <li>BRIL I., 2002, <em>Le</em><em> n&ecirc;l&ecirc;mwa (Nouvelle-Cal&eacute;donie)&nbsp;: Analyse syntaxique et s&eacute;mantique</em>, Collection &ldquo;Langues et Cultures du Pacifique&rdquo;, n&deg; 16, Peeters, Paris. (that you can see <a href="../../vient-de-paraitre/bril_nelemwa.htm" target="_blank">here</a>)</li>
        <li>BRIL I., 2000, <em>Dictionnaire n&ecirc;l&ecirc;mwa-nixumwak-fran&ccedil;ais-anglais.</em> LCP 14, Peeters, Paris. (that you can see <a href="../../vient-de-paraitre/bril_dico-nelemwa.htm" target="_blank">here</a>)</li>
        <li>HAUDRICOURT A.-G., 1963, <em>La langue des Nenemas et des Nigoumak, Dialectes de Poum et de Koumac, Nouvelle-Cal&eacute;donie</em>. Linguistic Society of New Zealand, Auckland. </li>
