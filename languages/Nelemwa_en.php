      <p>This is the story of Kaavo (a chief's elder daughter) Ara-Pai ma Paima. She becomes Te&acirc; Paak's (Chief of War) fifth wife. The other spouses, Green Fly, Mosquito, Horse-fly and Midget, do not accept Kaavo's presence among them. One day, Te&acirc; Paak leaves home to attend a ceremony and the other wives kill her. The story tells his adventures during his journey in search of Kaavo : his encounter and alliance with a mythical being by the Weiyem River and their descent to the Country of the Dead.<br>
      </p>
