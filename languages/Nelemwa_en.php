        </li>
      </ul>
      <h4><a name="transcription"></a>Transcription</h4>
      <ul>
        <li> Morpheme boundaries are indicated by hyphens. </li>
        <li> Vowel length is marked by a double vowel ; phonological nasal vowels are written with a circumflex accent and central vowels are marked by a diaresis. </li>
        <li>Consonant lenition in intervocalic position: <br>
          /p/ ou /f/ &gt; [v] ou [w] /r h/ &gt; [r] &nbsp; /fw/ &gt; [w] /c/ ou / &#643;/ &gt; [j] &nbsp; /t/ &gt; [r], [l] /k/ ou /x/ &gt; [ &#611;]&gt;&Oslash;&nbsp; <br>
          Intervocalic consonant lenition occurs at word boundaries, either initially or finally: <br>
          <em>ni v&acirc;&acirc;l&ucirc;</em> &quot;in the taro fields&quot; (<em>p&acirc;&acirc;l&ucirc;</em> &quot;taro field&quot;) ; <em>jeux-i na</em> &quot;close to me&quot; (<em>jeuk</em> &quot;near&quot;). </li>
      </ul>
      <ul>
        <li> Final sandhi are signalled by a hyphen : <em>hleeli pe-whaayaw-i hla</em> &quot;their wars&quot; (<em>whaayap</em> &quot;fight, war&quot;) ; <em>pe-faxer-i hli</em> &quot;their taboo relationship&quot; (<em>faxet</em> &quot;taboo&quot;). <br>
        </li>
      </ul>
      <h4><a name="characteristics"></a>Main characteristics <a href="#up"><img src="../../images/icones/fleche-haut.gif" alt="up" width="10" height="9" border="0"></a></h4>
      <ul>
        <li>Head-initial language. </li>
        <li> Numeral classifiers (around 20) and possessive classifiers (for food, weapons, baskets). </li>
        <li> VOS order with nominal arguments ; sVo order with personal pronouns. </li>
        <li> Ergative morphology with nominal arguments ; accusative pattern with personal pronouns. <br>
          The agent markers are <em>(e)a</em> (+human) and <em>ru</em> (-human). Absolutive case is zero-marked. </li>
        <li> Topicalisation (of a fronted argument or adjunct) is marked by <em>xe</em>. </li>
        <li> For a limited set of verbs, reflexivity is marked by coreferential subject and object pronouns : <em>i bwagi-e</em> &quot;he returns&quot; (<em>lit</em>. he returns him). </li>
        <li> According to noun classes, possession may be either direct (marked by possessive suffixes or nouns) or indirect (marked by the morpheme <em>i</em>). Relational nouns have an obligatory 3rd person suffix (<em>-n</em> +humans ; <em>-t</em> +inanimates, generic or collective humans). </li>
      </ul>
