        Nouvelle-Cal&eacute;donie, Tiabet, 1990, Philippe Dahoot, <a href="../../membres/bril.htm">Isabelle Bril</a></p>
      <p>This is the story of how a clan's tutelary spirits gave &quot;the magic for fishing&quot; to one of their ancestor. The story takes place south of Tiabet. One day, the ancestor, who is settled on the east coast, in the Bay of N&ocirc;&ocirc;muja, decides to visit his relatives on the west coast, at Hawawalic. He encounters his tutelary spirits on the sea-shore at Bweebun, steals their fishing-nets and hikes back home to N&ocirc;&ocirc;muja, across the central ridge over the Wiiwu Pass. The spirits run after him and retrieve their fishing-net,... in exchange for a magic for fishing.</p>
      <p class="intertitre"><a name="Dela"></a><a href="../tools/show_text_en.php?id=crdo-NEE_DELA_SOUND&idref=crdo-NEE_DELA"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0"> Kaavo Dela</a> <a href="#texts"><img src="../../images/icones/fleche-haut.gif" alt="to texts" width="10" height="9" border="0"></a><br>
        Nouvelle-Cal&eacute;donie, Tiabet, 1990, Phadom Rony, <a href="../../membres/bril.htm">Isabelle Bril</a></p>
      <p>This is the story of Kaavo (a chief's elder daughter) Dela, renowned for her beauty, and Te&acirc; Pwayili (Pwayili is the name of a Chief's younger son). One day, Te&acirc; Pwayili decides to leave in search of Kaavo Dela, whose residence is on an island to the north of the Mainland, and he convinces her to marry him. After a few adventures, they settle in Ouvea, but one day, Pwayili offends his wife.</p>
