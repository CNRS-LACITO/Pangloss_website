      <h1 align="left">Ubykh Corpus </h1>
      <p align="left">&nbsp;</p>
      <p class="intertitre"><a href="../../ALC/Languages/Ubykh_popup.htm" target="_blank" onClick="window.open(this.href,'popupLink','width=400,height=400,scrollbars=yes,resizable=yes',1);return false"><img src="../../images/icones/info.gif" alt="a" width="15" height="15" border="0">Ubykh</a>: a brief note</p>
      <p class="intertitre">&nbsp;</p>
      <p>Ubykh, studied notably by the French Caucasologist Georges Dum&eacute;zil (1898-1986), achieved a measure of celebrity as the poster-child of endangered languages before the demise of the last speaker in 1992. Its rich consonant inventory is legendary among linguists.</p>
      <p>In 1968 Dum&eacute;zil made ten tapes with the last speaker, Mr. Tevfic Esen&ccedil; (or Tevfic Sani&ccedil;) in Paris. We present here a series of recordings of seven stories found on the third of these tapes. Each track begins with an announcement of the French title.</p>
      <p>Transcriptions and word-by-word French glosses in Dum&eacute;zil's own handwriting have been found for four of the stories and are made available here as scanned images in pdf files. For one of the stories, "Eating fish makes you clever", Dumézil made a second transcription containing morpheme as well as word glosses for a non-specialist linguist, Mme Christine Leroy. This second annotation is in a sufficiently legible hand that we undertook, with the help of Mme Dina Dabjen-Bailly, a speaker of Adyge, to computerize the transcription and the word-glosses and to synchronize them with the recording for browsing. Sentence-level translations were composed on the basis of the word and morpheme glosses by a linguist (Boyd Michailovsky) with no knowledge of Ubykh, again with the help of Mme Bailly.</p>
      <p align="left">&nbsp;</p>
      <h4 align="left">Resources :</h4>
      <p class="intertitre">
      <img src="../../images/icones/h_parleur.gif" alt="a" width="20" height="20"> <a href="../tools/list_rsc_en.php?lg=Ubykh&aff=Ubykh" target="_blank"> All available resources here
      </a></p>
      <p class="intertitre">&nbsp;</p>
      <p class="intertitre"><a href="../tools/show_text_en.php?id=crdo-UBY_POISSON_SOUND&idref=crdo-UBY_POISSON"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0">  Browse the story &quot;Eating Fish Makes you Clever&quot;</a></p>
      <p>The following is a complete list of the recordings and annotations:</p>
      <p> 1. &quot;The Goat and the Sheep&quot; recording (wav); Dum&eacute;zil annotation (pdf).<br>
        2. &quot;Eating Fish Makes you Clever&quot; recording (wav); Dum&eacute;zil annotation (pdf); second annotation (pdf).<br>
        3. &quot;The World Ends Tomorrow&quot; recording (wav); Dum&eacute;zil annotation (pdf).<br>
        4. &quot;Hodjah is Dead&quot; recording (wav); Dum&eacute;zil annotation (pdf).<br>
        5. &quot;The Narts&quot; recording (wav).<br>
        6. &quot;The Ubykh Exodus&quot; recording (wav).<br>
        7. &quot;From Istanbul to Paris&quot; recording (wav).</p>
  
      <p>The complete set of ten tapes was digitalized at the Lacito in 2003 by M. Alexis Michaud, who undertook to locate, catalogue, and digitalize Parisian Ubykh materials as part of a pre-doctoral project on language documentation and archiving. The Ubykh vocabularies and other elicitations which make up the greater part of the recordings will be made available at a later date.</p>
      <p>For details concerning the Dum&eacute;zil system of transcription, see in particular:</p>
      <p>Charachidze, Georges, 1989, Ubykh. In: Greppin, John, ed. T<em>he Indigenous languages of the Caucasus</em>, vol. 2: Hewitt, George, ed. The North West Caucasian Languages. Caravan. Delmar, NY. 357-459.</p>
      <p>Colarusso, John. 1994. How many consonants does Ubykh have? Hewitt, G., ed. <em>Caucasian Perspectives</em>. Munich. Lincom Europa.</p>
      <p>Dum&eacute;zil, Georges, 1965, <em>Documents anatoliens sur les langues et les traditions du Caucase III</em>, Nouvelles &Eacute;tudes Oubykhs. Institut d'Ethnologie. Paris.</p>
      <p>Vogt, Hans. 1963. <em>Dictionnaire de la Langue Oubykh</em>. Universitets Forlaget. Oslo.</p>
      <p><strong><br>
