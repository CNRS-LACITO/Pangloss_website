      </ul>
      <p>&nbsp;</p>
      <h2>New annotations by Brian Fell, 2010</h2>
      <p>In March 2008, Brian Fell, a student of linguistics working on Ubykh, proposed to prepare transcriptions and English translations of the seven stories for us, an unexpected offer we were glad to accept. He sent us computerized annotations of the four stories for which we had Dumézil's manuscript transcriptions later the same year, and revised them slightly in 2010. These transcriptions are based on Dumézil's mss. transcriptions, which are difficult to interpret reliably without knowledge of Ubykh, with minor corrections. These, and further annotations are available below: <strong><strong> </strong></strong></p>
      <ul class="liste-liens">
        <li> <strong><strong><a href="http://lacito.vjf.cnrs.fr/pangloss/tools/show_text_en.php?id=crdo-UBY_CHEVRE_SOUND&idref=crdo-UBY_CHEVRE">The goat and the sheep</a></strong></strong></li>
        <li> <strong><strong><a href="http://lacito.vjf.cnrs.fr/pangloss/tools/show_text_en.php?id=crdo-UBY_POISSON_SOUND&idref=crdo-UBY_POISSON">Eating fish makes you clever</a></strong></strong></li>
        <li> <strong><strong><a href="http://lacito.vjf.cnrs.fr/pangloss/tools/show_text_en.php?id=crdo-UBY_FIN_DU_MONDE_SOUND&idref=crdo-UBY_FIN_DU_MONDE">The world ends tomorrow</a></strong></strong></li>
        <li> <strong><strong><a href="http://lacito.vjf.cnrs.fr/pangloss/tools/show_text_en.php?id=crdo-UBY_HODJA_SOUND&idref=crdo-UBY_HODJA">The Hodja is dead</a></strong></strong></li>
      </ul>
      <p>Brian Fell's annotation of the three remaining stories, for which we have no transcription or translation at present, will be posted when it is ready.</p>
      <p class="intertitre">During a visit to Paris in 2010 at our invitation, Brian Fell helped us complete the annotation of Dumézil's recordings of Ubykh vocabulary, elicited (usually in Turkish) to illustrate Ubykh phonology and verify the perception of some of the phonemic oppositions. The final versions were computerized and synchronized with the recordings by Tanguy Solliec during an internship in 2010.
      </p>
      <p>Brian Fell is currently working on a dictionary of Ubykh. Contact: bfell1618@gmail.com </p>
      <p>&nbsp;</p>
          
