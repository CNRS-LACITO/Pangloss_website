            <h2><a name="monosyll">Monosyllabic words read in carrier
sentence (audio and electroglottographic recordings)</a></h2>
            <p>These materials
were elicited in 2004 to study the lexical tones of Naxi. Forty minimal
sets such as /<span style="font-weight: bold;">lɑ́</span>/
&lsquo;to strike&rsquo;, /<span style="font-weight: bold;">lɑ̄</span>/
&lsquo;tiger&rsquo;, /<span style="font-weight: bold;">lɑ̀</span>/
&lsquo;hand&rsquo; were selected (i.e. a total of 120
syllables). As monosyllables only constitute a fraction of the lexicon,
it was not possible to be picky in terms of part of speech (N, V and
ADJ had to be included) or phonemic composition. The set of initials is
/<span style="font-weight: bold;">pʰ
tʰ kʰ tsʰ ʈʂʰ, p k ts, g, l m n, s ʂ h</span>/
plus the empty-onset filler /<span style="font-weight: bold;">ʝ</span>/;
vowels are /<span style="font-weight: bold;">i
y ɯ u e ɤ o ə˞ &aelig; ɑ wɑ v̩</span>/.&nbsp;</p>
            <p>The consultant was
shown the target items in writing, as part of a randomized list on
paper. The imagined context was that people are watching a TV programme
about calligraphy. The target words were recorded under two reading
conditions: the context for the first reading, referred to as CAREFUL,
is that a child asks which character is being written, and the
consultant provides the answer. Under the second condition, referred to
as IMPATIENT, the child is naughty, asking for the umpteenth time
without paying attention to answers; the consultant says the answer
again, but in a way that will clarify to the child that (s)he is being
rude and should not speak up again. The reason for asking consultants
to imagine a TV show, rather than a live performance, is so that they
will not feel called upon to whisper the answer in order not to disturb
the (fictitious) calligrapher. Minor adjustments to the carrier
sentence were made in consultation with the speakers: in particular,
use of the topic marker /<span style="font-weight: bold;">ʂɤ́</span>/
after the target item is mandatory in
some dialects. These adjustments detract somewhat from the symmetry of
the data, but they helped consultants feel fully comfortable with the
task.</p>
            <p>In addition to IPA
transcription, orthographic representation in romanized Naxi is
provided (except for the first 3 documents by speaker M5). Additional
information, such as observations about the speakers' phonemic systems,
is provided as notes in the XML document (annotation).&nbsp;</p>
