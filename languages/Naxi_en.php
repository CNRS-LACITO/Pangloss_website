            <p>&nbsp;<br>
With a view to avoiding large discrepancies between the
transcription in International Phonetic Alphabet and the actual
pronunciation, <b>the
transcription adopted here is not strictly
phonemic</b>. In particular:</p>
            <ul>
              <li>the palatal
nasal [ɲ] can be analyzed as an
allophone
of /ŋ/, as can other palatal consonants: [ci] can be analyzed as /ki/,
and so on.</li>
              <li>onset-less
syllables receive an
empty-onset-filler:
/i/ is realized as [ʝi], /u/ as [wu], /ɯ/ as [ɣɯ], etc. (This is
similar to Naxi.) These syllables are here transcribed with their
phonetic onset, as [ʝi], [ɣɯ], etc.</li>
            </ul>
            <p>On the other hand,
the apical vowels [ʅ] and [ɿ]
are
phonemicized as /ɯ/. </p>
            <p>The following
conventions are used for <b>passages
to be added or removed</b>
(following Martine Mazaudon's usage
for Tamang):</p>
            <table border="2">
              <tbody>
                <tr>
                  <td>[ ]</td>
                  <td>square
brackets indicate an addition to be
made
to the text (as indicated by the speaker when the transcription was
done)</td>
                </tr>
                <tr>
                  <td>&lt;
&gt;</td>
                  <td>angled
brackets indicate a 'false start' or
mistaken use of terms, and hence a passage to be suppressed (again, as
indicated by the speaker when the transcription was done)</td>
                </tr>
              </tbody>
            </table>
            <p><b>Comments/notes:</b>
The annotation
comprises notes, inserted in the XML code with the following markup: <br>
&lt;NOTE message="This is a comment."/&gt;</p>
            <p>These include
comments about verifications that
were
made about form and meaning, and comments about phonetic
implementation. The earliest notations, which were later corrected
systematically, are closer to phonetic realizations;
surface-phonological transcriptions abstract away from the actual
acoustics.&nbsp;</p>
