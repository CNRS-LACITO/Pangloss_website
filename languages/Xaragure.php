      <h1 align="center"><a name="up"></a>Corpus x&acirc;r&acirc;gur&egrave;</h1>
      <p class="intertitre">&nbsp;</p>
      <p class="intertitre">Fiche technique sur le <a href="../../ALC/Languages/Xaragure_popup.htm" target="_blank" onClick="window.open(this.href,'popupLink','width=400,height=400,scrollbars=yes,resizable=yes',1);return false">x&acirc;r&acirc;gur&egrave;<img src="../../images/icones/info.gif" alt="a" width="15" height="15" border="0"></a></p>
            <p align="left">&nbsp;</p>
      <h4 align="left">Ressources :</h4>
     <p align="left" class="intertitre">
      <img src="../../images/icones/h_parleur.gif" alt="a" width="20" height="20"> <a href="../tools/list_rsc.php?lg=Xaragure&aff=xârâgurè" target="_blank">Accès à toutes les ressources</a>
      </p>
       <p align="left" class="intertitre">&nbsp;</p>

      <ul class="liste-liens">
        <li><a href="#Bwotemere">Le mont Bw&ouml;t&eacute;m&ecirc;r&ecirc;</a></li>
        <li><a href="#Kaataamwaroo">Kaataamwaroo</a></li>
        <li><a href="#Nefasia">Le massacre de la N&egrave;fasia</a></li>
        <li><a href="#Koum">Les trois femmes de Koum</a></li>
        <li><a href="#Marata">La r&eacute;ponse de Marat&acirc;</a></li>
        <li><a href="#Nyiichaa">La proph&eacute;tie de Nyiichaa</a></li>
        <li><a href="#wood">Discours cérémoniel</a></li>
        <li><a href="#Trema">Feuille de Trema et feuille de Cerisier</a></li>
        <li><a href="#Giroo">Le jeune Gir&ouml;&ouml; et sa m&egrave;re</a></li>
        </ul>
      <p>&nbsp;</p>
      <p class="intertitre"><a name="Bwotemere"></a><a href="../tools/show_text.php?id=crdo-AXX_BOTAME_SOUND&idref=crdo-AXX_BOTAME"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0">Le mont Bw&ouml;t&eacute;m&ecirc;r&ecirc;</a><br>
        Nouvelle-Cal&eacute;donie, Thio, 1977, Agnès Tonwiri, <a href="../../membres/moyse.htm">Claire Moyse-Faurie </a></p>
      <p>Ce mythe, tout comme celui sur Kaataamwaroo, traite du d&eacute;licat probl&egrave;me du mariage de filles de chef. L'alliance se veut lointaine, en dehors des r&egrave;gles habituelles : l'&eacute;poux recherch&eacute; appartient primordialement au monde naturel ; dans ce mythe, Bw&ouml;t&eacute;m&ecirc;r&ecirc; est un serpent de mer. Elle r&eacute;ussit cependant, car la soeur a&icirc;n&eacute;e fait venir la m&egrave;re de Bw&ouml;t&eacute;m&ecirc;r&ecirc; afin de rendre supportable l'exil &agrave; son fils.</p>
      <p class="intertitre"><a name="Kaataamwaroo"></a><a href="../tools/show_text.php?id=crdo-AXX_NETOO_SOUND&idref=crdo-AXX_NETOO"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0">Kaataamwaroo</a><br>
        Nouvelle-Cal&eacute;donie, Thio, 1977, Agnès Tonwiri, <a href="../../membres/moyse.htm">Claire Moyse-Faurie </a></p>
      <p>Ce mythe, tout comme celui du Bw&ouml;t&eacute;m&ecirc;r&ecirc;, traite du d&eacute;licat probl&egrave;me du mariage de filles de chef. L'alliance se veut lointaine et en dehors des r&egrave;gles habituelles: Kaataamwaroo est un &quot;caillou&quot;. Ce type d'alliance, avec installation chez la femme, et non chez l'homme, ne peut r&eacute;ussir qu'au prix de concessions. Dans ce r&eacute;cit, l'alliance &eacute;choue et le mari est rendu &agrave; son &eacute;tat premier.</p>
      <p class="intertitre"><a name="Nefasia"></a><a href="../tools/show_text.php?id=crdo-AXX_NEFASIA_SOUND&idref=crdo-AXX_NEFASIA"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0">Le massacre de la N&egrave;fasia</a><br>
        Nouvelle-Cal&eacute;donie, Grand-Borindi, 1980, Albert Nékaré, <a href="../../membres/moyse.htm">Claire Moyse-Faurie </a></p>
      <p>Cette histoire relate un triste &eacute;pisode de la guerre entre les Europ&eacute;ens et les Kanaks de la r&eacute;gion de Thio-Borindi. Une trahison (obtenue &agrave; la suite d'un chantage) serait &agrave; l'origine de la quasi extermination des gens de l'ancienne tribu de Koum (situ&eacute;e dans la cha&icirc;ne centrale), qui auraient &eacute;t&eacute; fusill&eacute;s par les Europ&eacute;ens &agrave; l'embouchure de la rivi&egrave;re N&egrave;fasia.</p>
      <p class="intertitre"><a name="Koum"></a><a href="../tools/show_text.php?id=crdo-AXX_PAPEE_SOUND&idref=crdo-AXX_PAPEE"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0">Les trois femmes de Koum</a><br>
        Nouvelle-Cal&eacute;donie, Grand-Borindi, 1980, Albert Nékaré, <a href="../../membres/moyse.htm">Claire Moyse-Faurie </a></p>
      <p> Ce court r&eacute;cit veut rendre compte de la pr&eacute;sence de trois rochers insolites, situ&eacute;s &agrave; mi-chemin entre la plage et la montagne, entre la rivi&egrave;re Ngoi et la rivi&egrave;re Ni, alors que de tels rochers ne se rencontrent que dans la cha&icirc;ne, &agrave; Koum. Il s'agirait en fait de trois femmes originaires de Koum, parties &agrave; la p&ecirc;che aux coquillages au bord de mer, et qui se seraient laiss&eacute;es surprendre par le jour... Comme quoi il ne faut jamais c&eacute;der trop longtemps &agrave; ses d&eacute;sirs !</p>
      <p class="intertitre"><a name="Marata"></a><a href="../tools/show_text.php?id=crdo-AXX_MARATA_SOUND&idref=crdo-AXX_MARATA"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0">La r&eacute;ponse de Marat&acirc;</a><br>
        Nouvelle-Cal&eacute;donie, Petit-Borindi, 1980, Valentin Oundo, <a href="../../membres/moyse.htm">Claire Moyse-Faurie </a></p>
      <p>R&eacute;cit expliquant comment les Kanaks de Borindi furent d&eacute;poss&eacute;der des terres de Ngoi, par la faute du vieux Marat&acirc;. Celui-ci, n'ayant plus toute sa t&ecirc;te, aurait r&eacute;pondu de travers aux gendarmes venus le questionner.</p>
      <p class="intertitre"><a name="Nyiichaa"></a><a href="../tools/show_text.php?id=crdo-AXX_NYIICHA_SOUND&idref=crdo-AXX_NYIICHA"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0">La proph&eacute;tie de Nyiichaa</a><br>
        Nouvelle-Cal&eacute;donie, Petit-Borindi, 1980, Valentin Oundo, <a href="../../membres/moyse.htm">Claire Moyse-Faurie </a></p>
      <p>    Histoire de l'arriv&eacute;e de la religion catholique &agrave; Borindi, due &agrave; la clairvoyance du vieux Nyiichaa. Celui-ci pr&eacute;dit une &egrave;re de paix si les P&egrave;res sont bien accueillis.</p>
      <p class="intertitre"><a name="wood"></a><a href="../tools/show_text.php?id=crdo-AXX_PURA_SOUND&idref=crdo-AXX_PURA"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0">Discours cérémoniel / [axx] Xw&acirc;p&ucirc;r&acirc;</a><br>
        Nouvelle-Cal&eacute;donie, Thio, 1980, Raphaël Oundo, <a href="../../membres/moyse.htm">Claire Moyse-Faurie </a></p>
      <p>Cette harangue pr&eacute;c&eacute;dant le &quot;discours sur le bois&quot; (<em>xw&acirc;xa</em>) incite les gens &agrave; se regrouper pour la danse.</p>
      <p class="intertitre"><a name="Trema"></a><a href="../tools/show_text.php?id=crdo-AXX_NESOO_SOUND&idref=crdo-AXX_NESOO"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0">Feuille de Trema et feuille de Cerisier</a><br>
        Nouvelle-Cal&eacute;donie, Thio, 1980, Agnès Tonwiri, <a href="../../membres/moyse.htm">Claire Moyse-Faurie </a></p>
      <p>    Ce conte conna&icirc;t plusieurs variantes, mais toutes ont la particularit&eacute; de former un r&eacute;cit en boucle. La version pr&eacute;sent&eacute;e ici met en sc&egrave;ne, &agrave; c&ocirc;t&eacute; des deux personnages principaux, feuille de Trema (<em>Trema vieillardii</em> Schltr., Ulmac&eacute;es) et feuille de &quot;cerisier bleu&quot; (<em>Elaeocarpus persicifolius</em> Br. et Gr., &Eacute;la&eacute;ocarpac&eacute;es), des enfants malfaisants qui seront punis de leur malveillance, avant que le cours des &eacute;v&eacute;nements ne reprenne. Dans d'autres versions, les deux h&eacute;ro&iuml;nes, Trema et Cerisier bleu, se jouent mutuellement des tours, tout aussi scatologiques. La curiosit&eacute; d'un intrus, feuille d'Acacia, permettra de remonter le fil du temps, et la boucle de l'histoire pourra se refermer.</p>
      <p class="intertitre"><a name="Giroo"></a><a href="../tools/show_text.php?id=crdo-AXX_GIROO_SOUND&idref=crdo-AXX_GIROO"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0">Le jeune Gir&ouml;&ouml; et sa m&egrave;re</a><br>
        Nouvelle-Cal&eacute;donie, Thio, 1980, Agnès Tonwiri, <a href="../../membres/moyse.htm">Claire Moyse-Faurie </a></p>
      <p>Dans ce conte, le jeune Gir&ouml;&ouml; r&eacute;ussit &agrave; sauver sa m&egrave;re des dents d'une ogresse. Ils s'en d&eacute;barrassent ensuite gr&acirc;ce &agrave; la ruse et &agrave; la vigilance du jeune gar&ccedil;on. </p>
