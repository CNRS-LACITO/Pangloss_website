      <h1 align="left">Corpus arabe y&eacute;m&eacute;nite</h1>
            <p align="left">&nbsp;</p>
      <h4 align="left">Ressources :</h4>
     <p align="left" class="intertitre">
      <img src="../../images/icones/h_parleur.gif" alt="a" width="20" height="20"> <a href="../tools/list_rsc.php?lg=Yemeni Arabic&aff=arabe yéménite" target="_blank">Accès à toutes les ressources</a>
      </p>
       <p align="left" class="intertitre">&nbsp;</p>

      <p class="intertitre">Fiche technique sur l'<a href="../../ALC/Languages/Yemeni_Arabic_popup.htm" target="_blank" onClick="window.open(this.href,'popupLink','width=400,height=400,scrollbars=yes,resizable=yes',1);return false">arabe y&eacute;m&eacute;nite<img src="../../images/icones/info.gif" alt="a" width="15" height="15" border="0"></a></p>
      <p align="left">&nbsp;</p>
      <p>Traditionnellement, les femmes de Sanaa disent des contes au cours de r&eacute;unions de femmes qui se d&eacute;roulent tant&ocirc;t chez l'une, tant&ocirc;t chez l'autre, de fa&ccedil;on quasi quotidienne, entre la pri&egrave;re de l'apr&egrave;s-midi et celle du coucher du soleil. Les &eacute;v&eacute;nements de la vie courante (maladie, mort, naissance, retour de voyage, &hellip;) servent de pr&eacute;texte &agrave; ces r&eacute;unions qui sont surtout l'occasion de r&eacute;gler des litiges familiaux, des conflits de voisinage, ou encore de contracter des alliances.<br>
        Lorsque les circonstances s'y pr&ecirc;tent, comme c'est le cas pour une naissance, une partie du temps est consacr&eacute;e &agrave; &eacute;couter des contes et &agrave; jouer aux devinettes.<br>
        Contes et devinettes contribuent &agrave; l'apprentissage et &agrave; la transmission des r&egrave;gles de sociabilit&eacute; f&eacute;minine. &Agrave; travers eux, les femmes acqui&egrave;rent l'art de l'&eacute;change et de la concession. Avec l'introduction de la t&eacute;l&eacute;vision dans les foyers, force est cependant de constater que ces pratiques tendent &agrave; se rar&eacute;fier.<br>
        En arabe y&eacute;m&eacute;nite de Sanaa, les mots qui r&eacute;f&egrave;rent au conte  et &agrave; la devinette sont fl&eacute;chis sur la m&ecirc;me racine.<br>
        Ces deux genres litt&eacute;raires ont &eacute;galement en commun de se pr&eacute;senter &agrave; l'int&eacute;rieur d'un dispositif formel, ou &quot;cadre&quot;, constitu&eacute; par une formule d'entr&eacute;e et une formule de cl&ocirc;ture.<br>
        Dans le conte, auquel les femmes pr&ecirc;tent une vertu pacificatrice, ces formules sont une invocation &agrave; la protection divine. Dans la devinette, la formule de cl&ocirc;ture est pr&eacute;c&eacute;d&eacute;e d'un &eacute;change dialogu&eacute; au cours duquel les partenaires de jeu contractent des dettes et en n&eacute;gocient les modalit&eacute;s de r&egrave;glement. &quot;Les devinettes, disent les femmes, sont souvent cause de s&eacute;paration, de dispute et de mort. Ce sont les contes qui rapprochent les gens.&quot;</p>
      <p class="intertitre"><a href="../tools/show_text.php?id=crdo-AYN_SAMIA_SOUND&idref=crdo-AYN_SAMIA"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0"> Conte y&eacute;m&eacute;nite de Sanaa</a><br>
      Y&eacute;men, Sanaa, 1989, <a href="../../membres/naim.htm">Samia Na&iuml;m </a></p>
      <p>Le conte pr&eacute;sent&eacute; ici a &eacute;t&eacute; recueilli &agrave; Sanaa, en 1989, aupr&egrave;s d'une femme &acirc;g&eacute;e d'une quarantaine d'ann&eacute;es. L'enregistrement s'est d&eacute;roul&eacute; chez elle, en l'absence de toute autre femme, ce qui explique les commentaires qu'elle a pu faire &agrave; mon intention ; ceux-ci pr&eacute;sentent le double int&eacute;r&ecirc;t de soulever la question de l'intercompr&eacute;hension dialectale (ph&eacute;nom&egrave;nes de contacts &agrave; l'int&eacute;rieur d'une m&ecirc;me langue, l'arabe) et de renvoyer aux appr&eacute;ciations linguistiques que les locuteurs portent sur leur propre parler<br>
        La transcription est rigoureusement fid&egrave;le &agrave; la bande-son en ce qu'elle note les variantes contextuelles, phon&eacute;tiques et prosodiques (faits d'accentuation, de pause, de rythme) relev&eacute;es au niveau du phon&egrave;me (voisement, d&eacute;voisement, allongement, r&eacute;duction, effacement) et de la syllabe (modification de la structure syllabique).</p>
