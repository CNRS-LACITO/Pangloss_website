      <p class="intertitre">&nbsp;</p>
      <h3>Introduction to the Yucuna language and writing system (see <a href="#intro">below</a>)</h3>
            <p align="left">&nbsp;</p>
      <h4 align="left">Resources :</h4>
      <p class="intertitre">
      <img src="../../images/icones/h_parleur.gif" alt="a" width="20" height="20"> <a href="../tools/list_rsc_en.php?lg=Yucuna&aff=Yucuna" target="_blank"> All available resources here
