      <P ALIGN="JUSTIFY">1. The greatest remaining ambiguity probably resides in the rules for noting stress. As far as we know, no definitive consensus has been reached on the subject. The SIL linguists have chosen to apply the same rules as for Spanish, while other linguists and local teachers note stress on the accented vowel, independently of its morphological status. Given the confusion, most native speakers do not note stress (or at least not systematically), which leads to much confusion between morphemes – this is also the case in Tanimuca. To avoid such confusion here, we have chosen to follow the SIL linguists' system. This system works as follows: </P>
      <P ALIGN="JUSTIFY">As all morphemes in Yucuna end in a vowel, stress is noted to show accentuation, except when it falls on the penultimate syllable. </P>
      <table width="80%" align="center">
        <tr>
          <td width="100pt">Exemple :</td>
          <td width="100pt">[pipi'na]</td>
          <td width="100pt"><B>pipin&aacute;</B></td>
          <td width="100pt">&quot;your enemy&quot;</td>
        </tr>
        <tr>
          <td/>      
          <td width="100pt">[hi'mit&#x0283;i]</td>
          <td width="100pt"><B>jimichi </B></td>
          <td width="100pt">&quot;grass&quot;</td>
        </tr>
      </table>
      <P ALIGN="JUSTIFY">Stress is phonologically relevant as it often differentiates between two distinct morphemes.</P>
      <table width="80%" align="center">
        <tr>
          <td width="100pt">Exemple :</td>
          <td width="100pt">[&#x027e;i'&#x027e;a]</td>
          <td width="100pt"><B>rir&aacute;</B></td>
          <td width="100pt">&quot;he is sawing&quot; </td>
        </tr>
        <tr>
          <td/>      
          <td width="100pt">['&#x027e;i&#x027e;a]</td>
          <td width="100pt"><B>rira</B></td>
          <td width="100pt">&quot;his blood&quot;</td>
        </tr>
      </table>
      <P ALIGN="JUSTIFY">2. Everywhere else, the SIL linguists' graphic notation system has been adopted, with just a few exceptions. </P>
      <P ALIGN="JUSTIFY">2.1. The first exception concerns their notation of /w/ (noted hu) and /k/ (noted c before a, o and u, and qu before e and i), which are now respectively noted w and k.</P>
      <P ALIGN="JUSTIFY">Remark: this new notation is made more problematic because of borrowings from Spanish. If one changes the spelling of these borrowings, this could lead to confusion for those who wish to write in Spanish, but if the original spelling is retained in Yucuna texts, this could also lead to quite some confusion… The best solution is probably to leave the question open, so that native writers may make their own choice. </P>
      <P ALIGN="JUSTIFY">2.2. The other major modification which has been applied to the initial SIL notation system concerns <em>vowels in a glottal context</em>. </P>
      <P ALIGN="JUSTIFY">When a glottal stop is surrounded by two identical vowels, e.g. a'a, e'e, i'i, o'o, u'u, the second is not noted if the first bears more stress than the second, unless the second is in morpheme final position. </P>
      <table width="80%" align="center">
        <tr>
          <td width="100pt">Exemple:</td>
          <td width="100pt">[i&#x0294;ipi't&#x0283;i]</td>
          <td width="100pt"><B>i'pich&iacute;</B></td>
          <td width="100pt">&quot;worm&quot;</td>
        </tr>
        <tr>
          <td/>      
          <td width="100pt">['a&#x0294;a]</td>
          <td width="100pt"><B>a&rsquo;a</B></td>
          <td width="100pt">&quot;yes&quot;</td>
        </tr>
      </table>
      <P ALIGN="JUSTIFY">Remark: there are two reasons behind our choice of only noting one vowel. First, once the phonology has been defined, noting the second non stressed vowel appears redundant; and second, because it is often barely audible, or is not pronounced before a consonant, which in no way affects the meaning of the morpheme. Thus the distinction between 'CVʔ VCV and 'CVʔ CV is not relevant in Yucuna.</P>
      <P ALIGN="JUSTIFY">On the contrary, the vowel following the glottal stop is always noted when it bears stress. </P>
      <table width="80%" align="center">
        <tr>
          <td width="100pt">Exemple:</td>
          <td width="100pt">[&#x027e;i&#x0294;'imi]</td>
          <td width="100pt"><B>ri&rsquo;imi</B></td>
          <td width="100pt">&quot;his meat&quot;</td>
        </tr>
      </table>
      <P ALIGN="JUSTIFY">When one of the two vowels bears the morpheme's primary stress, the stress must be noted, unless it falls on the penultimate syllable. </P>
      <table width="80%" align="center">
        <tr>
          <td width="100pt"></td>
