      <h3 align="left">Yucuna Texts</h3>
      <p align="left" class="intertitre"><a href="../tools/show_text_en.php?id=crdo-YCN_KEYAKO_SOUND&idref=crdo-YCN_KEYAKO"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0"> The Story of Keyako</a><br>
        Colombia, Amazonas, Mirití-Parana, Jariyé, 1998, Milciades Yucuna, <a href="../../membres/fontaine.htm">Laurent Fontaine</a></p>
      <p align="left" class="intertitre">This is the story of the first indigenous Yucuna language speaker, from the Kamejeya tribe, to have established regular contacts with Whites, by going down the Caqueta and Japura (the same river on the Brazilian side) all the way to a Brazilian outpost (identified here as Tefé), to exchange objects manufactured locally by indigenous servants (from the Jurumi and Jupichiya tribes) for White goods. However, when he returns home, there are rumors abroad: his wife is said to have organized parties while he was away, in order to drink with her lovers… </p>
      <p align="left" class="intertitre"><a href="../tools/show_text_en.php?id=crdo-YCN_PIMI_MAWARI_SOUND&idref=crdo-YCN_PIMI_MAWARI"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0">The Myth of the Cocoi Heron and the Hummingbird</a><br>
        Colombia, Amazonas, Camaritagua, 2003i, Hua'mé Matapi, <a href="../../membres/fontaine.htm">Laurent Fontaine</a></p>
      <p align="left" class="ZonePrint">The Myth of the Cocoi Heron (<em>Ardea cocoi</em>) and the Hummingbird.</p>
      <p align="left" class="intertitre"><a href="../tools/show_text_en.php?id=crdo-YCN_KURUKURU_JUWA_SOUND&idref=crdo-YCN_KURUKURU_JUWA"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0">The Story of the Green Ibis and the Great Egret</a><br>
        Colombia, Amazonas, Camaritagua, 2003, Hua'mé Matapi, <a href="../../membres/fontaine.htm">Laurent Fontaine</a></p>
      <p align="left">The Story of the Green Ibis (<em>Mesembrinibis cayennensis</em>) and the Great Egret (<em>Casmerodius albus</em>).</p>
      <p align="left" class="intertitre"><a href="../tools/show_text_en.php?id=crdo-YCN_KANUMA_SOUND&idref=crdo-YCN_KANUMA"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0">The Story of Kanumá</a><br>
        Colombia, Amazonas, La Pedrera, 2005, Mario Matapi, <a href="../../membres/fontaine.htm">Laurent Fontaine</a></p>
      <p align="left">For the Yucuna speaking Indians, Kanumá was the first man. A primitive at first, he lived with the Amazons, a tribe of supernatural women called the Namatu. They behaved like men, and made him do the cooking like a woman. In secret, the women performed the Yurupari, and hid the rite's sacred flutes from Kanumá, but one day he managed to steal them… After chasing Kanumá for quite some time, the Namatu gave up and went down the Miriti river, then the Caqueta river, until they reached the Amazon delta. Kanumá was left on his own. He then met the two daughters of Je'chú (the Sky), one of whom became his wife, bringing him tubers (cassava, yams), garden fruits (chontaduro, pineapples, etc.), and even coca. </p>
      <p align="left" class="intertitre"><a href="../tools/show_text_en.php?id=crdo-YCN_KOMEYAPHU_SOUND&idref=crdo-YCN_KOMEYAPHU"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0">The Myth of Komeyaphu</a><br>
        Colombia, Amazonas, La Pedrera, 2005, Arturo Yucuna, <a href="../../membres/fontaine.htm">Laurent Fontaine</a></p>
      <p align="left">The myth of Komeyaphu (Rainbow) is that of a man who went with his children to stay with the son of the Moon, who lived near the mouth of the Amazon river, near the sea. Once there, Komeyaphu decides to hunt some small animals for his children, but much to his surprise they are all gigantic and give him considerable trouble…</p>
      <p align="left">&nbsp;</p>
      <h3><a name="intro"></a>Introduction to the Yucuna language and writing system</h3>
      <P ALIGN="JUSTIFY">The term &quot;Yucuna&quot; Indians refers to the group of Native Americans who habitually speak Yucuna with their families. The Yucuna language belongs to the Arawak family (Schauer, 1975). The Yucuna Indians share many cultural traits with the Tanimuca and the Letuama, their main exogamic partners, as well as similar practices (kinship organization, maloca, shamanism). Their culture differs however from that of the other neighboring tribes (Miraña, Huitoto, Tucano) covering a vast geographical zone, including Miriti-Parana, Popeyaca, and Guacaya (Jacopin, 1970, 1977, 1981). </P>
      <P ALIGN="JUSTIFY">Today the Yucuna Indians mainly live on the banks of the Miriti-Parana river and in the area of La Pedrera on the Lower Caqueta in Colombia (see map). Estimates set their population at between 500 and 1000 people. </P>
      <P ALIGN="CENTER"> <B>Map. Geographical location of the Yucuna speaking Indians </B> <br>
        <IMG SRC="yucuna.jpg" alt="yucuna" WIDTH=80%></P>
      <P ALIGN="JUSTIFY">&nbsp;</P>
      <P ALIGN="JUSTIFY">The Yucuna language was first documented in 1963 by an American linguist couple from the Summer Institute of Linguistics (SIL). They published a phonology (Schauer et Schauer, 1967), a translation of the Caripú Laquena myth (1975) and a grammar (1978). In the 1980s and 1990s they also prepared materials and trained assistants in Yucuna for literacy programs in bilingual schools (Matapi, 1984; Yucuna, 1994). More recently, they have translated the Old and New Testaments into Yucuna and have published a new phonological and syntactic study of the language (Schauer and Schauer, 2000)..</P>
      <P ALIGN="JUSTIFY">For the writing system, they chose to base the Yucuna alphabet on that of Spanish in order to facilitate its teaching and use among the Yucuna and avoid spelling confusion between Spanish and Yucuna. </P>
      <P ALIGN="JUSTIFY">This notation system however has led to much debate among linguists and Yucuna speakers, and no final writing system has yet been adopted. . </P>
      <P ALIGN="JUSTIFY">Judging by the most recent work I have been able to consult but which has yet to be published, some standards have been agreed upon, despite the fact that they still need to be defined explicitly, and are not used by all of the language's writers. To transcribe our data, we have used the main standards which are currently most in use, and have made a certain number of choices which will be explained below.</P>
