<h3><a name="anecdotes"></a>Les anecdotes <a href="#up"><img src="../../images/icones/fleche-haut.gif" alt="to up" width="10" height="9" border="0"></a></h3>
       <p>     Conter des anecdotes en nashta est une des thématiques préférées des derniers locuteurs. Celles-ci provoquent l'hilarité de l'assistance mais elles sont parfois difficiles à comprendre pour un auditeur extérieur car elles sont ancrées dans un savoir partagé. Nous avons choisi les plus complètes.</p>
       <p class="intertitre"><a href="../tools/show_text.php?id=crdo-MKD_BERGER_SOUND&idref=crdo-MKD_BERGER" target="_blank"><img src="../../images/icones/h_parleur.gif" alt="a" width="20" height="20" border="0">  Le berger et son ombre</a><br>
Grèce, Liti,  2004, locutrice anonyme, <a href="../../membres/adamou.htm">Evangelia Adamou</a></p>
       <p>Cette brève histoire raconte la naïveté d'un berger qui avait peur de son ombre. La confondant avec quelqu'un qui n'arrête pas de le suivre, il lui donne tous les beignets que sa mère lui avait préparés.</p>
       <p class="intertitre"><a href="../tools/show_text.php?id=crdo-MKD_COUPLE_SOUND&idref=crdo-MKD_COUPLE" target="_blank"><img src="../../images/icones/h_parleur.gif" alt="a" width="20" height="20" border="0"> Le couple qui se disputait</a><br>
        Grèce, Liti, avril 2002, locutrice anonyme, <a href="../../membres/adamou.htm">Evangelia Adamou</a>       </p>
       <p>Cette anecdote raconte les soucis de la vie conjugale. Le couple, à qui le juge interdit de s'insulter, se met à mimer avec les mains leurs insultes favorites "gros-bide" et "chassieuse".</p>
       <p class="intertitre"><a href="../tools/show_text.php?id=crdo-MKD_DIMITRO_SOUND&idref=crdo-MKD_DIMITRO" target="_blank"><img src="../../images/icones/h_parleur.gif" alt="a" width="20" height="20" border="0"> Le petit Dimitro et les haricots</a><br>
        Grèce, Liti, avril 2002, locutrice anonyme, <a href="../../membres/adamou.htm">Evangelia Adamou</a>       </p>
       <p>Cette anecdote s'appuie sur l'ambiguïté de la langue lorsque l'on ignore la pragmatique. Le petit Dimitro aide sa mère à la cuisine. Mais, à chaque fois que sa mère lui demande de mettre "de la bouse aux haricots", au lieu de mettre la bouse dans le feu, il la met dans la casserole.</p>
       <p class="intertitre"><a href="../tools/show_text.php?id=crdo-MKD_FANTOMES_SOUND&idref=crdo-MKD_FANTOMES" target="_blank"><img src="../../images/icones/h_parleur.gif" alt="a" width="20" height="20" border="0"> Les fantômes</a><br>
        Grèce, Liti, avril 2002, locutrice anonyme, <a href="../../membres/adamou.htm">Evangelia Adamou</a>       </p>
       <p>Encore une anecdote, dont le héros est le père de la locutrice. La croyance aux fantômes étant établie, un jeune valeureux décide de les affronter. Il va donc à leur rencontre, couteau en main, en pleine nuit. Toutefois, il ne réussit qu&rsquo;à tuer un petit âne, qui a eu le malheur d&rsquo;éternuer devant le chasseur de fantômes&nbsp;!</p>
       <p>&nbsp;</p>
       <h3><a name="traditions"></a>Traditions <a href="#up"><img src="../../images/icones/fleche-haut.gif" alt="to up" width="10" height="9" border="0"></a></h3>
       <p>     Parler des traditions n'est pas chose simple. Ces récits donnent tout de même une idée de ce qui faisait le fondement de la vie collective.</p>
       <p class="intertitre"><a href="../tools/show_text.php?id=crdo-MKD_CONFRERIE_SOUND&idref=crdo-MKD_CONFRERIE" target="_blank"><img src="../../images/icones/h_parleur.gif" alt="a" width="20" height="20" border="0"> Les confréries</a><br>
        Grèce, Liti,  2003, locuteur anonyme, <a href="../../membres/adamou.htm">Evangelia Adamou</a>       </p>
       <p>Comme l'indique le locuteur, les confréries étaient une institution importante (qui est encore en partie vivante). Les confréries du village réunissaient des jeunes d'une même génération, mais elles n'étaient pas professionnelles. Cette coutume répandue dès le Moyen Âge dans toute l'Europe chrétienne était en effet organisée par le clergé. Il s'agissait d'institutions de solidarité et de contrôle lors des festivités et des cérémonies : mariage, funérailles, fêtes religieuses.</p>
       <p class="intertitre"><a href="../tools/show_text.php?id=crdo-MKD_FETE_SOUND&idref=crdo-MKD_FETE" target="_blank"><img src="../../images/icones/h_parleur.gif" alt="a" width="20" height="20" border="0"> Les festivités au village</a><br>
        Grèce, Liti,  2004, locutrice anonyme, <a href="../../membres/adamou.htm">Evangelia Adamou</a>       </p>
       <p>Ce texte conte brièvement le déroulement des festivités lors d'événements religieux. Comme dans le texte "confréries" on retrouve la coutume des enchères afin que le plus offrant se voie dédier l'icône d'un saint. On aperçoit aussi les règles sociales qui régissent la danse, l'ordre des danseurs dépendant de la date de leur mariage.</p>
       <p class="intertitre"><a href="../tools/show_text.php?id=crdo-MKD_MAIRE_SOUND&idref=crdo-MKD_MAIRE" target="_blank"><img src="../../images/icones/h_parleur.gif" alt="a" width="20" height="20" border="0"> Les institutions</a><br>
        Grèce, Liti,  2006, locuteur anonyme, <a href="../../membres/adamou.htm">Evangelia Adamou</a>       </p>
       <p>Ce texte porte sur les élections des maires, la collecte des impôts, le clergé et les instituteurs tant durant la dernière période de l'empire ottoman que dans le royaume de Grèce.</p>
       <p class="intertitre"><a href="../tools/show_text.php?id=crdo-MKD_MARIAGE_SOUND&idref=crdo-MKD_MARIAGE" target="_blank"><img src="../../images/icones/h_parleur.gif" alt="a" width="20" height="20" border="0"> Le mariage</a><br>
         Grèce, Liti,  2006, locuteur anonyme, <a href="../../membres/adamou.htm">Evangelia Adamou</a>       </p>
       <p>&nbsp;</p>
       <h3><a name="vie-quotidienne"></a>La vie quotidienne <a href="#up"><img src="../../images/icones/fleche-haut.gif" alt="to up" width="10" height="9" border="0"></a></h3>
       <p>     Ces textes sont peu représentés car souvent très personnels. Toutefois, les récits de vie portent souvent sur la guerre en Albanie contre l'armée italienne fasciste et l'armée nazie. Les récits des locuteurs qui n'ont pas fait la guerre portent sur leur vie professionnelle. Nous avons choisi un texte qui porte sur les jeux de l'enfance.</p>
       <p class="intertitre"><a href="../tools/show_text.php?id=crdo-MKD_JEUX_SOUND&idref=crdo-MKD_JEUX" target="_blank"><img src="../../images/icones/h_parleur.gif" alt="a" width="20" height="20" border="0"> Les jeux</a><br>
        Grèce, Liti,  2006, locuteur anonyme, <a href="../../membres/adamou.htm">Evangelia Adamou</a>       </p>
       <p>Ce texte raconte brièvement les jeux des enfants dans les années trente. Il fait aussi une référence à la place périphérique de l'école dans leur vie et à leur obligation d'assistance à leur famille.</p>
       <p>&nbsp;</p>
       <h3><a name="savoir-faire"></a>Le savoir-faire <a href="#up"><img src="../../images/icones/fleche-haut.gif" alt="to up" width="10" height="9" border="0"></a></h3>
       <p class="intertitre"><a href="../tools/show_text.php?id=crdo-MKD_SOIE_SOUND&idref=crdo-MKD_SOIE" target="_blank"><img src="../../images/icones/h_parleur.gif" alt="a" width="20" height="20" border="0"> La soie</a><br>
        Grèce, Liti,  2003, locuteur anonyme, <a href="../../membres/adamou.htm">Evangelia Adamou</a>       </p>
       <p>La fabrication de la soie dans le village était une activité importante au début du vingtième siècle, comme c'était le cas aussi dans certaines régions du sud de la France (voir aussi la version romanesque dans "La soie" de Alessandro Barico). Ici, le locuteur relate la préparation des vers à soie par sa mère. Par d'autres récits nous savons que la soie était destinée à la vente mais aussi à la fabrication des vêtements.</p>
       <p class="intertitre"><a href="../tools/show_text.php?id=crdo-MKD_MOISSON_SOUND&idref=crdo-MKD_MOISSON" target="_blank"><img src="../../images/icones/h_parleur.gif" alt="a" width="20" height="20" border="0"> La moisson</a><br>
        Grèce, Liti,  2003, locuteur anonyme, <a href="../../membres/adamou.htm">Evangelia Adamou</a>       </p>
       <p>Ce récit porte sur la moisson, un des aspects les plus importants de la vie du village. Il parle brièvement des outils, de l&rsquo;organisation du travail dans les champs (étrangement, on n'y retrouve pas le système de mise en commun de la main d'œuvre entre les familles), ainsi que des repas habituels.</p>
       <p class="intertitre"><a href="../tools/show_text.php?id=crdo-MKD_CHAMPS_SOUND&idref=crdo-MKD_CHAMP" target="_blank"><img src="../../images/icones/h_parleur.gif" alt="a" width="20" height="20" border="0"> La vie dans les champs</a><br>
        Grèce, Liti, avril 2002, locutrice anonyme, <a href="../../membres/adamou.htm">Evangelia Adamou</a></p>
       <p>Ce texte démarre sur la vie dans les champs pour s'ouvrir à une confrontation des mœurs de l'époque avec l'époque moderne. Les linguistes qui s'occupent de documentation des langues en voie de disparition savent que cette confrontation est une thématique courante chez les locuteurs anciens qui ont vu leur monde changer et qui regardent le passé avec une certaine nostalgie.</p>
       <p>&nbsp;</p>
       <h3><a name="Pear-Story"></a>Pear Story <a href="#up"><img src="../../images/icones/fleche-haut.gif" alt="to up" width="10" height="9" border="0"></a></h3>
       <p>     Nous avons présenté au locuteur les vidéos de Chafe "Pear Story".  Il s'agit d'un petit voleur qui vole un panier plein de poires à un paysan qui est en train de cueillir les fruits sur l'arbre.</p>
       <p class="intertitre"><a href="../tools/show_text.php?id=crdo-MKD_POIRES_SOUND&idref=crdo-MKD_POIRES" target="_blank"><img src="../../images/icones/h_parleur.gif" alt="a" width="20" height="20" border="0"> Pear Story 1</a><br>
        Grèce, Liti,  2011, locuteur anonyme, <a href="../../membres/adamou.htm">Evangelia Adamou</a>       </p>
       <p class="intertitre"> Le locuteur regarde une première fois le film. La deuxième fois il décrit le film en le regardant&nbsp;; nous avons donc le récit synchronisé à la vidéo.</p>
       <p class="intertitre"><a href="../tools/show_text.php?id=crdo-MKD_POIRES_SOUND&idref=crdo-MKD_POIRES_PASSE"><img src="../../images/icones/h_parleur.gif" alt="a" width="20" height="20" border="0"> Pear Story 2</a><br>
        Grèce, Liti,  2011, locuteur anonyme, <a href="../../membres/adamou.htm">Evangelia Adamou</a>       </p>
       <p>Le deuxième enregistrement a consisté à résumer l'histoire.</p>
       <p>&nbsp;</p>
       <h3><a name="conte"></a>Un conte <a href="#up"><img src="../../images/icones/fleche-haut.gif" alt="to up" width="10" height="9" border="0"></a></h3>
       <p>     Au moment de l'enquête la tradition orale était déjà bien éteinte. Les locuteurs se souviennent de peu de contes.</p>
       <p class="intertitre"><a href="../tools/show_text.php?id=crdo-MKD_CONTE_SOUND&idref=crdo-MKD_CONTE" target="_blank"><img src="../../images/icones/h_parleur.gif" alt="a" width="20" height="20" border="0"> La fille et le fantôme</a><br>
Grèce, Liti,  2004, locutrice anonyme, <a href="../../membres/adamou.htm">Evangelia Adamou</a></p>
       <p>Dans ce texte, la locutrice est âgée d'une soixantaine d'années, elle fait donc partie des "rememberers", mais sa connaissance de la langue n'est pas bonne. Cela se voit très bien dans la phonologie, qui est celle du grec moderne scolaire, mais aussi dans les réflexions métalinguistiques ("comment dit-on ?"). Contrairement à nos attentes d'"authenticité", ce conte a été entendu chez les refugiées grecques d'Asie Mineure qui se sont installées dans le village. Il est donc traduit en nashta à notre demande.    </p>
