          <p>ROMBI Marie-Fran&ccedil;oise, 1983, <em>Le shimaore (&icirc;le de Mayotte, Comores). Premi&egrave;re approche d'un parler de la langue comorienne</em>, Paris, Peeters-Sefaf.</p>
