        <h4 class="intertitre">&nbsp;</h4>
        <h4 class="intertitre"><a name="presentation"></a>Pr&eacute;sentation </h4>
        <p>L'&icirc;le de Mayotte (Maore), situ&eacute;e &agrave; l'extr&eacute;mit&eacute; m&eacute;ridionale de l'archipel des Comores, &agrave; quelque 300 km de Madagascar et environ 70 km d'Anjouan, comprend la Grande Terre s&eacute;par&eacute;e par un bras de mer de 4 km de la Petite Terre et du rocher de Dzaoudzi.<br>
          La superficie globale de Mayotte est de 374 km2 et sa population estim&eacute;e &agrave; 150 000 habitants.<br>
          La pr&eacute;sence fran&ccedil;aise, pendant la premi&egrave;re p&eacute;riode de la colonisation (1843-1975) s'est surtout fait sentir sur le rocher de Dzaoudzi, capitale des Comores jusqu'en 1966.<br>
          Tandis que les trois autres &icirc;les &ndash; Grande-Comore (Ngazidja), Anjouan (Ndzuani) et Moh&eacute;li (Mwali) &ndash; acc&eacute;daient &agrave; l'ind&eacute;pendance en 1975, Mayotte reste jusqu'&agrave; ce jour d&eacute;pendance fran&ccedil;aise. Les raisons de cette diff&eacute;rence de choix sont d'ordre politique. Des deux points de vue, culturel et linguistique, Mayotte appartient &agrave; l'ensemble comorien.<br>
          Il y a de plus &agrave; Mayotte deux communaut&eacute;s linguistiques distinctes : l'une, majoritaire, parle le maore, parler ressortissant au groupe linguistique bantu, l'autre, dans une vingtaine de villages &eacute;parpill&eacute;s dans l'&icirc;le, parle un dialecte malgache. Les malgachophones parlent mahorais mais la r&eacute;ciproque n'est pas vraie. Deux langues mais une culture unique, caract&eacute;ris&eacute;e entre autres par l'Islam.</p>
          <p>Ce parler de Mayotte est une langue bantu au m&ecirc;me titre que le ngazidja de Grande Comore et il ne faut surtout pas le confondre avec un autre parler de Mayotte, minoritaire, qui est un dialecte malgache et qui rel&egrave;ve donc, lui, des langues austron&eacute;siennes. (Ces deux parlers appartiennent &agrave; deux familles linguistiques diff&eacute;rentes, mais les habitants de Mayotte, qu'ils parlent l'une ou l'autre langue, ont tous la m&ecirc;me culture domin&eacute;e par l'Islam).</p>
          <p>Le maore pr&eacute;sente des diff&eacute;rences sensibles par rapport au ngazidja et au mwali (parlers des deux &icirc;les les plus septentrionales de l'archipel des Comores) et s'apparente nettement plus au parler de l'&icirc;le voisine d'Anjouan (le nzuani). </p>
