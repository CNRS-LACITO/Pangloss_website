      <h1 align="center">Corpus lazé</h1>
      <p class="intertitre">&nbsp;</p>
      <p class="intertitre">Fiche technique sur le <a href="../../ALC/Languages/Laze_popup.htm" target="_blank" onClick="window.open(this.href,'popupLink','width=400,height=400,scrollbars=yes,resizable=yes',1);return false">lazé<img src="../../images/icones/info.gif" alt="a" width="15" height="15" border="0"></a><br>
        Carnet de terrain sur le lazé (<a href="../fieldwork/Laze_fw-2008.htm">Étude de la langue lazé, 2008</a>)</p>
      <p class="intertitre">&nbsp;</p>
     <p> <b>Le lazé</b>, à ne pas confondre avec la langue caucasienne laze, est parlé dans quelques villages de la préfecture de Muli (province du Sichuan, Chine). Le nombre total de ses locuteurs est certainement inférieur à 1.000, et sans doute inférieur à 400. Cette langue appartient au groupe naish de la famille sino-tibétaine, qui comprend également le naxi et le lazé. Elle est également appelée, en chinois, 拉热 ou 木里水田话. </p>
      <p>&nbsp;</p>
      
       <p class="intertitre"><a href="http://lacito.vjf.cnrs.fr/pangloss/tools/show_text.php?id=crdo-NBF_ENTERREEVIVE_SOUND&idref=crdo-NBF_ENTERREEVIVE"><img src="../../images/icones/Txt_Inter_parchemin1.jpg" alt="a" width="20" height="20" border="0"> Enterrée vive</a><br>
        Muli, Liangshan, Sichuan, 2008, Tian, Xiufang , <a href="../../membres/michaud.htm">Alexis Michaud</a></p>
        <p>Ce récit est une mise en garde à l'égard des gourmands qui visitent le garde-manger quand ils sont seuls à la maison. Il raconte l'histoire d'une femme qui restait à la maison pour veiller aux tâches domestiques pendant que chacun travaillait aux champs. Elle mangeait des bonnes choses à la dérobée, sans rien en partager avec les autres. Un jour, alors qu'elle mangeait des œufs qu'elle s'était cuits en cachette, des gens de la famille sont rentrés à l'improviste. Paniquée, elle s'est fourré dans la gorge les œufs qui restaient, sans prendre le temps de mâcher. Un jaune d'œuf s'est coincé dans sa gorge, et elle est tombée évanouie, comme morte. Les gens de la famille, la voyant sans vie, ont préparé son enterrement. La famille était riche ; on l'a revêtue de vêtements somptueux et de bijoux d'or et d'argent. En pleine nuit, des voleurs qui convoitaient ces trésors l'ont entièrement déterrée ; la tenant par les genoux, ils ont rejeté son corps en arrière pour lui retirer ses vêtements, ce qui a eu pour effet de lui faire vomir le jaune d'œuf qui l'avait étouffée, et elle est revenue à elle. Les voleurs ont été pris de panique et se sont enfuis. Elle est rentrée chez elle ; les gens de la famille l'ont prise pour une revenante et lui ont fait toutes sortes de difficultés avant de lui permettre de revenir à la maison. Finalement, les gens de la famille ont dénoué l'écheveau de l'histoire, et lui ont pardonné.</p>
         <p class="intertitre"><a href="http://lacito.vjf.cnrs.fr/pangloss/tools/show_text.php?id=crdo-NBF_FEMMECELESTE_SOUND&idref=crdo-NBF_FEMMECELESTE"><img src="../../images/icones/Txt_Inter_parchemin1.jpg" alt="a" width="20" height="20" border="0">Femme céleste</a><br>
      Muli, Liangshan, Sichuan, 2008, Tian, Xiufang,  <a href="../../membres/michaud.htm">Alexis Michaud</a></p> 
      <p>Ce récit raconte les origines de l'humanité : l'histoire de ses premiers ancêtres, et l'origine des plantes cultivées, dont les graines ont été volées dans le monde céleste et ramenées sur terre en cachette. Il explique les dures conditions de vie de l'humanité par une punition infligée par les puissances d'en-haut. 
De grandes similarités existent entre ce récit et ceux des Na et des Naxi. Sans entrer dans le détail, on remarquera que la version naxi de cette histoire met l'accent sur les exploits de l'homme (assisté de sa future épouse) accomplissant toutes les tâches surhumaines imposées par son beau-père, tandis que la version lazé telle qu'elle est racontée ici n'entre pas dans le détail de ces tâches, et s'attache plus aux relations entre la Mère Céleste et sa fille descendue sur terre.
</p> 
