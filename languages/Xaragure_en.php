      <h1 align="center"><a name="up"></a>Xârâgurè Corpus </h1>
      <p class="intertitre">&nbsp;</p>
      <p class="intertitre"><a href="../../ALC/Languages/Xaragure_popup.htm" target="_blank" onClick="window.open(this.href,'popupLink','width=400,height=400,scrollbars=yes,resizable=yes',1);return false"><img src="../../images/icones/info.gif" alt="a" width="15" height="15" border="0">Xârâgurè</a>: a brief note </p>
            <p align="left">&nbsp;</p>
      <h4 align="left">Resources :</h4>
      <p class="intertitre">
      <img src="../../images/icones/h_parleur.gif" alt="a" width="20" height="20"> <a href="../tools/list_rsc_en.php?lg=Xaragure&aff=Xârâgurè" target="_blank"> All available resources here
      </a></p>
      <p class="intertitre">&nbsp;</p>

      <ul class="liste-liens">
        <li><a href="#Bwotemere">Mount Bw&ouml;t&eacute;m&ecirc;r&ecirc;</a></li>
        <li><a href="#Kaataamwaroo">The Story of Kaataamwaroo</a></li>
        <li><a href="#Nefasia">The River N&egrave;fasia Massacre</a></li>
        <li><a href="#Koum">The Three Women from Koum</a></li>
        <li><a href="#Marata">Marat&acirc;'s Answer</a></li>
        <li><a href="#Nyiichaa">Nyiichaa's Prophecy</a></li>
        <li><a href="#wood">A Traditional Speech on Wood</a></li>
        <li><a href="#Trema">Trema Leaf and Cherry Tree Leaf</a></li>
        <li><a href="#Giroo">Young Gir&ouml;&ouml; and His Mother</a></li>
      </ul>
      <p>&nbsp;</p>
      <p class="intertitre"><a name="Bwotemere"></a><a href="../tools/show_text_en.php?id=crdo-AXX_BOTAME_SOUND&idref=crdo-AXX_BOTAME"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0">Mount Bw&ouml;t&eacute;m&ecirc;r&ecirc;</a><br>
        New Caledonia, Thio, 1977, Agnès Tonwiri, <a href="../../membres/moyse.htm">Claire Moyse-Faurie </a></p>
      <p>This myth, like the one on Kaataamwaroo, raises the delicate problem of chiefs&rsquo; daughters&rsquo; marriages. Alliances are supposed to be distant and outside the usual rules: the desired husband must belong to the natural world; in this myth, Bw&ouml;t&eacute;m&ecirc;r&ecirc; is a sea snake. The alliance is successful however, because the eldest sister has Bw&ouml;t&eacute;m&ecirc;r&ecirc;&rsquo;s mother come so as to make her son&rsquo;s exile bearable.</p>
      <p class="intertitre"><a name="Kaataamwaroo"></a><a href="../tools/show_text_en.php?id=crdo-AXX_NETOO_SOUND&idref=crdo-AXX_NETOO"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0">The Story of Kaataamwaroo</a><br>
        New Caledonia, Thio, 1977, Agnès Tonwiri, <a href="../../membres/moyse.htm">Claire Moyse-Faurie </a></p>
      <p>This myth, like that of Bw&ouml;t&eacute;m&ecirc;r&ecirc;, deals with the delicate problem of chiefs&rsquo; daughters&rsquo; marriages. Alliances are supposed to be distant and outside the usual rules: Kaataamwaroo is a &ldquo;pebble&rdquo;. This type of alliance, where the couple lives in the woman&rsquo;s town, can only succeed if concessions are made. In this account, the alliance fails and the husband is restored to his original state.</p>
      <p class="intertitre"><a name="Nefasia"></a><a href="../tools/show_text_en.php?id=crdo-AXX_NEFASIA_SOUND&idref=crdo-AXX_NEFASIA"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0">The River N&egrave;fasia Massacre</a><br>
        New Caledonia, Grand-Borind, Albert Nékaréi, 1980, <a href="../../membres/moyse.htm">Claire Moyse-Faurie </a></p>
      <p>This narrative tells the sad story of an episode in the war between the Europeans and the Kanaks in the Thio-Borindi region. Treachery (obtained by blackmail) is supposed to have caused the near extermination of the members of the former Koum tribe (located in the central mountain range), who are said to have been shot down by the Europeans at the mouth of the N&egrave;fasia river.</p>
      <p class="intertitre"><a name="Koum"></a><a href="../tools/show_text_en.php?id=crdo-AXX_PAPEE_SOUND&idref=crdo-AXX_PAPEE"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0"> The Three Women from Koum</a><br>
        New Caledonia, Grand-Borindi, 1980, Albert Nékaré, <a href="../../membres/moyse.htm">Claire Moyse-Faurie </a></p>
      <p>This short story explains the presence of three unusual rocks, situated half way between the beach and the mountain, between the Ngoi and the Ni rivers, whereas such rocks are usually only encountered in the mountain range, in Koum. They are said to be in fact three women from Koum, who had gone fishing for shells along the sea shore, and who had been overtaken by daylight&hellip; Which goes to show that one mustn&rsquo;t give in to one&rsquo;s desires for too long!</p>
      <p class="intertitre"><a name="Marata"></a><a href="../tools/show_text_en.php?id=crdo-AXX_MARATA_SOUND&idref=crdo-AXX_MARATA"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0">Marat&acirc;'s Answer</a><br>
        New Caledonia, Petit-Borindi, 1980, Valentin Oundo, <a href="../../membres/moyse.htm">Claire Moyse-Faurie </a></p>
      <p>This narrative explains how the Borindi Kanaks were dispossessed of the Ngoi lands, through the fault of old Marat&acirc;. The latter, not being quite all there, is said to have wrongly answered the gendarmes who had come to question him.</p>
      <p class="intertitre"><a name="Nyiichaa"></a><a href="../tools/show_text_en.php?id=crdo-AXX_NYIICHA_SOUND&idref=crdo-AXX_NYIICHA"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0">Nyiichaa's Prophecy</a><br>
        New Caledonia, Petit-Borindi, 1980, Valentin Oundo, <a href="../../membres/moyse.htm">Claire Moyse-Faurie </a></p>
      <p>This is the story of how the Catholic religion came to Borindi, due to old Nyiichaa&rsquo;s clairvoyance. The latter predicted that there would be an era of peace if the Fathers were made welcome.</p>
      <p class="intertitre"><a name="wood"></a><a href="../tools/show_text_en.php?id=crdo-AXX_PURA_SOUND&idref=crdo-AXX_PURA"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0">A Traditional Speech on Wood</a><br>
        New Caledonia, Thio, 1980, Raphaël Oundo, <a href="../../membres/moyse.htm">Claire Moyse-Faurie </a></p>
      <p>Ceremonial discourse. This harangue which precedes the &ldquo;discourse on wood&rdquo; (xw&acirc;xa) is a call for people to gather for the dance.</p>
      <p class="intertitre"><a name="Trema"></a><a href="../tools/show_text_en.php?id=crdo-AXX_NESOO_SOUND&idref=crdo-AXX_NESOO"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0"> Trema Leaf and Cherry Tree Leaf</a><br>
        New Caledonia, Thio, 1980, Agnès Tonwiri, <a href="../../membres/moyse.htm">Claire Moyse-Faurie </a></p>
      <p>This story has several variants, but they all have the particularity of constituting a loop. The version presented here shows, beside the two main characters, Trema leaf (<em>Trema vieillardii</em> Schltr., Ulmaceae) and &ldquo;blue cherry&rdquo; leaf (<em>Elaeocarpus persicifolius</em> Br. and Gr., Elaeocarpaceae), naughty children who are punished for their misdeeds, before the course of events catches up again. In other versions, the two heroines, Trema and Blue cherry, play tricks on one another, equally scatological. The curiosity of an intruder, Acacia leaf, allows the narrator to go back in time, and the story&rsquo;s loop can be closed once more.</p>
      <p class="intertitre"><a name="Giroo"></a><a href="../tools/show_text_en.php?id=crdo-AXX_GIROO_SOUND&idref=crdo-AXX_GIROO"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0"> Young Gir&ouml;&ouml; and His Mother</a><br>
        New Caledonia, Thio, 1980, Agnès Tonwiri, <a href="../../membres/moyse.htm">Claire Moyse-Faurie </a></p>
      <p>In this story, young Gir&ouml;&ouml; saves his mother from being devoured by an ogress. They then get rid of her thanks to the wits and vigilance of the young boy.</p>
