      <p class="intertitre"><a name="taplejung"></a><strong>Textes de Libang (Taplejung)</strong></p>
         <p>Les mat&eacute;riaux pr&eacute;sent&eacute;s ici ont &eacute;t&eacute; enregistr&eacute;s en 1977 dans le village de Libang dans la vall&eacute;e de la rivi&egrave;re Mewa (district de Taplejung). Il s'agit principalement de r&eacute;cits, souvent &agrave; caract&egrave;re autobiographique, de la vie limbu.</p>
        <p>&nbsp;</p>
         <p class="intertitre"><a name="doc_chant"></a><strong>Un document chanté</strong></p>
         <p>Ce document, daté de 1956, exprime les craintes et les aspirations pour son peuple d'un chef traditionnel limbu de cette période. Il est présenté (en anglais) sur une page à part <a href="Limbu_more1_en.htm"> ici</a>.</p>
      <p>&nbsp;</p>
      <p class="intertitre"><a name="remarques"></a><strong>Remarques sur l'annotation des textes</strong> <a href="#up"><img src="../../images/icones/fleche-haut.gif" alt="up" width="10" height="9" border="0"></a></p>
      <p>La traduction en fran&ccedil;ais est disponible pour deux textes, &quot;Untimely death&quot; et &quot;Father-in-law&quot;.</p>
      <p>Une traduction en anglais au niveau de l'&eacute;nonc&eacute; est disponible pour tous les textes. Des gloses de morph&egrave;me en anglais sont disponibles pour les textes suivants: &quot;Untimely death&quot; (gloses en fran&ccedil;ais et anglais), &quot;Ogre Kanayongba&quot;, &quot;Manioc&quot;, &quot;Father-in-law&quot;, &quot;Wife-stealing&quot;, et &quot;Paddy-dancing&quot;.</p>
      <p>La transcription est bas&eacute;e sur l'API, sauf que &quot;y&quot; est employ&eacute; pour la semivoyelle palatale (IPA [j]).</p>
      <p>L'apostrophe indique l'&eacute;lision d'une voyelle (parfois hypoth&eacute;tique).</p>
      <p>Les emprunts du n&eacute;pali ou de l'anglais sont transcrits approximativement selon leur prononciation limbu. La voyelle schwa (&quot;&#x0259;&quot;) n'appara&icirc;t que dans les emprunts.</p>
      <ul>
        <li>Les transcriptions aux niveaux de l'&eacute;nonc&eacute; et du morph&egrave;me diff&egrave;rent sur plusieurs points:</li>
        <li> Dans la transcription au niveau de l'&eacute;nonc&eacute;, la transcription des morph&egrave;mes suit la prononciation, qui peut varier selon l'environnement phon&eacute;tique &agrave; l'int&eacute;rieur du mot.<br>
          Dans la transcription au niveau du morph&egrave;me, chaque morph&egrave;me a toujours la m&ecirc;me forme (sauf exception), sans tenir compte de la variation due au contexte phonologique, pour en faciliter l'identification.<br>
          Les formes finies du verbe sont analys&eacute;es en (prefixe-)th&egrave;me(-suffixe): les pr&eacute;fixes et suffixes d'accord et de polarit&eacute; ne sont pas analys&eacute;s en leurs morph&egrave;mes individuels. Du fait que pr&eacute;fixes et le suffixes sont glos&eacute;s ind&eacute;pendamment, les informations qui peuvent &ecirc;tre d&eacute;duites de leur combinaison ne sont pas refl&eacute;t&eacute;es dans les gloses. Certains mots grammaticaux ne sont pas analys&eacute;s en morph&egrave;mes. Mais en g&eacute;n&eacute;ral, les morph&egrave;mes sont s&eacute;par&eacute;s par des tirets et leurs gloses s&eacute;par&eacute;s par des tirets correspondants dans la traduction au niveau du morph&egrave;me. Le point est employ&eacute; comme s&eacute;parateur pour des gloses de plusieurs mots correspondant &agrave; un seul morph&egrave;me ou &agrave; une seul unit&eacute; pr&eacute;fixale ou suffixale.</li>
      </ul>
      <p>Les gloses de morph&egrave;me sont n&eacute;cessairement incompl&egrave;tes; des informations plus compl&egrave;tes sont disponibles dans le dictionnaire. Il est &agrave; noter que celui-ci comprend des formes grammaticaux qui n'apparaissent pas normalement dans un dictionnaire.</p>
