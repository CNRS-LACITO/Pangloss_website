      <p class="intertitre"><a name="langue"></a><strong>La langue</strong></p>
      <p>Le limbu marque la limite orientale du sous-groupe &quot;kiranti&quot; (ou &quot;East Himalayish&quot;) de la famille tib&eacute;to-birmane. Il pr&eacute;sente l&rsquo;accord verbal complexe, indexant sujet et objet, caract&eacute;ristique de ce groupe. Il ne poss&egrave;de que deux s&eacute;ries d&rsquo;occlusives initiales, non aspir&eacute;e et aspir&eacute;e, le voisement &eacute;tant conditionn&eacute; par le contexte phonologique. Il existe plusieurs dialectes limbu, mais les locuteurs font g&eacute;n&eacute;ralement l'effort n&eacute;cessaire pour communiquer entre dialects sans recourir au n&eacute;pali. Il existe un alphabet limbu; les premiers &eacute;crits connus datent des ann&eacute;es 1840.</p>
     
