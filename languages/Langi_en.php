      <p>&nbsp;</p>
      <p>Langi is a Bantu language (F.33 in Guthrie's classification) spoken in the Kondoa region of central Tanzania by approximately 200,000 people. The texts presented here were recorded on location in 1996, in Kondoa, Pahi and Piriri, in traditional settings.</p>
      <p></p>
      <h4 class="intertitre"><a name="transcription"></a>Transcription</h4>
      <p>The texts are transcribed using the International Phonetic Alphabet, with the exception of the symbol 'y' which represents the palatal glide. The Langi phonological system contains 29 consonants and 7 vowels. Vowel length is distinctive, length is indicated by a double vowel. As the study of the prosody is not complete, tones are not marked.</p>
      <h4 class="intertitre"><a name="characteristics"></a>Main linguistic features</h4>
      <p>Like all Bantu languages, the morphology of Langi is agglutinating. The most striking feature of the language is that, even though the dominant word order is SVO, it presents several phenomena which are characteristic of SOV or VSO languages, e.g. certain periphrastic conjugations such as the future tense where the auxiliary follows the infinitive instead of preceding it, which is what one expects in a language with this word order; demonstratives precede the noun although all other nominal determinants follow the noun. <br>
        These unexpected features are most certainly due to prolonged contact between Langi speakers and speakers of two neighboring Cushitic languages, Alagwa and Burunge. Furthermore, the region where these languages are spoken, the Tanzanian Rift Valley Area, one of the most linguistically diverse areas in Africa, is recognized as constituting a convergence area, where certain linguistic features are shared by languages from different phyla.</p>
