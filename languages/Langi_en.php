      <h1 align="left"><a name="up"></a>Langi Corpus </h1>
      <p class="intertitre">&nbsp;</p>
      <p class="intertitre"><a href="../../ALC/Languages/Langi_popup.htm" target="_blank" onClick="window.open(this.href,'popupLink','width=400,height=400,scrollbars=yes,resizable=yes',1);return false"><img src="../../images/icones/info.gif" alt="infos" width="15" height="15" border="0">Langi</a>: a brief note </p>
      <p align="left">&nbsp;</p>
      <ul class="liste-liens">
        <li>
          <p><a href="#transcription">Transcription</a></p>
        </li>
