      <h1 align="center">Limbu Corpus </h1>
      <p class="intertitre">&nbsp;</p>
      <p class="intertitre"><a href="../../ALC/Languages/Limbu_popup.htm" target="_blank" onClick="window.open(this.href,'popupLink','width=400,height=400,scrollbars=yes,resizable=yes',1);return false"><img src="../../images/icones/info.gif" alt="a" width="15" height="15" border="0">Limbu</a>: a brief note </p>
      <p>&nbsp;</p>
       <h4 align="left">Resources :</h4>
      <p class="intertitre">
      <img src="../../images/icones/h_parleur.gif" alt="a" width="20" height="20"> <a href="../tools/list_rsc_en.php?lg=Limbu&aff=Limbu" target="_blank"> All available resources here
      </a></p>
      <p class="intertitre">&nbsp;</p>
<p>The Limbu inhabit Nepal east of the Arun river and bordering areas of the Indian states of West Bengal (Darjeeling District) and Sikkim. They are called &quot;Limbu&quot; in Nepali, &quot;Yakthung&quot; in Limbu. There are 380,000 speakers of the language in Nepal (self-reported; 2011 Nepal Census), virtually all bilingual in Nepali, the Indo-Aryan national language of Nepal. </p>
      <p>&nbsp;</p>
      <ul class="liste-liens">
        <li><a href="#language">The language</a></li>
        <li><a href="#dico">Online dictionary</a></li>
        <li><a href="#references">References</a></li>
        <li><a href="#textsLibang">Texts from Libang (Taplejung)</a></li>
        <li><a href="#textsAppeal">An historic song</a></li>
        <li><a href="#annotation">About the annotation</a></li>
      </ul>
      <p class="intertitre"><a name="language"></a><strong>The language</strong></p>
      <p>The Limbu language belongs to the Tibeto-Burman family. It was classified by Shafer (1955) in the East Himalayish section of the Bodic Division of Sino-Tibetan. Limbu is the easternmost member of this group, also known as &quot;Kiranti&quot;. There are several Limbu dialects, but speakers consider them to be intercomprehensible, with a bit of effort.</p>
      <p class="intertitre"><a name="dico"></a><strong>Online dictionary</strong></p>      
<p>A hypertext dictionary of the Mewa Khola dialect (a variety of the Tamar Khola dialect) (Michailovsky 2002) is available <a href="../../pangloss/dico" target="_blank">here</a>. The introduction to the printed version <a href="http://lacito.vjf.cnrs.fr/documents/publi/L22Ib.pdf" target="_blank">(pdf)</a> contains information on Limbu phonology and verbal morphology. Many of the example sentences in the dictionary are taken from the texts archived on this site. <!--)Clicking on these examples gives acces to the original recordings.--></p>
<p class="intertitre"><a name="references"></a><strong>References</strong></p>
<p>The following works on Limbu may be found useful:</p>
      <ul>
        <li>Michailovsky, Boyd, 2002, <i>Limbu-English Dictionary of the Mewa Khola Dialect with English-Limbu index</i>, Kathmandu, Mandala Book Point.</li>
        <li>Royal Nepal Academy, 2002, <i>Limbu-Nepali-English Dictionary</i> edited by Bairagi Kainla [Til Bikram Nembang]. (This is a much more comprehensive, multi-dialectal dictionary based mainly, but not exclusively, on the Panchthar dialect, and including literary Limbu. Entries are in the Srijonga, Devanagari, and roman scripts. Nepali-Limbu index.) </li>
        <li>Van Driem, George, 1987. <i>A grammar of Limbu</i>. Berlin. Mouton de Gruyter. (Description of the Phedappe dialect; vocabulary).</li>
        <li>Weidert, A. and B. Subba [D. Bikram Ingwaba], 1985, <i>Concise Limbu Grammar and Dictionary</i>. Amsterdam. Lobster. (Panchthar dialect.) </li>
      </ul>
      <p>&nbsp;</p>
      <p class="intertitre"><a name="textsLibang"></a><strong>The archived texts: Libang</strong></p>
      <p>Most of the archived documents were originally recorded and transcribed in 1977 by B. Michailovsky and Martine Mazaudon in the village of Libang, in the valley of the Mewa Khola in Taplejung district. They are mainly autobiographical narratives.
