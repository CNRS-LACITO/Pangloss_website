      <p>All texts have free, sentence-level English translations. The texts &quot;Untimely death&quot;, &quot;Ogre Kanayongba&quot;, &quot;Manioc&quot;, &quot;Father-in-law&quot;, &quot;Wife-stealing&quot;, and &quot;Paddy-dancing&quot; also have English morpheme glosses. French translation and glosses are provided for &quot;Untimely Death&quot;. Sentence-level French translation is provided for &quot;Father-in-Law&quot;.</p>
      <p>&nbsp;</p>
       <p class="intertitre"><a name="textsAppeal"></a><strong>An historic song</strong>
<p>&quot;An Appeal&quot; (1956), expressing the concerns and the aspirations for his people of an important Limbu headman, is presented on a separate page <a href="Limbu_more1_en.htm">here</a>.
      <p>&nbsp;</p>
      <p class="intertitre"><strong>About the transcription</strong></p>
      <p>The transcription is IPA-based, except that "y" is used for the palatal glide (IPA [j]). </p>
      <p>Loanwords from Nepali (or from English, etc. via Nepali) are transcribed roughly as they are pronounced in the recordings. The phoneme "ə" appears only in Nepali loanwords</p>
      <p>Two different Limbu transcriptions are provided:</p>
      <ul>
        <li>The sentence-level transcription is close to the pronunciation and is punctuated for easier understanding. In this transcription, the form of individual morphemes may vary according to the context within the word, in accord with the pronunciation. The transcription does not take into account phonological context across word boundaries (indicated by space). This is why no native word is transcribed with a voiced stop initial (b, d, g, dz) although word-initial stops (p, t, k, ts) may be voiced in context. </li>
        <li>In the morpheme-level transcription, most Limbu morphemes always appear in the same form, ignoring regular, phonologicially conditioned variation. This transcription facilitates word-searching. Remarks: finite verb forms are are analysed as prefix-stem-suffix; the agreement and negative-marking prefixes and suffixes are not analysed into individual morphemes. Frequent grammatical words are not always analysed into their component morphemes; etymologies can be found in the dictionary. </li>
      </ul>
      <p>Apostrophe in the transcription indicates an elided vowel. In some cases these are hypothesized on the theory that Limbu has the syllable structure (C)V(C) with no initial consonant clusters. The apostrophe does not indicate a syllable boundary.</p>
      <p>Morpheme-level translations (where available) are displayed as interlinear glosses, visually aligned word-by-word with the morpheme-level transcription. In the morpheme-level transcription, morphemes are separated by hyphens, and their glosses are separated by corresponding hyphens in the morpheme-level translation. Periods are used as separators in multi-word glosses which correspond to single morphemes or to verb prefixes or suffixes transcribed as single units (see above). </p>
      <p>In the morpheme glosses, where a verb prefix or suffix indexes both actor and undergoer (subject and object), the actor appears before the arrow and the undergoer after it. The notations "S1" and "S2" in verb stem glosses indicate present and past stems, respectively. Since prefixes and suffixes are glossed idependently, and only audible morphemes are transcribed and glossed, information that can be inferred from the combination of prefix and suffix, or from the absence of a prefix or suffix, is not necessarily reflected in the gloss. </p>
      <p>The text documents (XML-coded documents available through the metadata interface) contain some information that is not displayed by the current interface, including form-class information (identification of verb stems and of prefix and suffix strings) and standardized transliterations of Nepali loan words.</p>
      <p>&nbsp;</p>
      
