      <h1 align="center"><a name="up"></a>Corpus x&acirc;r&acirc;c&ugrave;&ugrave;</h1>
      <p class="intertitre">&nbsp;</p>
      <p class="intertitre">Fiche technique sur le <a href="../../ALC/Languages/Xaracuu_popup.htm" target="_blank" onClick="window.open(this.href,'popupLink','width=400,height=400,scrollbars=yes,resizable=yes',1);return false">x&acirc;r&acirc;c&ugrave;&ugrave;<img src="../../images/icones/info.gif" alt="a" width="15" height="15" border="0"></a></p>
      <p align="left">&nbsp;</p>
      <h4 align="left">Ressources :</h4>
     <p align="left" class="intertitre">
      <img src="../../images/icones/h_parleur.gif" alt="a" width="20" height="20"> <a href="../tools/list_rsc.php?lg=Xaracuu&aff=xârâcùù" target="_blank">Accès à toutes les ressources</a>
      </p>
       <p align="left" class="intertitre">&nbsp;</p>
