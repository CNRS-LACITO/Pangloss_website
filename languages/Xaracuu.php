      <p>&nbsp;</p>
      <p class="intertitre"><a name="prosodie1"></a><img src="../../images/icones/h_parleur.gif" alt="a" width="20" height="20"><a href="../tools/show.php?id=crdo-ANE_JCR_001_B_SOUND"> Prosodie xârâcùù (1)</a><br>
        Nouvelle-Calédonie, Thio-Canala, 1977, Guy Tamai, <a href="../../membres/rivierre.htm">Jean-Claude Rivierre</a></p>
      <p class="intertitre">Etude prosodique, d'un locuteur de 22 ans, originaire de la région de Thio. Enregistrements de mots, phrases et syntagmes.</p>
      <p class="intertitre"><a name="prosodie2"></a><img src="../../images/icones/h_parleur.gif" alt="a" width="20" height="20"><a href="../tools/show.php?id=crdo-ANE_JCR_002_B_SOUND"> Prosodie xârâcùù (2)</a><br>
        Nouvelle-Calédonie, Thio-Canala, 1977, Guy Tamai, <a href="../../membres/rivierre.htm">Jean-Claude Rivierre</a></p>
      <p class="intertitre">Etude prosodique, d'un locuteur de 22 ans, originaire de la région de Thio. Enregistrements de mots, phrases et syntagmes.</p>
      <p class="intertitre"><a name="Yoora"></a><a href="../tools/show_text.php?id=crdo-ANE_YOORA_SOUND&idref=crdo-ANE_YOORA"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0"> Yoora</a><br>
Nouvelle-Cal&eacute;donie, Thio, 1980, Georges Chaanyiroa, <a href="../../membres/moyse.htm">Claire Moyse-Faurie </a></p>
      <p> Ce mythe relate un &eacute;pisode de la rivalit&eacute; entre deux chefs, symbolis&eacute;s par des montagnes : Yoora, situ&eacute; au fond de la vall&eacute;e de Ouindo, dont on dit qu'on ne peut le gravir, son sommet s'&eacute;loignant au fur et &agrave; mesure de la mont&eacute;e, et Fir&egrave;&egrave;g&ouml;&ouml;, qui surplombe la tribu d'Ourou&eacute;. Humili&eacute; un jour par Fir&egrave;&egrave;g&ouml;&ouml;, qui l'avait invit&eacute; chez lui et l'avait bless&eacute; en le br&ucirc;lant, Yoora d&eacute;cide de se venger en invitant &agrave; son tour Fir&egrave;&egrave;g&ouml;&ouml;. Ce dernier, apr&egrave;s avoir perdu un pari, est d&eacute;finitivement rel&eacute;gu&eacute; dans la r&eacute;gion de Thio-Borindi, o&ugrave; il introduit la langue x&acirc;r&acirc;gur&egrave;.</p>
      <p class="intertitre"><a name="Fireegoo"></a><a href="../tools/show_text.php?id=crdo-ANE_FIREGO_SOUND&idref=crdo-ANE_FIREGO"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0"> Fir&egrave;&egrave;g&ouml;&ouml;</a><br>
        Nouvelle-Cal&eacute;donie, Thio, 1980, Georges Chaanyiroa, <a href="../../membres/moyse.htm">Claire Moyse-Faurie </a></p>
      <p> Histoire de rivalit&eacute;s entre des chefs, symbolis&eacute;s par des montagnes. Le mont Fir&egrave;&egrave;g&ouml;&ouml; qui surplombe la tribu d'Ourou&eacute;, au nord de Thio, avait humili&eacute; plusieurs chefs qu'il avait invit&eacute;s chez lui : ainsi, le mont M&egrave;r&egrave;n&egrave; avait d&ucirc; rest&eacute; courb&eacute; apr&egrave;s s'&ecirc;tre pench&eacute; pour ramasser un panier de coquillages et le mont Yoora, situ&eacute; au fond de la tribu de Ouindo, avait &eacute;t&eacute; br&ucirc;l&eacute; par Fir&egrave;&egrave;g&ouml;&ouml;. Ce r&eacute;cit concerne plus pr&eacute;cis&eacute;ment Fir&egrave;&egrave;g&ouml;&ouml;, chass&eacute; par ses cousins de la r&eacute;gion de Kouaoua et forc&eacute; de se r&eacute;fugier vers Borindi, avant de se fixer au-dessus d'Ourou&eacute;. Au cours de sa fuite, il aurait modifi&eacute; le paysage, cr&eacute;ant des &icirc;lots, les terres fertiles de Koum, d&eacute;pla&ccedil;ant certaines esp&egrave;ces d'herbes et transformant les fl&egrave;ches de ses poursuivants en pins colonnaires, symboles de l'habitat kanak.</p>
      <p class="intertitre"><a name="Pweedi"></a><a href="../tools/show_text.php?id=crdo-ANE_PWEE_SOUND&idref=crdo-ANE_PWEE"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0"> Histoire de Pw&ecirc;&ecirc;di M&uuml;&uuml;</a><br>
        Nouvelle-Cal&eacute;donie, K&egrave;r&egrave;ji, 1982, Apollinaire Satoayè Moindou, <a href="../../membres/moyse.htm">Claire Moyse-Faurie </a></p>
      <p>Cette l&eacute;gende &eacute;voque la difficult&eacute; que rencontre un cadet pour se faire une place &agrave; c&ocirc;t&eacute; de ses a&icirc;n&eacute;s. Pw&ecirc;&ecirc;di M&uuml;&uuml;, le cadet du clan M&uuml;&uuml;, d&eacute;crit comme plus malin que ses fr&egrave;res, est ainsi jalous&eacute; et doit fuir Ouass&eacute;, sa terre d'origine, pour &eacute;chapper &agrave; la mort. Se cherchant alors un territoire en parcourant la cha&icirc;ne centrale, il est rejet&eacute; &agrave; plusieurs reprises par les propri&eacute;taires des diff&eacute;rents lieux qu'il traverse. Il trouve enfin un endroit o&ugrave; s'installer sur la c&ocirc;te ouest, pr&egrave;s de La Foa. Il revient &agrave; Ouass&eacute; voir sa m&egrave;re, cr&eacute;e pour elle quelques &icirc;lots fertiles le long de la c&ocirc;te est, entre Canala et Ngoye, mais emporte avec lui tous les crabes et les poissons, &quot;comme si c'&eacute;taient les poux de sa t&ecirc;te&quot;, ne laissant comme produit de p&ecirc;che pour ses fr&egrave;res a&icirc;n&eacute;s qu'un lochon de pal&eacute;tuviers. Ce serait la raison pour laquelle la mer est dite plus poissonneuse &agrave; La Foa qu'&agrave; Canala.</p>
      <p class="intertitre"><a name="Ant"></a><a href="../tools/show_text.php?id=crdo-ANE_FOURMI_SOUND&idref=crdo-ANE_FOURMI"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0"> Fourmi et Roussette</a><br>
        Nouvelle-Cal&eacute;donie, K&egrave;r&egrave;ji, 1982, Apollinaire Satoayè Moindou, <a href="../../membres/moyse.htm">Claire Moyse-Faurie </a></p>
      <p>Conte mettant en sc&egrave;ne une fourmi et une roussette. La fourmi, ayant manqu&eacute; de respect envers la roussette, est d&eacute;vor&eacute;e par cette derni&egrave;re. Mais l'union fait la force, ses cong&eacute;n&egrave;res prennent la rel&egrave;ve et continuent &agrave; tourmenter les roussettes !</p>
      <p class="intertitre"><a name="bird"></a><a href="../tools/show_text.php?id=crdo-ANE_OISEAU_SOUND&idref=crdo-ANE_OISEAU"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0"> Histoire d'oiseaux</a><br>
Nouvelle-Cal&eacute;donie, N&eacute;x&ouml;, 1984, Simon Kédé, <a href="../../membres/moyse.htm">Claire Moyse-Faurie </a></p>
      <p>Conte assorti d'une morale : l'oiseau-lunettes et le coucou-&eacute;ventail sont alli&eacute;s; l'oiseau-lunettes, devant se rendre &agrave; Ouv&eacute;a, confie au coucou la garde de ses sujets. Parmi ces derniers, un seul, la poule sultane, transgresse l'interdit et doit en subir les cons&eacute;quences.</p>
      <p class="intertitre"><a name="Octopus"></a><a href="../tools/show_text.php?id=crdo-ANE_POULPE_SOUND&idref=crdo-ANE_POULPE"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0">Le poulpe</a><br>
        Nouvelle-Cal&eacute;donie, N&eacute;x&ouml;, 1984, Simon Kédé, <a href="../../membres/moyse.htm">Claire Moyse-Faurie </a></p>
      <p> All&eacute;gorie sur le comportement de certains chefs, qui n&eacute;gligent leurs sujets pour ne s'occuper que de leurs propres int&eacute;r&ecirc;ts, et qui sont alors destitu&eacute;s.</p>
