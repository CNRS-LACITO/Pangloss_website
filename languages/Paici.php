        &ndash; Issu d'un arbre qui se dresse dans la grande all&eacute;e, &agrave; proximit&eacute; du tertre de l'anc&ecirc;tre, il peut faire figure d'autochtone, de fondateur, aussi enracin&eacute; dans son terroir que les arbres qui y poussent. <br>
