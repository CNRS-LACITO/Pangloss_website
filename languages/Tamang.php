      <h1 align="center">Corpus tamang</h1>
      <p class="intertitre">&nbsp;</p>
      <p class="intertitre">Fiche technique sur le <a href="../../ALC/Languages/Tamang_popup.htm" target="_blank" onClick="window.open(this.href,'popupLink','width=400,height=400,scrollbars=yes,resizable=yes',1);return false">tamang<img src="../../images/icones/info.gif" alt="a" width="15" height="15" border="0"></a></p>
       <p align="left">&nbsp;</p>
      <h4 align="left">Ressources :</h4>
     <p align="left" class="intertitre">
      <img src="../../images/icones/h_parleur.gif" alt="a" width="20" height="20"> <a href="../tools/list_rsc.php?lg=Tamang&aff=tamang" target="_blank">Accès à toutes les ressources</a>
      </p>
       <p align="left" class="intertitre">&nbsp;</p>
      <p>Le tamang, parl&eacute; par un million de locuteurs au Centre du N&eacute;pal, &eacute;tait connu au si&egrave;cle dernier sous le nom de murmi, et plac&eacute; par Konow (<i>Linguistic Survey of India</i>) dans le groupe "non-pronominalis&eacute;" des langues himalayennes. Proche parent du tib&eacute;tain, le tamang appartient &agrave; la branche "gurung" de la section "bodaise" (<i>Bodish</i>) de la division "bodique" (<i>Bodic</i>) du sino-tib&eacute;tain dans la classification de Shafer, une branche actuellement renomm&eacute;e "branche tamang" du fait que les locuteurs des autres parlers de cette branche utilisent majoritairement pour se d&eacute;signer eux-m&ecirc;mes, comme les Tamang proprement dits, un nom d&eacute;rivable d'une forme ancienne *tamang. Les autres langues principales de cette branche sont le gurung, le thakali, et les parlers de Manang, langues avec lesquelles, quoique proches, il n'y a pas intercompr&eacute;hension</p>
      <p> Le tamang lui-m&ecirc;me peut se diviser en deux groupes dialectaux, occidental et oriental. Les dialectes orientaux sont phonologiquement plus conservateurs, ayant conserv&eacute; un canon syllabique avec consonnes finales et groupes de consonnes initiales, un sch&eacute;ma qui se simplifie progressivement d'Est en Ouest, pour aboutir en gurung &agrave; un canon CV. Le dialecte ici pr&eacute;sent&eacute; est celui du bourg de Risiangku, un parler oriental, phonologiquement conservateur.</p>
      <p>Les Tamang sont principalement paysans. Ils suivent majoritairement la religion bouddhiste, particuli&egrave;rement &agrave; Risiangku, avec une part importante de pratiques chamanistes, surtout dans le domaine m&eacute;dical.</p>
