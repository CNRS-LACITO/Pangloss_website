            <h3><a name="phonemes"></a>3.
Vowels and
consonants: Illustrations
of some of the phonemes</h3>
            <p style="text-align: justify;">With a view to
avoiding large discrepancies between
the
transcription in International Phonetic Alphabet and the actual
pronunciation, <b>the
transcription adopted is not strictly
phonemic</b>. In particular: </p>
            <ul>
              <li>the palatal
nasal [ɲ] can be analyzed as an
allophone
of /ŋ/</li>
              <br>
              <li>onset-less
syllables receive an empty-onset-filler:
/i/ is
realized as [ʝi], /o/ as [wo], /ɯ/ as [ɣɯ], etc. (This is similar to
Naxi.) These syllables are here transcribed with their phonetic onset,
as [ʝi], [ɣɯ], etc.</li>
            </ul>
            <p style="text-align: justify;">On the other hand,
the apical vowels [ʅ] and [ɿ] are
phonemicized as /ɯ/. For details on the phonemic analysis, the reader
is referred, again, to the <a
 href="https://www.dropbox.com/s/v9uigdos3l9vbyq/ToneInYongningNa_Draft.pdf?dl=0"
 target="_blank">book draft available online</a><a
 href="http://halshs.archives-ouvertes.fr/halshs-00358610/"
 target="_blank"></a>.</p>
            <p style="text-align: justify;">The documents
presented in this section contain examples
illustrating the phonemic contrasts that proved most challenging. The
first is one between two different rhymes after alveolo-palatal
affricates: /dʑi/ vs. /dʑɯ/; /tɕi/ vs. /tɕɯ/; /tɕʰi/ vs. /tɕʰɯ/. The
vowel is more strongly apicalized for the latter than for the former.</p>
