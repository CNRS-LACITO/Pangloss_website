            <h2><a name="narratives"></a>1.
Narratives</h2>
            <h3><a name="narratives_presentation"></a>Transcribed
and translated narratives</h3>
            <p style="text-align: justify;">About
twenty transcribed narratives are available. About half
of
them have word-level, sentence-level and text-level translations into
Chinese and French; to date, only one of these (<a
 href="http://lacito.vjf.cnrs.fr/pangloss/tools/show_text_en.php?id=crdo-NRU_MARIAGE_DE_LA_SOEUR_V1_SOUND&amp;idref=crdo-NRU_Mariage_De_La_Soeur_V1"
 target="_blank">"The
sister's wedding", version 1</a>)
has English glosses. The others have word-level,
sentence-level and text-level translations into French; some also have
Chinese translations.&nbsp;</p>
            <p style="text-align: justify;">If
you have suggestions for improvements, or if you are
willing to contribute a translation into another language (either
English, or another language), you are most welcome to get in touch
            <script>writemail ('vjf.cnrs.fr','alexis.michaud','write to me',0)</script>.
A number of narratives have not yet been transcribed; they are
nonetheless made available for people who have a command of the
language, from native speakers to motivated students.</p>
            <p style="text-align: justify;">All
of these narratives were told by Ms. Latami Dashilame (language
consultant F4). Some
explanations about the process of collecting these texts are provided <a
 href="#collection_process">further down, below the
list of
narratives</a>.&nbsp;</p>
            <p style="text-align: justify;">In
the narratives, the surface-phonological tones are
indicated in the transcription at the sentence level, and lexical
(underlying) tones in the word-by-word glosses. This makes it possible
to study the numerous tonal processes at play in Na.</p>
