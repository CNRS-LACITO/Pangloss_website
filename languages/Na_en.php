            <p>&nbsp;</p>
            <p style="text-align: justify;"><b>Na
(endonym: /nɑ˩-ʐwɤ˧/, i.e. "Na language")</b>
is
spoken
in an area straddling the boundary between the Chinese provinces of
Yunnan and Sichuan, in the vicinity of lake Lugu (lo˧ʂv̩˩-hi˩nɑ˧mi˧): <a
 href="#map">see map</a>.
The total number of speakers was estimated at about 40,000 on the basis
of early surveys (He Jiren &amp; Jiang Zhuyi 1985:107); the same
figure
is taken up by Yang Zhenhong (2009). </p>
            <p style="text-align: justify;"> It is a member of
the Naish group of languages, a
lower-level
grouping within Sino-Tibetan that also includes Naxi and Laze: see <a
 href="#tree" target="_blank">the tentative
family
tree at the bottom of this page</a>,
reproduced from <a
 href="http://halshs.archives-ouvertes.fr/halshs-00537990/en/"
 target="_blank">a historical study of Na, Naxi and
Laze</a>.
In Chinese, the Na language
is
commonly referred to as 'Mos(u)o' (摩梭). </p>
            <p style="text-align: justify;"> It has recently
been granted an entry of its own, under
the
romanized name 'Narua', in the inventory of languages maintained by the
Summer Institute of Linguistics (code: <a
 href="http://www.sil.org/iso639-3/cr_files/2010-023_nru.pdf"
 target="_blank">NRU</a>),
whereas it used to be
classified as a dialect of Naxi. The former language code NBF is now
split into Naxi proper (new code: NXQ), on the one hand, and Narua
(code: NRU), on the other. </p>
            <p style="text-align: justify;">Most of the
documents presented here are from the plain
of Yongning
永宁
(Na name: /ɬi˧di˩-di˩mi˩/). Some recordings from the village of Labai
拉伯 are also presented.</p>
