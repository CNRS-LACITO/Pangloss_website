      <h1 align="center">Data on two sentence-final particles</h1>

      <p class="intertitre">&nbsp;</p>
      A detailed description of this data set is <a href="http://lacito.vjf.cnrs.fr/pangloss/docs_pdf/VIE_SFP_dialogues_2013.pdf" target="_blank">available in PDF format</a>. The
reader is referred to the PDF document&nbsp;for full details and
explanations. This page proposes a bird's-eye view with links to the
resources.
            <h4 align="left">Introduction:</h4>

            <p>Sentence-final particles are expressive words
indicating speech act types, evidential/epistemic nuances, and
affective/emotional coloring - functions that tend to be fulfilled by
intonation in a language such as English. The relationship between
intonation and SFPs is not simply one of functional equivalence,
however, since SFPs also carry intonational information: sentence-level
intonational phenomena are known to cluster on SFPs. One and the same
SFP can take on different nuances depending on intonation. <br>

The materials presented here were collected for a pilot study
investigating intonational variation in the realization of the tones of
SFPs (<a href="http://halshs.archives-ouvertes.fr/halshs-00824076/en/">click
here to access the full paper, presented at Interspeech 2013</a>).
One of the features of these materials is that they allow for an
investigation of phonation type, extending the scope of analysis beyond
F0. The research issue is how speaker attitude affects the realization
of glottalization for tones 3 and 6.</p>

            <h4 align="left">Description of data:</h4>

            <p> Due to the great amount of carry-over tonal
coarticulation in Vietnamese, the tones of SFPs are strongly influenced
by those of preceding syllables. We therefore devised sentence (1)
using only syllables carrying tone 1 (ngang, A1: a level, non-low
tone), which is phonetically the simplest. <br>

            <br>

(1) Lam l&ecirc;n c&ocirc;ng ty (Lam goes to work / or I'm
going to work) <br>

            <br>

This sentence was then associated with two SFPs that carry glottalized
tones: ạ /a6/, conveying politeness, and đ&atilde; /ɗa/, conveying
tense-aspect-modality information. This yields (2) Lam l&ecirc;n
c&ocirc;ng ty ạ and (3) Lam l&ecirc;n c&ocirc;ng ty
đ&atilde;. Finally, sentences (1-3) were placed inside dialogues,
which were precisely contextualized. The attitudes under study are (i)
POLITENESS, associated lexically to the SFP ạ, and (ii) DECLARATION,
IRRITATION, and SURPRISE, elicited by context. The basic context is as
follows: Lam, Minh and An are three friends who have just moved into a
shared flat; today is Saturday, a day when they neither go to class nor
to their workplace; but Lam is suddenly requested to go to work for
urgent business. </p>

          

