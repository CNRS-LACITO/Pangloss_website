  <h4 class="intertitre"><a name="presentation"></a>Presentation </h4>
  <p>Mayotte Island (Maore), located at the southern extremity of the Comoros archipelago, approximately 300km from Madagascar and 70 km from Anjouan, includes the Grande Terre, separated from the Petite Terre and the Dzaoudzi rock by four kilometers of sea. <br>
    The global surface of Mayotte is 374 km&sup2; and its population is estimated at 150,000 inhabitants. <br>
    During the first period of colonization (1843-1975) the presence of the French was mostly only felt on Dzaoudzi rock, which was the capital of the Comoros until 1966. <br>
    Whereas the other three islands, Grande-Comore (Ngazidja), Anjouan (Ndzuani) and Moheli (Mwali) &ndash; gained independence in 1975, Mayotte still remains a French territory. The reasons behind these different choices were political. From the two points of view, both cultural and linguistic, Mayotte is part of the Comoro group. <br>
    Furthermore, Mayotte houses two distinct linguistic communities: the first, the largest, speaks Maore, a language belonging to the Bantu linguistic group, the second is a dialect of Malagasy spoken in roughly twenty villages scattered over the island. The Malagasy speakers also know Maore, but the reverse is not true. Two languages but a single culture, characterized among other traits by Islam.<br>
    The Maore language of Mayotte is a Bantu language just as is Ngazidja spoken in Grande Comore, and must not be confused with the other, minority, language spoken there, which is a Malagasy dialect and as such belongs to the Austronesian languages. (These two languages belong to two distinct language families, but the inhabitants of Mayotte, whether they speak one language or the other, share the same Islam dominated culture). </p>
        Maore presents several major differences as compared to Ngazidja and Mwali (the languages spoken on the two more northern islands of the Comoro archipelago) and is much more clearly related to the language spoken on the neighboring island of Anjouan (the Nzuani language).
