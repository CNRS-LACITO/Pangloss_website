          Comoros, Mayotte, Mtsapere (Grande Terre), 1978, Anonymous, <a href="../../membres/rombi.htm">Marie-Fran&ccedil;oise Rombi</a></p>
        <p>This story was collected in Mayotte in 1978. It was recorded in urban settings, in Mutsapere, on the outskirts of Mamudzu (actually Momoju), which, in 1978, was the main town on the Grande Terre. <br>
          The story teller is a man of about 50 years of age, non francophone, never having left the Comoros. <br>
          This story was chosen for two reasons: the story line and the part that is sung. <br>
          This theme, that of a tree or branch being transformed into a woman, is very popular in the Comoros.</p>
        The language used by the story teller is everyday Maore, perfectly understandable for the younger generation in 1978.<br>
        The part that is sung shows that the study of oral literature can furnish very important elements and make it possible to sketch, though of course with caution, hypotheses on population movements in this region.<br>
        Indeed, this sung part was presented by the story teller as belonging to the language of the ancestors of Africa. It later became clear that it is in fact the Makua language of Mozambique.
