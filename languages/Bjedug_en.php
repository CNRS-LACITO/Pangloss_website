            <h1 align="left">Adyghe (Circassian): Bjedug
Dialect</h1>

            <p class="intertitre">&nbsp;</p>

            <p class="intertitre"><a href="../../ALC/Languages/Bjedug_popup.htm" target="_blank" onClick="window.open(this.href,'popupLink','width=400,height=400,scrollbars=yes,resizable=yes',1);return false"><img src="../../images/icones/info.gif" alt="a" border="0" height="15" width="15">Bjedug</a>: a brief note</p>

            <p class="intertitre">&nbsp; </p>

            <h4 align="left">Resources :</h4>

            <p class="intertitre"> <img src="../../images/icones/h_parleur.gif" alt="a" height="20" width="20"> <a href="../tools/list_rsc_en.php?lg=Bjedug&amp;aff=bjedoug" target="_blank"> All available resources here </a></p>

            <p class="intertitre">&nbsp;</p>

            <h4 align="left">Nart Sagas</h4>

            <p>The Narts are legendary heros of the Caucasus. The
three stories presented were recorded in 1973 by the eminent Adyghe
scholar Asker Hadaghatl'a (in Russian, Gadagatl'). They had previously
been collected and published in volume 7 of his vast anthology of Nart
sagas (as they have come to be called in English) in a variety of
Adyghe languages:<br>

Gadagatl', Asker. 1968-1971. <i>Nartxer</i> [<i>The
Narts</i>]. Maikop. 7 vols.</p>

            <p>The sagas have the following titles:&nbsp;: </p>

            <ul class="liste-liens">

              <li> <a href="../tools/show_text_en.php?id=crdo-ADY_BAOUTCH_SOUND"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" border="0" height="20" width="20"> The Hill of
Nart Baoutch</a> (Gadagatl' 1971 v. 7 no. 596, p. 65)</li>

              <li> <a href="../tools/show_text_en.php?id=crdo-ADY_GOUTCHEPL_SOUND"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" border="0" height="20" width="20"> The
Marriage of Nart Ghoutch'epl</a> (Gadagatl' 1971 v. 7 no. 641, p.
158)</li>

              <li> <a href="../tools/show_text_en.php?id=crdo-ADY_FEU_SOUND"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" border="0" height="20" width="20"> The Nart
who Brought Fire</a> (Gadagatl' 1971 v. 7 no. 693, p. 268)</li>

            </ul>

            <p>The transcriptions, in the Cyrillic alphabet, are
taken from the work cited. They were computerized (with the author's
permission), synchronized with the recordings, and translated into
French by Mme Dina Dabjen-Bailly in 2003-2004.</p>

            <p>&nbsp;</p>

            <h4 class="intertitre"> Word Lists</h4>

            <p>Two word lists are presented, illustrating
different varieties of Bjedug.</p>

            <ul class="liste-liens">

              <li> <a href="../tools/show_text_en.php?id=crdo-ADY_LISTE_KAT_SOUND"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" border="0" height="20" width="20"> The first
word list</a> was prepared by Catherine Paris and recorded in
Tblisi in 1974, as spoken by Mr Teotchej Kat, a speaker from the
Caucasus. It shows the opposition of aspiration, which is considered to
be characteristic of the dialect. The elicitation by the linguist (in
Russian) is included in the recording, but it has not been transcribed,
and it has been excluded from the time alignment. To hear it, the
recording must be played in its entirety, not segment by segment.</li>

              <li> <a href="../tools/show_text_en.php?id=crdo-ADY_LISTE_DINA_SOUND"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" border="0" height="20" width="20"> The second</a>
was prepared and spoken by Mme Dina Dabjen-Bailly, a speaker from the
Circassian diaspora in Syria. It was recorded, together with <a href="http://lacito.vjf.cnrs.fr/pangloss/audio/crdo-ADY_LISTE_DINA_EGG.wav" target="_blank"><img class="sansBordure" src="../../images/icones/egg2.jpg" height="25" width="25">electroglottographic data</a>, at the LIMSI research centre (by Mr. Alexis Michaud, Mr. Vu-Ngoc Tu&acirc;n, and Mr. Michailovsky) in 2003, and transcribed by
Mme Dabjen-Bailly. The opposition of aspiration is absent.</li>

            </ul>

            <p>The Tblisi recording script was accompanied by a
phonological <a href="kat_phono.htm" target="_blank" onClick="window.open(this.href,'popupLink','width=600,height=400',1);return false">chart</a>
presenting the principles and the particularities of the transcription.
Mme Dabjen-Bailly uses essentially the same transcription, without
gemination.</p>

