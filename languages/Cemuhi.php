      <h1 align="center"><a name="up"></a>Corpus c&egrave;muh&icirc;</h1>
      <p class="intertitre">&nbsp;</p>
      <p class="intertitre">Fiche technique sur le <a href="../../ALC/Languages/Cemuhi_popup.htm" target="_blank" onClick="window.open(this.href,'popupLink','width=400,height=400,scrollbars=yes,resizable=yes',1);return false">c&egrave;muh&icirc;<img src="../../images/icones/info.gif" alt="a" width="15" height="15" border="0"></a></p>
      <p class="intertitre">&nbsp;</p>
      <h4 align="left">Ressources :</h4>
     <p align="left" class="intertitre">
      <img src="../../images/icones/h_parleur.gif" alt="a" width="20" height="20"> <a href="../tools/list_rsc.php?lg=Cemuhî" target="_blank">Accès à toutes les ressources</a>
      </p>
       <p align="left" class="intertitre">&nbsp;</p>
      <ul class="liste-liens">
        <li><a href="#laiteron">Fleurs de laiteron de Pw&ouml;paat</a></li>
        <li><a href="#crabe">Le crabe et la fillette de Pwa&eacute;&eacute;to</a></li>
        <li><a href="#tortue">Tortue et les deux p&ecirc;cheuses</a></li>
        <li><a href="#ecailles">Les &eacute;cailles de poisson de Tiw&eacute;caal&egrave;</a></li>
        <li><a href="#large">Dui Dupaan et la fille du grand large</a></li>
