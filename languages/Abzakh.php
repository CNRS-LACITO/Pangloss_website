      <h1 align="left">Tcherkesse (adyghe) : dialecte abzakh</h1>
      <p class="intertitre">&nbsp;</p>
      <p class="intertitre">Fiche technique sur l'<a href="../../ALC/Languages/Abzakh_popup.htm" target="_blank" onClick="window.open(this.href,'popupLink','width=400,height=400,scrollbars=yes,resizable=yes',1);return false">abzakh<img src="../../images/icones/info.gif" alt="a" width="15" height="15" border="0"></a></p>
      <p align="left">&nbsp;</p>
      <h4 align="left">Ressources :</h4>
     <p align="left" class="intertitre">
      <img src="../../images/icones/h_parleur.gif" alt="a" width="20" height="20"> <a href="../tools/list_rsc.php?lg=Abzakh&aff=abzakh" target="_blank">Accès à toutes les ressources</a>
      </p>
       <p align="left" class="intertitre">&nbsp;</p>
      <p align="left" class="intertitre"><a href="../tools/show_text.php?id=crdo-ADY_GARCON_SOUND"><img src="../../images/icones/Txt_Inter_parchemin.jpg" width="20" height="20" border="0"> Le gar&ccedil;on adoptif</a><br>
      Turquie, Ankara, 1970, conteur anonyme, Catherine Paris</p>
      <p align="left">Source&nbsp;: </p>
      <p align="left">Paris, Catherine. 1980. Le gar&ccedil;on adoptif. <i>Bedi Kartlisa</i> 38:198-232.</p>
      <p align="left">Catherine Paris introduit cette histoire comme suit:</p>
      <div align="left">
        <blockquote> <em>Le conte que nous pr&eacute;sentons ici a &eacute;t&eacute; recueilli en &eacute;t&eacute; 1970 &agrave; Ankara, de la bouche d'un Tcherkesse d'un quarantaine d'ann&eacute;es, originaire de Reyhaniye, au Sud de la Turquie, et parlant le dialecte abzakh. [...] Le texte du Gar&ccedil;on adoptif a &eacute;t&eacute; transcrit ult&eacute;rieurement, &agrave; Paris, avec l'aide de M. Niaz Batouka, dont le parler, &eacute;galement abzakh, diff&egrave;re l&eacute;g&egrave;rement de celui du r&eacute;cit. C'est sa version que nous pr&eacute;sentons ici. </em></blockquote>
      </div>
      <p align="left">En pr&eacute;parant ce document pour &ecirc;tre consult&eacute; en synchronie avec l'enregistrement, Dina Dabjen-Bailly a fait quelques changements, r&eacute;ordonnant les phrases 27-29 (les num&eacute;ros des phrases 27-35 de la version publi&eacute;e sont inclus entre crochets carr&eacute;s), ajoutant les mots qui apparaissent entre parenth&egrave;ses dans les phrases 11 et 30, et inversant l'ordre des deux premiers mots du num&eacute;ro 34.</p>
      <p align="left">&nbsp;</p>
      <p align="left" class="intertitre"><a href="../tools/show_text.php?id=crdo-ADY_LISTE_NIAZ_SOUND"><img src="../../images/icones/Txt_Inter_parchemin.jpg" width="20" height="20" border="0"> Vocabulaire</a><br>
      France, Paris, 1974, Niaz Batouka, Catherine Paris</p>
      <p align="left">Cette liste a &eacute;t&eacute; pr&eacute;par&eacute;e par Catherine Paris et Niaz Batouka comme illustration des oppositions phonologiques de l'abzakh, et enregistr&eacute;e par M. Batouka, locuteur originaire de Berike, village syrien du plateau du Golan.</p>
      <p align="left">&nbsp;</p>
      <p align="left" class="intertitre"><a href="../tools/show_text.php?id=crdo-ADY_PATSITSE_SOUND"><img src="../../images/icones/Txt_Inter_parchemin.jpg" width="20" height="20" border="0"> Patsitse le Corbeau</a><br>
        Serbie, Kosovo, Stanovce, 1982, Isak Tsey, René Gsell</p>
      <p align="left">Ce conte a &eacute;t&eacute; improvis&eacute; par M. Isak Tsey, ing&eacute;nieur originaire du village de Stanovce au Kosovo, et enregistr&eacute; &agrave; Pristina par le Professeur Ren&eacute; Gsell en 1982. Il a &eacute;t&eacute; transcrit, avec l'aide de deux locuteurs abzakhophones, sous la direction de Catherine Paris au cours de son s&eacute;minaire en 1983.</p>
      <p align="left">Gsell, Ren&eacute;, Isak Tsey, Catherine Paris, Niaz Batouka, A. Tlich, Pierre Dr&eacute;an, Marc-Yves Lautrou. 1984. Patsitse-le-corbeau, conte tcherkesse en dialecte abzakh. <i>Bedi Kartlisa</i> lxii:253-292.</p>
      <p align="left">La transcription et la traduction fran&ccedil;aise du conte ont &eacute;t&eacute; informatis&eacute;es et synchronis&eacute;es avec l'enregistrement en 2003-2004 par Mme Dina Dabjen-Bailly, qui y a ajout&eacute; la transcription cyrillique &agrave; l'intention des Tcherkesses. Des omissions dans la transcription d'origine ont &eacute;t&eacute; corrig&eacute;es dans les phrases 66 et 67, et l'ordre des mots de la phrase 72 a &eacute;t&eacute; modifi&eacute;.</p>
      <h4 align="left"><br>
        La transcription</h4>
      <p>La transcription est d&eacute;crite par Mme Paris (1987) comme mi-phon&eacute;tique, mi-phonologique, correspondant &agrave; celle utilis&eacute;e par Dum&eacute;zil pour l'oubykh avec des modifications mineures. </p>
      <p>Voir le <a href="phonoabz.jpg" target="_blank" onClick="window.open(this.href,'popupLink','width=600,height=400',1);return false">tableau phonologique</a>.</p>
      <p align="center">&nbsp;</p>
      <p>Source du tableau : <br>
        Paris, Catherine et Batouka, Niaz. 1987-1992. <i>Dictionnaire Abzakh (tcherkesse occidental). II: Phrases et textes illustratifs.</i> (en trois volumes&nbsp;; le tableau appara&icirc;t dans chacun des trois volumes). Paris. <a href="http://www.peeters-leuven.be" target="_blank">Peeters</a>. (le <a href="../../vient-de-paraitre/dicoabzakh.htm">premier volume</a>, mais le dernier paru, est sorti en 2005)</p>
