      <p><b>La langue naxi (endonyme : /nɑ˩hi˧-kɯ˧ʈʂɯ˩/, 'Naxi'+'langue')</b> est parlée au nord-ouest de la province chinoise du Yunnan, près de la première boucle du fleuve Yangtzé, aux environs de la ville de Lijiang (naxi : /ʝi˧gv˧-dy˩). Elle appartient au groupe naish de la famille sino-tibétaine, qui comprend également le lazé et le na.  </p>
      <p>Afin d'éviter un décalage important entre la transcription et la prononciation réelle, <b>la transcription adoptée ici n'est pas strictement phonémique</b>. En particulier : </p>
      <ul>
      <li>les consonnes palatales peuvent être analysées comme allophones des vélaires : [c] comme /k/, [ɲ] comme /ŋ/, etc. Elles sont néanmoins notées comme palatales (phonétiquement) dans les transcriptions. </li>
      <li>les syllabes sans consonne initiale en reçoivent une au plan phonétique: /i/ est réalisé [ʝi], /u/ [wu], /ɯ/ [ɣɯ], etc. (La situation est similaire en naxi.) Ces syllabes sont transcrites ici avec leur initiale phonétique : [ʝi], [ɣɯ], etc.</li>
      </ul>
      <p>En revanche, les voyelles apicales [ʅ] et [ɿ] sont notées selon l'analyse phonémique : comme /ɯ/. </p>
      <p>Les conventions suivantes sont utilisées pour les <b>passages à supprimer ou ajouter </b>(suivant l'usage de Martine Mazaudon pour le tamang):</p>
    <table border="2">
<tr>
<td>[ ]</td><td>les crochets carrés indiquent un ajout à faire au texte (indiqué par le locuteur lors de la transcription)</td>
</tr>
<tr>
<td>< ></td><td>les chevrons indiquent un faux départ, lapsus ou emploi de termes qui paraissent inappropriés ; là aussi, il s'agit d'informations fournies par le locuteur lors du travail de transcription.</td>
</tr>
</table>
