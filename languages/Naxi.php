      <p><b>Enregistrements de Ge A-Gan </b>戈阿干先生录音资料</p> 
      <p>Ge A-Gan est une des grandes personnalités des études naxi. Il a consacré de nombreuses années à la collecte et l'étude des traditions naxi. Lorsque nous avons fait connaissance en 2011, Ge A-Gan était âgé de soixante-quinze ans, et les réflexions sur le cours de sa propre vie tenaient une place grandissante dans ses pensées. Au terme d'une carrière marquée par les soubresauts de l'Histoire, il est revenu vivre dans la maison familiale, dans son village natal des environs de Lijiang, où il a fondé un Centre pour la préservation de l'héritage culturel naxi. Lorsque je lui ai suggéré de réaliser des enregistrements de sa langue maternelle, il a souhaité enregistrer le récit de sa vie.</p>
      <p>Quatre amis naxi (Yang Jiehong 杨杰红, Yang Junlin 杨军林, He Liu 和柳, and He Wenjian 和文戬) ont assisté à l'enregistrement, formant un public connaisseur et chaleureux.</p>
     <p>Le premier fichier audio a une durée de plus d'une heure ; il présente la trame de la carrière de Ge A-Gan. Le second évoque les livres pictographiques to-mba des Naxi. Le troisième est une lecture commentée des premières pages d'un rituel central de la tradition naxi : le culte du Ciel, /mɯ˧py˩/.</p>
     <p>Ces trois enregistrements sont mis à libre disposition en ligne pour les personnes intéressées, dans l'esprit des principes de l'association "噜噜叭叭 Lulu Baba" fondée par Ge A-Gan pour la préservation de l'héritage culturel naxi.</p> 
     <p>Au plan technique, les enregistrements se présentent sous la forme de fichiers stéréo (de très grande taille). Le signal audio sur la piste gauche provient d'un micro de table Sony C535EB; la piste droite provient d'un micro serre-tête Sennheiser. Il n'existe pas de transcription à l'heure actuelle.</p>
     <p>(A. Michaud)</p>
