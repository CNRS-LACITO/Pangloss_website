      <h4><a name="vanuatu"></a>Vanuatu</h4>
      <p> Araki language is spoken in a small, round, rocky island officially called Araki (<b>Daki</b> in the vernacular); it is just three miles off the southern shores of Espiritu Santo, which is the largest island of the country. Vanuatu, formerly the New Hebrides, is an independent Republic of the South Pacific [<i>Map 1</i>]; it is mainly populated by Melanesian people, who settled there about three thousand years ago. </p>
      <p> The archipelago of Vanuatu is famous for its linguistic density, one of the highest in the world: more than a hundred distinct languages are spoken in its eighty islands, for an overall population of less than 200,000 [<i>Map 2</i>]. They all belong to the Oceanic group of Austronesian languages, and are often designated as &lsquo;Melanesian&rsquo; languages, though this label corresponds to more than one genetic sub&shy;group. More precisely, Araki belongs to the set of languages which Clark (1985) labelled "North and Central Vanuatu". Beside these vernacular languages, all Ni-Vanuatu can speak the English-based pidgin Bislama; but this <i>lingua franca</i> is mainly used in the two towns of the country, Port-Vila and Luganville, and seldom in rural areas. </p>
      <p>&nbsp;</p>
      <p align="center"> <IMG src="Oceania_English.gif" alt="oceania" width="60%" border="1"> <br>
        Map 1. <i>Location of Vanuatu inside the Pacific Ocean<br>
      </i>The largest island of this archipelago is Santo I., to which Araki is closest. </p>
      <p align="center">&nbsp;</p>
      <p align="center"> <IMG src="Vanuatu.gif" alt="vanuatu" width="60%" border="1" class="ZonePrint"><br> 
        Map 2 <i>Vanuatu <br>
      </i>The Republic of Vanuatu consists of more than a hundred distinct languages,<br>
all belonging to the Oceanic subgroup of the Austronesian family. </p>
      <h4><a name="araki"></a>Araki, a dying language <a href="#up"><img src="../../images/icones/fleche-haut.gif" alt="to up" width="10" height="9" border="0"></a></h4>
      <p> Former figures recorded up to <i>eighty</i> (Tryon and Charpentier 1989) or even <i>one hundred and five</i> (SIL 1983) speakers of Araki; but they are evidently overestimated. An in-depth sociolinguistic survey of the island (Varisipite and Bogiri-Vari 1998) showed that though <i>thirty four</i> people claimed to speak Araki, the majority of them are not fluent. In reality, there appears to have been a steady decrease in the number of speakers over the last three or four genera&shy;tions: as a consequence, there are nowadays hardly <i>fifteen speakers</i>, who can understand and speak Araki fluently. Most of them were born between the 1920s and the early 1960s, which means that this language ceased to be used daily, in most families of the island, at least fifty years ago. </p>
