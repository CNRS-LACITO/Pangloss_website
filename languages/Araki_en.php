      <p>Araki is no longer used in public, but only in a domestic context. This can explain why the language has survived in some families more than in others: e.g. Lele Moli's ten children, and even some of his grand&shy;children who lived with him, can still speak the language fairly well; but this extension to three generations is not usually found in other family clans. </p>
