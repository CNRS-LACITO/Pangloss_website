      <p><br/>
        Des corrections mineures doivent être apportées à la description du système tonale publiée en 2008 (Michaud 2008):<br/>
      </p>
      <ul>
  <li>
    <p>pour les disyllabes, il existe une catégorie tonale LML, distincte de LM. Un exemple était rapporté dans l'article de 2008 mais il y était considéré comme une exception.  D'autres exemples ont été observés depuis : voir Michaud and Latami Dashi (forthcoming).</p>
  </li>
  <li>
    <p>lorsqu'un groupe tonal (qu'on pourrait appeler "mot morphologique") ne comporte que des tons bas, un ton post-lexical est ajouté à la fin de la séquence tonale. Du fait de l'absence d'oppositions lexicales entre un schéma LM et un schéma LH, il est malaisé de déterminer si le ton ajouté est un ton M(oyen) ou un ton H(aut). Ce ton post-lexical était décrit comme M dans l'article de 2008 ; depuis, un argument phonologique a été trouvé qui suggère qu'il s'agit d'un ton H, et c'est ainsi qu'il est noté dans la transcription des textes présentés ici. L'argument est le suivant : tous les tons qui suivent ce ton post-lexical sont arasés en L, effet caractéristique du ton H et non du ton M.</p>
  </li>
</ul>
      <p><br/>
        Une étude détaillée de la grammaire tonale du na de Yongning est en cours.
  </p>
      </p>
      <p>
        Afin d'éviter un décalage important entre la transcription et la prononciation réelle, la <b>transcription adoptée ici n'est pas strictement phonémique</b>. En particulier : <br/>
<ul>
<li>
  <p>les voyelles transcrites comme [o] et [u] sont allophones d'un même phonème ; par souci de précision phonétique, [o] est employé après les dentales et les uvulaires, et [u] après les autres consonnes (bilabiales, alvéolo-palatales, rétroflexes, uvulaires et glottales)</p>
</li>
<li>
  <p>la nasale palatale [ɲ] peut être analysée comme allophone de /ŋ/</p>
</li>
<li>
  <p>les syllabes sans consonne initiale en reçoivent une au plan phonétique: /i/ est réalisé [ʝi], /u/ [wu], /ɯ/ [ɣɯ], etc. (La situation est similaire en naxi.) Ces syllabes sont transcrites ici avec leur initiale phonétique : [ʝi], [ɣɯ], etc.</p>
</li>
</ul>
<p>&nbsp;</p>
<p>En revanche, les voyelles apicales [ʅ] et [ɿ] sont notées selon l'analyse phonémique : comme /ɯ/. Au sujet de l'analyse phonémique, voir, à nouveau, Michaud 2008.</p>
<p>Les  <b>gloses grammaticales </b> suivent pour l'essentiel l'analyse proposée par Liberty Lidz (Lidz 2010); il va de soi que le transcripteur est seul responsible des différences introduites sur certains points. Les abréviations standard sont utilisées pour les concepts qui figurent dans la liste d'abréviations des Leipzig Glossing Rules (Comrie, Haspelmath, and Bickel), tandis que le terme entier est employé pour toutes les autres gloses. Les gloses techniques sont précédées d'un symbole spécial ° qui les différencie de simples traductions.</p>
<p>Les conventions suivantes sont utilisées pour les passages à supprimer ou ajouter (suivant l'usage de Martine Mazaudon pour le tamang) :</p>
<table border="2">
<tr>
<td>[ ]</td><td>les crochets carrés indiquent un ajout à faire au texte (indiqué par le locuteur lors de la transcription)</td>
</tr>
<tr>
<td>< ></td><td>les chevrons indiquent un faux départ, lapsus ou emploi de termes qui paraissent inappropriés ; là aussi, il s'agit d'informations fournies par le locuteur lors du travail de transcription.</td>
</tr>
</table>
<br/>
</p>
