      <h1 align="left">Oubykh</h1>
      <p class="intertitre">&nbsp;</p>
      <p class="intertitre">Fiche technique sur l'<a href="../../ALC/Languages/Ubykh_popup.htm" target="_blank" onClick="window.open(this.href,'popupLink','width=400,height=400,scrollbars=yes,resizable=yes',1);return false">oubykh<img src="../../images/icones/info.gif" alt="a" width="15" height="15" border="0"></a></p>
      <p align="left">&nbsp;</p>
      <p>La langue oubykh a fait l'objet de nombreuses &eacute;tudes par le caucasologue Georges Dum&eacute;zil (1898-1986). Elle repr&eacute;sentait, pour les m&eacute;dias de la fin du 20e si&egrave;cle, le type m&ecirc;me de langue en danger, puis de langue &eacute;teinte sous nos yeux avec la disparition du dernier locuteur en 1992. La richesse de son inventaire de consonnes est rest&eacute;e l&eacute;gendaire dans le monde de la linguistique.</p>
      <p>Les textes pr&eacute;sent&eacute;s ici proviennent d'une s&eacute;rie de dix bandes enregistr&eacute;es par G. Dum&eacute;zil &agrave; Paris en 1968 avec le dernier locuteur, M. Tevfik Esen&ccedil; (ou Tevfic Sani&ccedil;). Nous pr&eacute;sentons ici une s&eacute;rie de sept enregistrements de r&eacute;cits tir&eacute;s de la bande n&deg; 3.</p>
      <p>Les transcriptions et gloses mot-&agrave;-mot en fran&ccedil;ais de quatre de ces histoires, de l'&eacute;criture de Dum&eacute;zil lui-m&ecirc;me, ont &eacute;t&eacute; retrouv&eacute;es et sont  disponibles ici sous forme num&eacute;ris&eacute;e en format pdf. L'une de ces histoires, &quot;La chair de poisson rend intelligent&quot;, a &eacute;t&eacute; retrouv&eacute;e &eacute;galement dans une deuxi&egrave;me transcription, comprenant des gloses de morph&egrave;me en plus des gloses de mot, pr&eacute;par&eacute;e par Dum&eacute;zil pour une linguiste non-sp&eacute;cialiste, Mme Christine Leroy. La meilleure lisibilit&eacute; de cette deuxi&egrave;me annotation nous a encourag&eacute; &agrave; l'informatiser (sans les gloses de morph&egrave;me) et &agrave; la synchroniser avec l'enregistrement sonore, avec l'aide de Mme Dina Dabjen-Bailly, locutrice de tcherkesse.</p>
      <p>Les documents originaux ne comprennent pas de traduction au niveau de la phrase. Les traductions de phrase propos&eacute;es pour &quot;La chair de poisson rend intelligent&quot; ont &eacute;t&eacute; r&eacute;alis&eacute;es &agrave; partir des gloses de mot et de morph&egrave;me du document d'origine par un linguiste, B. Michailovsky, n'ayant pas de connaissance particuli&egrave;re de l'oubykh, aussi avec l'aide de Mme Bailly.</p>
      
      <p align="left">&nbsp;</p>
      <h4 align="left">Ressources :</h4>
     <p align="left" class="intertitre">
      <img src="../../images/icones/h_parleur.gif" alt="a" width="20" height="20"> <a href="../tools/list_rsc.php?lg=Ubykh&aff=oubykh" target="_blank">Accès à toutes les ressources</a>
      </p>
       <p align="left" class="intertitre">&nbsp;</p>
      <p class="intertitre"><strong><a href="../tools/show_text.php?id=crdo-UBY_POISSON_SOUND&idref=crdo-UBY_POISSON"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0"> Consulter le texte "La chair de poisson rend intelligent"</a></strong></p>
      <p>La liste compl&egrave;te des enregistrements et des annotations de cet ensemble de textes est la suivante:</p>
      <p> 1. &quot;La ch&egrave;vre et le mouton&quot; enregistrement (wav); annotation ms. de Dum&eacute;zil (pdf).<br>
        2. &quot;La chair du poisson rend intelligent&quot; enregistrement (wav); annotation ms. de Dum&eacute;zil (pdf); deuxi&egrave;me annotation (pdf).<br>
        3. &quot;C'est demain la fin du monde&quot; enregistrement (wav); annotation ms. de Dum&eacute;zil (pdf).<br>
        4. &quot;Le Hodjah est mort&quot; enregistrement (wav); annotation ms. de Dum&eacute;zil (pdf).<br>
        5. &quot;Les Narthes&quot; enregistrement (wav).<br>
        6. &quot;L'exode des Oubykh&quot; enregistrement (wav).<br>
        7. &quot;Voyage d'Istanbul &agrave; Paris&quot; enregistrement (wav).</p>
      
      <p>L'ensemble des 10 bandes a &eacute;t&eacute; num&eacute;ris&eacute; au Lacito en 2003 par M. Alexis Michaud, qui avait entrepris de rassembler,  cataloguer et effectuer l'archivage num&eacute;rique des mat&eacute;riaux oubykhs dispers&eacute;s &agrave; Paris, dans le cadre de son projet de m&eacute;moire pr&eacute;-doctoral (il est depuis devenu membre du Lacito). Les enregistrements de vocabulaire qui constituent la majeure partie des bandes et les transcriptions et traductions correspondantes seront  disponibles ult&eacute;rieurement.</p>
      <p>Pour l'interpr&eacute;tation du syst&egrave;me de transcription, on se r&eacute;f&egrave;rera aux ouvrages suivants :</p>
      <p>Charachidze, Georges, 1989, Ubykh, <em>in</em> J. Greppin ed., <em>The Indigenous languages of the Caucasus</em>, vol. 2, G. Hewitt ed., The North West Caucasian Languages, Caravan, Delmar-New York, 357-459.</p>
      <p>Colarusso, John. 1994. How many consonants does Ubykh have? Hewitt, G., ed. <em>Caucasian Perspectives</em>. Munich. Lincom Europa.</p>
      <p>Dum&eacute;zil, Georges, 1965,<em> Documents anatoliens sur les langues et les traditions du Caucase</em> III, <em>Nouvelles &Eacute;tudes Oubykhs</em>, Paris, Institut d'Ethnologie.</p>
      <p>Vogt, Hans, 1963, <em>Dictionnaire de la Langue Oubykh</em>, Oslo, Universitets Forlaget.</p>
      <p>Nous remercions les personnes suivantes qui nous ont permis de r&eacute;unir  les mat&eacute;riaux de d&eacute;part :</p>
      <ul>
        <li>
