      </ul>
      <p class="intertitre"><a href="../../membres/michailovsky.htm">B. Michailovsky</a>. 10 juillet 2006</p>
      <p>&nbsp;</p>
      <h2>Nouvelles transcriptions de Brian Fell, 2010</h2>
      <p>En mars 2008, un étudiant américain en linguistique travaillant sur l'oubykh, M. Brian Fell, nous a proposé de saisir, transcrire et traduire les sept histoires enregistrées par Dumézil. Nous présentons ici ses transcriptions des quatre histoires déjà transcrites par Dumézil, avec leur traduction en anglais. Les annotations sont largement basées sur les annotations manuscrites de Dumézil, avec quelques modifications dans la transcription. Ces annotations, synchronisées avec les enregistrements d'origine, sont accessibles ci-dessous :<strong><strong>
      </strong></strong></p>
      <ul class="liste-liens">
        <li><strong><strong><a href="http://lacito.vjf.cnrs.fr/pangloss/tools/show_text.php?id=crdo-UBY_CHEVRE_SOUND&idref=crdo-UBY_CHEVRE">La chèvre et le mouton</a></strong></strong></li>
        <li><strong><strong><a href="http://lacito.vjf.cnrs.fr/pangloss/tools/show_text.php?id=crdo-UBY_POISSON_SOUND&idref=crdo-UBY_POISSON">La chair de  poisson rend intelligent</a></strong></strong></li>
        <li><strong><strong><a href="http://lacito.vjf.cnrs.fr/pangloss/tools/show_text.php?id=crdo-UBY_FIN_DU_MONDE_SOUND&idref=crdo-UBY_FIN_DU_MONDE">C'est demain la fin du monde</a></strong></strong></li>
        <li><strong><strong><a href="http://lacito.vjf.cnrs.fr/pangloss/tools/show_text.php?id=crdo-UBY_HODJA_SOUND&idref=crdo-UBY_HODJA">Le Hodjah est mort</a></strong></strong></li>
      </ul>
      
      <p>L'annotation des trois histoires restantes, pour lesquelles nous ne disposons actuellement ni de transcription ni de traduction, est en cours.</p>
      <p class="intertitre">Pendant un séjour au Lacito en 2010, Brian Fell nous a aidé à compléter l'annotation des listes de vocabulaire que Dumézil avait enregistrées pour illustrer la phonologie de l'oubykh et vérifier certaines oppositions phonémiques. Ces annotations ont été saisies et synchronisées avec les enregistrements par M. Tanguy Solliec pendant un stage au Lacito en 2010.
      </p>
      <p>Brian Fell travaille actuellement à un dictionnaire de l'oubykh. Contact: bfell1618@gmail.com
      </p>
      <p>&nbsp;</p>
     
