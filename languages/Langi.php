        </li>
      </ul>
      <p>&nbsp;</p>
      <p>Le langi est une langue bantu (F33 dans la classification de Guthrie) parlée dans la région de Kondoa au centre de la Tanzanie par environ 200 000 personnes. Les textes qui suivent ont été enregistrés sur place en 1996, à Kondoa, Pahi et Piriri, dans un contexte traditionnel.</p>
      <h4 class="intertitre"><a name="transcription"></a>Transcription</h4>
      <p>Les textes sont transcrits en API (Alphabet phonétique international), à l'exception du symbole &quot;y&quot; qui correspond à la semi-voyelle palatale. Le système phonologique langi comprend 29 consonnes et 7 voyelles. La longueur de la voyelle est motivée : elle est indiquée par un redoublement. L'étude de la prosodie n'étant pas achevée, les tons ne sont pas mentionnés.</p>
      <h4 class="intertitre"><a name="characteristics"></a>Principaux traits linguistiques</h4>
      <p>La morphologie du langi est agglutinante, comme c'est le cas dans les autres langues bantu. On note, concernant cette langue que, bien que sa phrase soit ordonnée en SVO (Sujet Verbe Objet), elle présente plusieurs phénomène que l'on pourrait rattacher aux langues SOV ou VSO : certains types de conjugaison sous forme de périphrases comme le futur où l'auxilliaire suit l'infinitif, au lieu de le précéder comme on pourrait s'attendre à le trouver dans une langue en SVO ; ou encore, le cas des démonstratifs qui précèdent le nom alors que tous les autres déterminants du nom suivent le nom.</p>
      <p>Ces faits inhabituels sont certainement dus au contact qui s'est prolongé entre les Langi et deux autres populations voisines de langue couchitique, les Alagwa et les Burunge. En fait, la région où ces langues sont parlées, la &quot;Rift Valley&quot; de Tanzanie,  l'une des plus diversifiées linguistiquement en Afrique, est connue comme constituant une aire de contact où certains faits linguistiques sont partagés par des langues génétiquement différentes.<br>
      </p>
