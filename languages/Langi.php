      <h1 align="left"><a name="up"></a>Corpus langi<em> </em> </h1>
      <p class="intertitre">&nbsp;</p>
      <p class="intertitre">Fiche technique sur le <a href="../../ALC/Languages/Langi_popup.htm" target="_blank" onClick="window.open(this.href,'popupLink','width=400,height=400,scrollbars=yes,resizable=yes',1);return false">langi<img src="../../images/icones/info.gif" alt="a" width="15" height="15" border="0"></a></p>
      <p align="left">&nbsp;</p>
      <ul class="liste-liens">
        <li>
          <p><a href="#transcription">Transcription</a> </p>
        </li>
        <li>
          <p><a href="#characteristics">Principaux traits linguistiques</a></p>
        </li>
        <li>
          <p><a href="#biblio">R&eacute;f&eacute;rences bibliographiques</a> </p>
        </li>
