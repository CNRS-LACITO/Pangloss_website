      <h1 align="center">Koyi Rai Corpus </h1>
      <p class="intertitre">&nbsp;</p>
      <p class="intertitre"><a href="../../ALC/Languages/Koyi_Rai_popup.htm" target="_blank" onClick="window.open(this.href,'popupLink','width=400,height=400,scrollbars=yes,resizable=yes',1);return false">Koyi Rai<img src="../../images/icones/info.gif" alt="a" width="15" height="15" border="0"></a>: a brief note </p>
      <p>&nbsp;</p>
      <h4 align="left">Resources :</h4>
      <p class="intertitre">
      <img src="../../images/icones/h_parleur.gif" alt="a" width="20" height="20"> <a href="../tools/list_rsc_en.php?lg=Koyi rai&aff=Koyi Rai" target="_blank"> All available resources here
      </a></p>
      <p class="intertitre">&nbsp;</p>
      <p class="intertitre"><strong><a href="http://lacito.vjf.cnrs.fr/pangloss/tools/show_text_en.php?id=crdo-KKT_SELF-INTRO_SOUND&idref=crdo-KKT_SELF-INTRO"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0">Self-Introduction by Phanindra</a></strong><br>
        Nepal, Kathmandu, 2004, Phanindra Rai, <a href="../../membres/lahaussois.htm">Aim&eacute;e Lahaussois</a></p>
      <p class="ZonePrint">This is a self-introduction by Phanindra Rai, who is acknowledged to be one of the best speakers of Koyi Rai, even by elders. He is the son of a now-deceased shaman, and as his father taught him the ritual chants, it is he who trains new shamans in the language of the ritual. [As we were transcribing the story, Phanindra realized that the audio recording for this piece was not as complete as he would have liked, and added additional details (which will appear in a written form elsewhere, as there is no corresponding recording)]</p>
      <p class="intertitre"><strong><a href="http://lacito.vjf.cnrs.fr/pangloss/tools/show_text_en.php?id=crdo-KKT_RECIPE_SOUND&idref=crdo-KKT_RECIPE"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0"> Recipe for Millet Paste</a></strong><br>
        Nepal, Kathmandu, 2004, Phanindra Rai, <a href="../../membres/lahaussois.htm">Aim&eacute;e Lahaussois</a></p>
      <p class="ZonePrint">Recipe for making millet dish which is eaten by the Rais in Nepal (<em>dero</em>). This recipe is in response to a request for a description of how the dish is made, but the recipe was not accompanied by a demonstration.</p>
      <p class="intertitre"><strong><a href="http://lacito.vjf.cnrs.fr/pangloss/tools/show_text_en.php?id=crdo-KKT_MILLET_SOUND&idref=crdo-KKT_MILLET"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0"> Millet Paste Cooked on a Stone</a></strong><br>
        Nepal, Kathmandu, 2004, Phanindra Rai, <a href="../../membres/lahaussois.htm">Aim&eacute;e Lahaussois</a></p>
      <p class="ZonePrint">This story is the account of a three-person excursion into the jungle. During the excursion, which lasted several days, Phanindra, his grandfather and brother got lost and took refuge from the rain in a cave. There, they made millet paste (see Recipe for millet paste) by cooking it on a hot stone, which they ate accompanied by frog stew.</p>
      <p class="intertitre"><strong><a href="http://lacito.vjf.cnrs.fr/pangloss/tools/show_text_en.php?id=crdo-KKT_ORIGIN_SOUND&idref=crdo-KKT_ORIGIN"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0"> Origin Myth</a></strong> <br>
        Nepal, Kathmandu, 2004, Phanindra Rai, <a href="../../membres/lahaussois.htm">Aim&eacute;e Lahaussois</a></p>
      <p class="ZonePrint">This is a version of the origin myth for the Koyi people. It tells of a time before man, when two gods, Ruwahang and Paruhang, lived in the heavens, and Ribipma, the original female, lived alone on earth. As Ribipma grew, she wanted to find a mate, and it came to her in a dream that if he climbed a walnut tree and whistled, she would be satisfied. She did so, and the wind came and impregnated her. She bore a daughter Nayoma, who in turn grew up and sought a mate of her own. She imitated her mother, climbed a walnut tree, and called the wind. Her mother left her, infuriated by her incestuous behaviour, and she fell unconscious. Some friendly thrushes, messengers of the heavens, woke her, and she looked up at the sky and saw Ruwahang, and fell in love with him. She asked the thrushes to bring him a message that she loved him, but when they arrived in the sky, Ruwahang had left and Paruhang was there instead. They convinced him to come down instead, but he, suspicious that she would not like his appearance, sent her a present of a comb. She saw the comb and came to love him for his craft, and the thrushes convinced him to descend to earth. When Nayoma saw him, she was disgusted by his festering face, and rejected him. Paruhang cursed her, in fury, and dried up all the water on earth. When Nayoma regained consciousness, she was very thirsty, and all she could find to drink was urine that Paruhang had put on a yam leaf, which she was forced to drink on all fours. When she drank, she became pregnant with all things in the world, and water reappeared on earth. When she gave birth to all things, Paruhang had the thrushes separate out the humans from the rest, and cut their umbilical cords. Due to a mistake on their part, she raised humans and a number of animals, all together. As a result of jealousies between them, she went off to live in the jungle with one of them, Leopard, but they were as man and wife and brought back luck upon themselves. Leopard eventually killed his mother, and his brothers found the body. In the meantime, Man (the male human among the children) had an adventure of his own, falling in love with the daughter of Naga, the snake god. He went down into the rivery underworld to ask Naga for his daughter's hand, and they were told that they would have to farm the earth for a living. When Man and Naga's daughter could not conceive, they called for Man's two sisters (who had thought he was dead, after he fainted from hunger): unable to contact them, he sent various animals, until finally a rooster drew them in the right direction by called out &quot;kokchulupa&quot; (which happened to be one of his names). Eventually, Man had many children, who ended up populating the area of Sungdel.</p>
      <p class="intertitre"><strong><a href="http://lacito.vjf.cnrs.fr/pangloss/tools/show_text_en.php?id=crdo-KKT_SYURIME_SOUND&idref=crdo-KKT_SYURIME"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0"> Syurime </a></strong><br>
        Nepal, Kathmandu, 2004, Phanindra Rai, <a href="../../membres/lahaussois.htm">Aim&eacute;e Lahaussois</a></p>
      <p class="ZonePrint">Sjurime is the story of how the Koyi people came to live in the villages of Sungdel and Dipsung.</p>
      <p class="intertitre"><a href="http://lacito.vjf.cnrs.fr/pangloss/tools/show_text_en.php?id=crdo-KKT_CONVERSATION_SOUND&idref=crdo-KKT_CONVERSATION"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0"> Conversation between Two Cousins</a><br>
        Nepal, Kathmandu, 2004, Phanindra Koyee Rai &amp; Ram Kumar Rai, <a href="../../membres/lahaussois.htm">Aim&eacute;e Lahaussois</a></p>
      <p>This is a staged conversation between two cousins. They play out an imagined reunion between two cousins who have not seen each other in a while, one living in the Gulf States for work, and the other living in the village. They compare notes about their personal situations and life in the village (including the difficulties of living with the Maoists, refered to as 'uncles'), and discuss their plans for the immediate and longer-term future.<br>
        In the Sentence level, R marks utterances by Ram Kumar, and P marks utterances by Phanindra </p>
