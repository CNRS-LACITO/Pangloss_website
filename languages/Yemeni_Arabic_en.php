      <h1 align="left">Yemeni Arabic Corpus </h1>
      <p class="intertitre">&nbsp;</p>
      <p class="intertitre"><a href="../../ALC/Languages/Yemeni_Arabic_popup.htm" target="_blank" onClick="window.open(this.href,'popupLink','width=400,height=400,scrollbars=yes,resizable=yes',1);return false"><img src="../../images/icones/info.gif" alt="info" width="15" height="15" border="0">Yemeni Arabic</a>: a brief note </p>
            <p align="left">&nbsp;</p>
      <h4 align="left">Resources :</h4>
      <p class="intertitre">
      <img src="../../images/icones/h_parleur.gif" alt="a" width="20" height="20"> <a href="../tools/list_rsc_en.php?lg=Yemeni Arabic&aff=Yemeni Arabic" target="_blank"> All available resources here
      </a></p>
      <p class="intertitre">&nbsp;</p>

      <p>Traditionally, women in Sanaa tell folk tales during women&rsquo;s gatherings which take place in the homes of each woman in turn, on an almost daily basis, between the afternoon and sunset prayers. The events of daily life (sicknesses, deaths, births, return from travel&hellip;) serve as the pretext for these gatherings which are above all the occasion to settle family disputes, neighborhood quarrels or to forge matrimonial alliances. <br>
        When circumstances permit, as in the case of births, a moment is set aside to tell stories and to ask riddles. <br>
        The tales and riddles contribute to the education and transmission of the social rules of the women. It is in this way that they acquire the art of exchanges and concessions. However, with the advent of television, it is now obvious that these practices are dying out. <br>
        In Sanaa Yemeni Arabic, the words for story and riddle share the same root. <br>
        These two literary genres also share a formal introductory &ldquo;frame&rdquo;, constituted by a ritual beginning phrase and a ritual ending phrase. <br>
        In tales, to which the women ascribe a pacifying role, these ritual phrases invoke divine protection. In riddles, the ritual ending phrase is preceded by a dialogue in which the partners in the game contract debts and negotiate the payment modalities. &ldquo;Riddles&rdquo;, as the women say, &ldquo;are often a cause for separation, quarrels and death. It is the tales which bring people together.&rdquo;</p>
      <p class="intertitre"><a href="../tools/show_text_en.php?id=crdo-AYN_SAMIA_SOUND&idref=crdo-AYN_SAMIA"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0"> Yemeni Arabic Story from Sanaa</a><br>
        Yemen, Sanaa, 1989n <a href="../../membres/naim.htm">Samia Na&iuml;m </a></p>
      <p>The tale presented here was recorded in Sanaa, in 1989. It is told by a woman in her forties. The recording took place at her house, with no other audience, which explains the comments she was able to make for my benefit, and which have the double advantage of raising issues of dialectal inter-comprehension (contact phenomena within a single language, Arabic) and to show the linguistic awareness that speakers have of their own speech. <br>
        The transcription faithfully follows the sound recording in that it notes the phonetic and prosodic contextual variants (due to accentuation, pauses, rhythm) present at the phonemic level (voicing, devoicing, lengthening, shortening, deletion) and at the syllabic level (modifications in syllable structure).</p>
