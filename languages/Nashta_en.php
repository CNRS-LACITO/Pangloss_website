      <h3><a name="anecdotes"></a>Anecdotes <a href="#up"><img src="../../images/icones/fleche-haut.gif" alt="to up" width="10" height="9" border="0"></a></h3>
      <p>Telling anecdotes in Nashta is one of the last speakers&rsquo; favorite pastimes. This activity usually leads to general hilarity, but can often be difficult to understand for outsiders as the jokes call upon stores of local knowledge. The most complete are given here. </p>
      <p class="intertitre"> <a href="../tools/show_text_en.php?id=crdo-MKD_BERGER_SOUND&idref=crdo-MKD_BERGER" target="_blank"><img src="../../images/icones/h_parleur.gif" alt="a" width="20" height="20" border="0"> The shepherd and his shadow</a><br>
Greece, Liti,  2004, anonymous, <a href="../../membres/adamou.htm">Evangelia Adamou</a></p>
      <p>This short narrative tells the story of a simple minded shepherd who is afraid of his shadow. He believes it is a person who will not stop following him, and gives it all the pastries his mother had made for him.</p>
      <p class="intertitre"><a href="../tools/show_text_en.php?id=crdo-MKD_COUPLE_SOUND&idref=crdo-MKD_COUPLE" target="_blank"><img src="../../images/icones/h_parleur.gif" alt="a" width="20" height="20" border="0"> The arguing coupl</a>e<br>
Greece, Liti, avril 2002,  anonymous, <a href="../../membres/adamou.htm">Evangelia Adamou</a></p>
      <p>This story is about marital strife. As a judge has ordered them to stop insulting each other, a couple resorts instead to miming their favorite insults with their hands: &ldquo;tub of lard&rdquo; and &ldquo;crusty-eyes&rdquo;. </p>
      <p class="intertitre"><a href="../tools/show_text_en.php?id=crdo-MKD_DIMITRO_SOUND&idref=crdo-MKD_DIMITRO" target="_blank"><img src="../../images/icones/h_parleur.gif" alt="a" width="20" height="20" border="0"> Little Dimitro and the beans</a><br>
Greece, Liti, avril 2002, anonymous, <a href="../../membres/adamou.htm">Evangelia Adamou</a></p>
      <p>This story describes the misunderstandings which can arise when someone lacks common sense. Little Dimitro is helping his mother in the kitchen. Every time she asks him to &lsquo;give the beans some more dung&rsquo;, instead of adding the dung to the fire, he puts it in the pot. </p>
      <p class="intertitre"><a href="../tools/show_text_en.php?id=crdo-MKD_FANTOMES_SOUND&idref=crdo-MKD_FANTOMES" target="_blank"><img src="../../images/icones/h_parleur.gif" alt="a" width="20" height="20" border="0"> Ghost</a>s<br>
Greece, Liti, avril 2002, anonymous, <a href="../../membres/adamou.htm">Evangelia Adamou</a></p>
      <p>In this story, the hero is the narrator&rsquo;s father. Being convinced that ghosts exist, the valiant young man decides to fight them. He thus goes out in the middle of the night, armed with a knife. All he succeeds in killing is a poor little donkey who was unfortunate enough to sneeze in the vicinity of the ghost hunter! </p>
      <p>&nbsp;</p>
      <h3><a name="traditions"></a>Traditions<span class="intertitre"> <a href="#up"><img src="../../images/icones/fleche-haut.gif" alt="to up" width="10" height="9" border="0"></a></span></h3>
      <p>It is not easy to speak about traditions. These narratives do however give some idea of the basics in the traditional daily life in the village. </p>
      <p class="intertitre"><a href="../tools/show_text_en.php?id=crdo-MKD_CONFRERIE_SOUND&idref=crdo-MKD_CONFRERIE" target="_blank"><img src="../../images/icones/h_parleur.gif" alt="a" width="20" height="20" border="0"> Brotherhoods</a><br>
Greece, Liti,  2003, anonymous, <a href="../../membres/adamou.htm">Evangelia Adamou</a></p>
      <p>As indicated by the narrator, brotherhoods were a very important part of society (and partially remain so today). The village brotherhoods brought together young people belonging to a same generation, without any professional purpose. In fact, brotherhoods were widespread as far back as the Middle Ages throughout Christian Europe, and were organized by the clergy. Members of a brotherhood lent each other mutual assistance, and were active in organizing and controlling festivities and ceremonies: marriages, funerals, religious holidays. </p>
      <p class="intertitre"><a href="../tools/show_text_en.php?id=crdo-MKD_FETE_SOUND&idref=crdo-MKD_FETE" target="_blank"><img src="../../images/icones/h_parleur.gif" alt="a" width="20" height="20" border="0"> Village festivities</a><br>
Greece, Liti,  2004, anonymous, <a href="../../membres/adamou.htm">Evangelia Adamou</a></p>
      <p>This text briefly describes the festivities that would take place during religious events. As in the text on &lsquo;brotherhoods&rsquo; one finds the custom of auctions, where the highest bidder is awarded a saint&rsquo;s icon. It also mentions the social rules governing dances, the dancers being ordered depending on how long they have been married. </p>
      <p class="intertitre"><a href="../tools/show_text_en.php?id=crdo-MKD_MAIRE_SOUND&idref=crdo-MKD_MAIRE" target="_blank"><img src="../../images/icones/h_parleur.gif" alt="a" width="20" height="20" border="0"> Institutions</a><br>
Greece, Liti,  2006, locuteur anonyme, <a href="../../membres/adamou.htm">Evangelia Adamou</a></p>
      <p>This text describes how mayors are elected, how taxes are levied, the clergy and teachers, both in the later stages of the Ottoman Empire and in the Kingdom of Greece. </p>
      <p class="intertitre"><a href="../tools/show_text_en.php?id=crdo-MKD_MARIAGE_SOUND&idref=crdo-MKD_MARIAGE" target="_blank"><img src="../../images/icones/h_parleur.gif" alt="a" width="20" height="20" border="0"> Wedding</a><br>
        Greece, Liti,  2006, anonymous, <a href="../../membres/adamou.htm">Evangelia Adamou</a></p>
      <p>&nbsp;</p>
      <h3><a name="vie-quotidienne"></a>Daily life<span class="intertitre"> <a href="#up"><img src="../../images/icones/fleche-haut.gif" alt="to up" width="10" height="9" border="0"></a></span></h3>
      <p>There are very few of these texts as they are often of a highly personal nature. Life stories are often about the war in Albania against the fascist Italian army and the Nazi army. Non veterans speak of their professional lives. Our choice here is a description of childhood games.</p>
      <p class="intertitre"><a href="../tools/show_text_en.php?id=crdo-MKD_JEUX_SOUND&idref=crdo-MKD_JEUX" target="_blank"><img src="../../images/icones/h_parleur.gif" alt="a" width="20" height="20" border="0"> Games</a><br>
Greece, Liti,  2006, anonymous, <a href="../../membres/adamou.htm">Evangelia Adamou</a></p>
      <p>This is a brief description of children&rsquo;s games in the 1930s. It also mentions the peripheral role school played in their lives and the obligation to help one&rsquo;s family in times of need.</p>
      <p>&nbsp;</p>
      <h3> <a name="savoir-faire"></a>Traditional techniques<span class="intertitre"> <a href="#up"><img src="../../images/icones/fleche-haut.gif" alt="to up" width="10" height="9" border="0"></a></span></h3>
      <p class="intertitre"><a href="../tools/show_text_en.php?id=crdo-MKD_SOIE_SOUND&idref=crdo-MKD_SOIE" target="_blank"><img src="../../images/icones/h_parleur.gif" alt="a" width="20" height="20" border="0"> Silk</a><br>
Grèce, Liti,  2003, anonymous, <a href="../../membres/adamou.htm">Evangelia Adamou</a></p>
      <p>Weaving silk was an important occupation in the beginning of the 20th century, as it was in some regions in southern France (see also a fictional description in &ldquo;La soie&rdquo; (silk) by Alessandro Barico). In this narrative, the speaker explains how his mother used to prepare the silk worms. Other stories explain that this silk was both sold and used to make clothes. </p>
      <p class="intertitre"><a href="../tools/show_text_en.php?id=crdo-MKD_MOISSON_SOUND&idref=crdo-MKD_MOISSON" target="_blank"><img src="../../images/icones/h_parleur.gif" alt="a" width="20" height="20" border="0"> Harvesting</a><br>
Greece, Liti,  2003, anonymous, <a href="../../membres/adamou.htm">Evangelia Adamou</a></p>
      <p>This narrative is about the harvest, one of the most important aspects of village life. It briefly mentions the tools used, how the work was organized in the fields (strangely enough, no mention is made of pooling family labor), as well as a description of ordinary meals. </p>
      <p class="intertitre"><a href="../tools/show_text_en.php?id=crdo-MKD_CHAMPS_SOUND&idref=crdo-MKD_CHAMP" target="_blank"><img src="../../images/icones/h_parleur.gif" alt="a" width="20" height="20" border="0"> Life in the field</a>s<br>
Greece, Liti, avril 2002, anonymous, <a href="../../membres/adamou.htm">Evangelia Adamou</a></p>
      <p>This text opens with a description of life in the fields before moving on to a comparison between life in &lsquo;the old days&rsquo; and life today. Linguists who document endangered languages are well used to these comparisons among elderly consultants, who have seen their worlds change, and look back on the past with some nostalgia. </p>
      <p>&nbsp;</p>
      <h3><a name="Pear-Story"></a>Pear Story<span class="intertitre"> <a href="#up"><img src="../../images/icones/fleche-haut.gif" alt="to up" width="10" height="9" border="0"></a></span></h3>
      <p>These texts follow a showing of the elicitation video &ldquo;Pear Story&rdquo; by Chafe. In the video, there is a small thief who steals a basket full of pears from a farmer who is picking the pears from a tree.  </p>
      <p class="intertitre"><a href="../tools/show_text_en.php?id=crdo-MKD_POIRES_SOUND&idref=crdo-MKD_POIRES" target="_blank"><img src="../../images/icones/h_parleur.gif" alt="a" width="20" height="20" border="0"> Pear Story 1</a><br>
Greece, Liti,  2011, anonymous, <a href="../../membres/adamou.htm">Evangelia Adamou</a></p>
      <p>During the first viewing, the consultant is asked to simply watch the film. During a second viewing, the consultant describes the film as he watches; thus one obtains a synchronized description of the video.</p>
      <p class="intertitre"><a href="../tools/show_text_en.php?id=crdo-MKD_POIRES_SOUND&idref=crdo-MKD_POIRES_PASSE" target="_blank"><img src="../../images/icones/h_parleur.gif" alt="a" width="20" height="20" border="0"> Pear Story 2</a><br>
Greece, Liti,  2011, anonymous, <a href="../../membres/adamou.htm">Evangelia Adamou</a></p>
      <p>The consultant is then asked to summarize the story. </p>
      <p>&nbsp;</p>
      <h3><a name="conte"></a>Folk tale<span class="intertitre"> <a href="#up"><img src="../../images/icones/fleche-haut.gif" alt="to up" width="10" height="9" border="0"></a></span></h3>
      <p>When this fieldwork was carried out, oral traditions had already mostly died out. The speakers remembered very few folk tales.</p>
      <p class="intertitre"><a href="../tools/show_text_en.php?id=crdo-MKD_CONTE_SOUND&idref=crdo-MKD_CONTE" target="_blank"><img src="../../images/icones/h_parleur.gif" alt="a" width="20" height="20" border="0"> A girl and a ghost</a><br>
Greece, Liti,  2004, anonymous, <a href="../../membres/adamou.htm">Evangelia Adamou</a></p>
      <p>In this narrative, the speaker is a woman of approximately sixty years of age. She is therefore one of the &ldquo;rememberers&rdquo;, but had only sketchy mastery of the language. This can be clearly seen in her phonology, which is that of modern school Greek, as well as in her metalinguistic commentary (&ldquo;how does one say…?&rdquo;). Contrary to our hopes of an &ldquo;authentic&rdquo; tale, this story was heard among the Greek refugees from Asia Minor who had come to live in the village, and was translated into Nashta for us.
      </p>
