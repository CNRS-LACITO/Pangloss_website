      <h4 class="intertitre"><a name="transcription"></a>Transcription</h4>
      <ul>
        <li> Les limites de morphèmes sont signalées par des tirets</li>
        <li>La longueur vocalique est marquée par une double voyelle, les voyelles nasales phonologiques sont marquées par un accent circonflexe et les voyelles centrales sont marquées par une diérèse </li>
        <li>Lénition consonantique en position intervocalique : </li>
      </ul>
      &nbsp;/p/ or /f/ &gt; [v] or [w] /r h/ &gt; [r] &nbsp; /fw/ &gt; [w] /c/ or / &#643;/ &gt; [j] &nbsp; /t/ &gt; [r], [l] /k/ or /x/ &gt; [ &#611;] &gt; &Oslash; &nbsp;
      <p> La lénition consonantique entre voyelles se produit à la limite des mots, à l’initiale ou à la finale :<br> 
        <em>ni vââl</em>û &quot;dans les champs de taro&quot; (<em>pââlû</em> &quot; champs de taro&quot;) ; <em>jeux-i na</em> &quot;proche de moi&quot; (<em>jeuk</em> &quot;proche&quot;). </p>
      <ul>
        <li> Les sandhis finaux sont signalés par des tirets : <em>hleeli pe-whaayaw-i hla</em> &quot;leurs guerres&quot; (<em>whaayap</em> &quot;combat, guerre&quot;) ; <em>pe-faxer-i hli</em> &quot;leur relation tabou&quot; (<em>faxet</em> &quot;tabou&quot;) <br>
        </li>
      </ul>
      <h4 class="intertitre"><a name="characteristics"></a>Caractéristiques principales <a href="#up"><img src="../../images/icones/fleche-haut.gif" alt="up" width="10" height="9" border="0"></a></h4>
      <ul>
        <li>Langue à tête initiale. </li>
        <li> Classificateurs numériques (une vingtaine) et classificateurs possessifs (pour la nourriture, les armes, les paniers)</li>
        <li> Ordre VOS avec des arguments nominaux ; ordre sVo avec des pronoms personnels. </li>
        <li> Morphologie ergative avec des arguments nominaux; système accusatif avec des pronoms personnels <br>
          Les marques d’agent sont <em>(e)a</em> (+humain) et <em>ru</em> (- humain). Le cas absolutif est marqué par zéro</li>
        <li> La topicalisation d’un argument ou d’un circonstant détaché est marquée par <em>xe</em> </li>
        <li> Pour un petit nombre de verbes, le  réfléchi est marqué par des pronoms sujet et objet coréférents : <em>i mwaaxi-e</em> &quot;il s’allonge&quot; (<em>litt</em>. il allonge lui).</li>
        <li> Selon les classes de nom, la possession peut être marquée soit directement (par des suffixes possessifs ou des déterminants nominaux) ou indirectement (par le morphème <em>i</em>). Les noms relationnels ont un suffixe obligatoire de 3ème personne (<em>-n</em> +humains ; <em>-t</em> +inanimés, générique ou humains collectifs).</li>
        </ul>
