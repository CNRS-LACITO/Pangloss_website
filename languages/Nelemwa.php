      <h3 class="titre">Pr&eacute;sentation de la langue n&ecirc;l&ecirc;mwa</h3>
      <p>Le <em>nêlêmwa</em> et sa variante dialectale le <em>nixumwak</em> font partie des 29 langues kanak de la Nouvelle-Calédonie, qui appartiennent au sous-groupe Océanien de la famille des Langues Austronésiennes. Elles sont parlées par environ 1500 locuteurs, à l'extrême nord de l'île principale de la Nouvelle-Calédonie (autour de Koumak, Poum, Tiabet, ainsi que sur les îles voisines).</p>
