      <h4 align="left">Romani Texts</h4>
      <p class="intertitre"><a href="../tools/show_text_en.php?id=crdo-RMN-ROM3_SOUND&idref=crdo-RMN-ROM3"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0"> The Louse and the Rom</a><br>
        Grèce, Thrace, Komotini, 2006, <a href="../tools/show_text.php?id=crdo-RMN-ROM3_SOUND" target="_blank"></a><a href="../../membres/adamou.htm">Evangelia Adamou</a></p>
      <p>This is a comical tale of a Rom anti-hero who gets married to the king&rsquo;s daughter (for having recognized an over-sized louse), wins a war for his new kingdom (for having followed a dog who stole his food) and is mistaken for a wise, educated man (for speaking Romani to an old Rom woman begging in the streets).</p>
      <p class="intertitre"><a href="../tools/show_text_en.php?id=crdo-RMN-MAN_SNAKE_SOUND&idref=crdo-RMN-MAN_SNAKE"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0"> The Man-Snake</a><br>
        Grèce, Thrace, Komotini, 2006, <a href="../../membres/adamou.htm">Evangelia Adamou</a></p>
      <p>This is a short version of a classical tale of a man who was born as a snake. The man-snake kills all the women he marries during their wedding night. But one mother dreams of how her daughter will get married to him and help him stay a man. She has to wear forty one pieces of clothing and take them off one by one as the man-snake removes one by one his forty coats of snake skin. Thus he removes all his layers of snake skin and reveals himself as a very handsome man.</p>
      <p class="intertitre"><a href="../tools/show_text_en.php?id=crdo-RMN-COWARD_SOUND&idref=crdo-RMN-COWARD"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0"> The Coward and the Giants</a><br>
        Grèce, Thrace, Komotini, 2006, <a href="../../membres/adamou.htm">Evangelia Adamou</a></p>
      <p>This tale is another case where the Rom is an anti-hero who is mistaken for a hero. The story is about the village coward who is even scared to go to the toilet alone at night. But one such night he kills forty mosquitoes and proudly buys a sword with this inscription: “the man who killed forty at once”. One day, while he’s sleeping under a tree, some giants happen to see his sword and thinking he’s really strong, they take him as their groom. Except that their groom turns out to be extremely lazy and their only wish then becomes to find a way to get rid of him.<br>
        <br>
      </p>
      <h4>Romani </h4>
      <p>Romani is an Indic language. The dialects currently spoken in Greece belong to two main groups: Balkan Romani and Vlach Romani. The presence of Balkan Romani speakers is documented as early as the 11th century and has been continuous since then. The Vlach groups arrived more recently, mostly in the 1920s after the Lausanne Treaty. Komotini-Anahoma Romani is clearly a Vlach variety. <br>
        The city of Komotini is famous for Romani studies because this is where the earliest text of Balkan Romani was taken down in 1668 by Evliya Çelebi and published in Seyahat under the title ‘Book of travels’ (see Friedman and Dankoff 1991). <br>
        Today, men, women and children are trilingual in Romani, Turkish and Greek (Romani is still transmitted to children). The most common language name is romane (adv.), characterized by the loss of the final -s. There is a distinction between the Rom varieties spoken by the elderly, considered pure and called the ‘closed language’, while the younger generation uses bilingual speech, Romani-Turkish, to which they refer as the ‘open language’. </p>
      <h4>Self-identification </h4>
      <p>The Rom community in Anahoma identifies itself based on two characteristics: <br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        - The first group designation is related to the name of the city they are live in: thus they call themselves <em>komotin&acute;ia</em>, from the Greek name, Komotini, or <em>giumurdzjinal&acute;ia</em> from the Turkish G&uuml;m&uuml;lcine.<br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;    	
        - The second name is related to their religion: they call themselves<em> xoraxan&acute;e roma</em> &ldquo;Muslim, Turkish Rom&rdquo;, a very common name in the Balkans.<em> Xoraxan&acute;e</em> indicates their Muslim religion, to distinguish themselves from the <em>dasikan&acute;e roma</em> &ldquo;Christian Rom, Greek Rom&rdquo;. While in most Romani speaking communities the Greeks are called <em>balam&rsquo;e</em>, here they are named here <em>dasikan&acute;e</em>. This name, used elsewhere for the Slavs, indicates that the Rom using it nowadays probably lived in contact with Slavs (possibly in Bulgaria), who were &ldquo;the Christians&rdquo; in the main religious dichotomy Christians/Muslims. When they arrived in Thrace, where the Slavs are Muslims (called Pomaks), they applied the name <em>dasikan&acute;e</em> &ldquo;Christians&rdquo;, to the local Christian population, the Greeks.</p>
