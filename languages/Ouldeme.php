        <h4 class="intertitre"><a name="presentation"></a>Pr&eacute;sentation </h4>
        <p>Parmi les populations montagnardes, toutes de langue tchadique, comprenant entre 2 000 et 15 000 habitants et occupant la bordure orientale des monts du Mandara (Nord-Cameroun), celle des Ould&eacute;m&eacute;s est de taille moyenne. On compte parmi les populations voisines les Podokos, les Moukt&eacute;l&eacute;s, les Moras-massif, les Vam&eacute;-br&eacute;m&eacute;s, les Madas, les Ourzos et les Mouyangs.<br>
          Le territoire ould&eacute;m&eacute; est un petit &quot;massif presqu'&icirc;le&quot; de trois kilom&egrave;tres sur six, adoss&eacute; au plateau central des monts du Mandara et bien individualis&eacute; &agrave; cause des larges vall&eacute;es des mayo Plata et Ould&eacute;m&eacute; qui l'encerclent. Quand on en fait le tour, les habitations sont invisibles et le massif offre l'aspect d'une forteresse. Les habitants se regroupent sur le sommet et le haut des pentes entre 650 et 900 m&egrave;tres. De nos jours, certains villages ont amorc&eacute; une descente et se regroupent en pi&eacute;mont.<br>
          Cette montagne est situ&eacute;e dans une r&eacute;gion de savane arbustive, au climat sec et &agrave; la v&eacute;g&eacute;tation pauvre pendant la plus grande partie de l'ann&eacute;e. Les pluies tombent de juin &agrave; ao&ucirc;t. Le paysage n'est vert que pendant cette p&eacute;riode. C'est &agrave; cette saison que ses habitants, agriculteurs de longue date, proc&egrave;dent &agrave; leurs cultures dont le produit de base est le mil.<br>
          Les modes de vie de ces populations, de tradition animiste, contrastent avec ceux des musulmans des villes, Foulb&eacute;s et Mandaras, souvent commer&ccedil;ants (notre conte y fait allusion). Comme dans l'ensemble des monts du Mandara, l'&eacute;conomie ould&eacute;m&eacute; est une &eacute;conomie de subsistance dans un milieu contraignant, ayant maintenu son &eacute;quilibre dans l'autarcie. Cet &eacute;quilibre devient pr&eacute;caire s'il s'agit d'entrer dans l'&eacute;conomie mon&eacute;taire. Les exploitations agricoles traditionnelles, viables en autarcie, ont utilis&eacute; au maximum les conditions naturelles. Or, les possibilit&eacute;s d'am&eacute;lioration du niveau de vie sont relativement limit&eacute;es. Les cultures se font &agrave; la houe. Dans ces conditions, &eacute;lever un enfant infirme est une lourde charge, ce que sugg&egrave;re le conte. Ce r&eacute;cit, toujours d'actualit&eacute;, fait partie de l'ensemble le plus appr&eacute;ci&eacute; de nos jours par les jeunes Ould&eacute;m&eacute;s. C'est cette r&eacute;alit&eacute; qui a guid&eacute; notre choix.<br>
          La langue ould&eacute;m&eacute;, comme les autres langues tchadiques, peut, par ses racines et certains traits grammaticaux, &ecirc;tre rapproch&eacute;e de l'ensemble chamito-s&eacute;mitique. Pour plus de pr&eacute;cisions sur les affixes verbaux et les phon&egrave;mes originaux, tels que les consonnes lat&eacute;rales-fricatives, on peut se reporter aux ouvrages cit&eacute;s ci-apr&egrave;s.</p>
