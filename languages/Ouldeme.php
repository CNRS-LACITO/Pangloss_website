        Cameroun, Dibon, 1976, Machkoua Alegnoua, <a href="../../membres/colombel.htm">V&eacute;ronique de Colombel </a></p>
        <p>Le conte  a &eacute;t&eacute; enregistr&eacute;  sur le massif ould&eacute;m&eacute;, dans le village de Dibon, le 10 mai 1976, un soir de saison s&egrave;che, dans le cadre traditionnel.<br>
  &Agrave; la naissance d'un enfant qui arrive au monde avec une grosse t&ecirc;te ronde, un seul bras et une seule jambe, les parents d&eacute;cident de quitter pour toujours leur maison en y abandonnant l'enfant monstrueux. Mais la s&oelig;ur a&icirc;n&eacute;e, bien qu'étant encore une fillette, tient &agrave; l'emmener. Voyant cela, les parents décident d'abandonner les deux enfants en chemin. Plus tard, apr&egrave;s avoir demand&eacute; &agrave; sa s&oelig;ur de le cogner contre un tamarinier, puis contre un arbre &agrave; gr&ecirc;lons, le gar&ccedil;on retrouve ses membres, puis un corps complet de jeune homme. Ensuite, il construit une maison t&ocirc;l&eacute;e et prend huit &eacute;pouses. Pendant ce temps, sans enfant pour les aider, les vieux parents errent en brousse o&ugrave; ils tentent de survivre en se nourrissant de souchets sauvages. C'est alors que le gar&ccedil;on leur fait parvenir de la nourriture par magie. Il va ensuite &agrave; cheval &agrave; leur rencontre. Les parents, qui le prennent pour un Mandara, ont tout d'abord peur de lui. Puis ils finissent par reconna&icirc;tre leur enfant et celui-ci leur offre alors le g&icirc;te et le couvert.</p>
