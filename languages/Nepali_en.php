      <h2>2. Folktales from far Western Nepal</h2>
      <p>&quot;The old woodcutter&quot; was recorded and transcribed by Prem Bahadur Shahi, of Gaighat, Surkhet, and translated with further help from Rajesh Khatiwada. The remaining texts were transcribed and translated by  Bharat Kumar Saud of Chalsa, Achham, who helped Boyd Michailovsky with the linguistic analysis, and by Prem Bahadur Shahi.</p>
      <p>The local language of Achham is not normally written. These stories are transcribed in Devanagari, in the absence of a full phonological analysis. A provisional phonological transcription, using the IPA, is used for the two stories told by Mrs Lal Shobha Tamatar. </p>
      <p class="intertitre"><a href="../tools/show_text_en.php?id=crdo-NEP_DAURE_SOUND&idref=crdo-NEP_DAURE"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0"> The Old Woodcutter </a><br>
        Nepal, Surkhet, Ghatgaun, 2002, Dholi Laphe, Marie Lecomte-Tilouine<br>
        How it came about that man does not see Death, although Death sees her victims.      </p>
      <p class="intertitre"><a href="../tools/show_text_en.php?id=crdo-NEP_SYALDB_SOUND&idref=crdo-NEP_SYALDB"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0"> <strong>Tiger and Jackal / [nep] Bag ra syal</strong></a><strong></strong><br>
        Nepal, Seti Zone, Achham District, Dhanku VDC, Dungala, 2007, &quot;DB&quot; (anonyme), Marie Lecomte-Tilouine</p>
      <p>Traditional story. The speaker is an elderly Brahman woman. The story is told in some haste.</p>
      <p class="intertitre"><a href="../tools/show_text_en.php?id=crdo-NEP_LATODB_SOUND&idref=crdo-NEP_LATODB"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0"> <strong>The Cretin Brother (1) / [nep] Lato Bha</strong></a><strong>i</strong><br>
        Nepal, Seti Zone, Achham District, Dhanku VDC, Dungala, 2007, conteur anonyme, Marie Lecomte-Tilouine</p>
      <p>Traditional story, also known as &quot;The long-haired girl&quot; (nep. &quot;Lamcultheni&quot;), told by an elderly brahman woman. </p>
      <p class="intertitre"><a href="../tools/show_text_en.php?id=crdo-NEP_LATOLS_SOUND&idref=crdo-NEP_LATOLS"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0"><strong> The Cretin Brother (2) / [nep] Lato Bhai</strong></a><strong></strong><br>
