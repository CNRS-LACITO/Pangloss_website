      <h1 align="center">Nepali Corpus </h1>
      <p class="intertitre">&nbsp;</p>
      <p class="intertitre"><a href="../../ALC/Languages/Nepali_popup.htm" target="_blank" onClick="window.open(this.href,'popupLink','width=400,height=400,scrollbars=yes,resizable=yes',1);return false"><img src="../../images/icones/info.gif" alt="a" width="15" height="15" border="0">Nepali</a>: a brief note </p>
      <p>&nbsp;</p>
      <p>Nepali is an Indo-Aryan language of the Eastern Pahari subgroup. It is the official language of Nepal, and the lingua franca of the Indian state of Sikkim and of Darjiling district in the state of West Bengal. </p>
      <p>&nbsp;</p>
       <h4 align="left">Resources :</h4>
      <p class="intertitre">
      <img src="../../images/icones/h_parleur.gif" alt="a" width="20" height="20"> <a href="../tools/list_rsc_en.php?lg=Nepali&aff=Nepali" target="_blank"> All available resources here
      </a></p>
      <p class="intertitre">&nbsp;</p>
      <h2>1. Epics, or heroic sung narratives</h2>
      <p>This epic genre (<em>bhārat</em>) is declaimed and sung by bards and musicians of the tailor caste. It is also known as <em>huḍkelī</em>, after the hourglass-shaped drum, the <em>huḍ ko</em>, played by the performers. </p>
      <p>The recordings presented were made in 1969 in Dandeldhura District, Nepal, by Marc Gaborieau and Mireille Helffer of the French National Center for Scientific Research (CNRS). The language is not standard Nepali, but Dotyal, spoken in far western Nepal. The recordings were transcribed by Man Bahadur Kathayat in 1969, and more recently by Jaya Raj Pant and Man Bahadur Shahi, and translated by Jaya Raj Pant (into standard Nepali) and Boyd Michailovsky and Marie Lecomte-Tilouine.</p>
      <p class="intertitre"> <a href="../tools/show_text_en.php?id=crdo-NEP_INVOCATION69_SOUND&idref=crdo-NEP_INVOCATION69"><img src="../../images/icones/Txt_Inter_parchemin.jpg" alt="a" width="20" height="20" border="0">Invocation</a><br>
        Népal, Mahakali Zone, Dadeldhura District, Amargadhi VDC, 1969, bard Camalek Bahadur Dholi, 
        B. Michailovsky<br>
        Traditional invocation of deities and introduction to a performance.</p>
